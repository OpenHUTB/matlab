    methods(Access=private)
        function faces=getFaces(~,numPoints,scanlimits)


            azlimits=scanlimits(1,:);
            if diff(azlimits)>=360
                useSides=false;
            else
                useSides=true;
            end



            left=2:numPoints+1;
            top=(1:numPoints).*numPoints+1;
            bottom=2+((1:numPoints)-1).*numPoints;
            right=numPoints^2+2+(-numPoints:-1);

            if~useSides
                faces=zeros(2*numPoints*(numPoints-1),3);
                for i=1:numPoints-1
                    faces(i,:)=[1,bottom(i),bottom(i+1)];
                end
                for i=1:numPoints-1
                    faces(numPoints-1+i,:)=[1,top(i),top(i+1)];
                end
                count=2*(numPoints-1);
            else
                faces=zeros(2*(numPoints^2-1),3);
                for i=1:numPoints-1
                    faces(i,:)=[1,left(i),left(i+1)];
                end
                for i=1:numPoints-1
                    faces(numPoints-1+i,:)=[1,top(i),top(i+1)];
                end
                for i=1:numPoints-1
                    faces(2*(numPoints-1)+i,:)=[1,right(i),right(i+1)];
                end
                for i=1:numPoints-1
                    faces(3*(numPoints-1)+i,:)=[1,bottom(i),bottom(i+1)];
                end
                count=4*(numPoints-1);
            end


            for j=1:numPoints-1
                for k=1:numPoints-1

                    count=count+1;
                    faces(count,:)=[...
                    1+k+(j-1)*numPoints...
                    ,1+k+1+(j-1)*numPoints...
                    ,1+k+(j)*numPoints];

                    count=count+1;
                    faces(count,:)=[...
                    1+k+1+(j-1)*numPoints...
                    ,1+k+1+(j)*numPoints...
                    ,1+k+(j)*numPoints];
                end
            end
        end
    end
