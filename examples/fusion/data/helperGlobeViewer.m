            end

            hasCov=nargin>2;
            if hasCov
                [xB,cB]=fusion.internal.frames.transformChildToParent(xA,cA,orient,offset);
            else
                stateSz=size(xA,1);
                numVals=size(xA,2);
                unused=NaN(stateSz,stateSz,numVals,'like',xA);
                [xB,cB]=fusion.internal.frames.transformChildToParent(xA,unused,orient,offset);
            end
        end
