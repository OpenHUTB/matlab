%#codegen



function validateMatFileAndVariableName(matfile,variableName)
    coder.inline('always');
    coder.extrinsic('exist');
    coder.allowpcode('plain');



    coder.internal.assert((coder.internal.isCharOrScalarString(matfile)),...
    'gpucoder:cnncodegen:invalid_filename',...
    matfile);

    if coder.const(feval('endsWith',matfile,'.mat'))
        coder.internal.assert((coder.internal.isConst(matfile)&&...
        coder.const(exist(matfile,'file'))),...
        'gpucoder:cnncodegen:invalid_matfile',...
        matfile);
    else
        coder.internal.assert(coder.internal.isConst(matfile),...
        'gpucoder:cnncodegen:invalid_filename',...
        matfile);
    end

    coder.internal.assert((coder.internal.isCharOrScalarString(variableName)&&...
    coder.internal.isConst(variableName)...
    ),...
    'dlcoder_spkg:cnncodegen:invalid_fieldname',...
    variableName);


end
