%#codegen







function checkInputsForVaryingSize(oldInputSize,newInputSize,callerFunction)


    coder.allowpcode('plain');
    coder.inline('always');
    coder.extrinsic('strcat');
    coder.extrinsic('num2str');


    coder.internal.assert(coder.const(@isequal,oldInputSize(1:3),newInputSize(1:3)),...
    'dlcoder_spkg:cnncodegen:VaryingInputSize',...
    coder.const(strcat('[',coder.const(@num2str,newInputSize(1:3)),']')),...
    coder.const(strcat('[',coder.const(@num2str,oldInputSize(1:3)),']')),...
callerFunction...
    );


    coder.internal.assert(coder.const(@isequal,oldInputSize(4),newInputSize(4)),...
    'dlcoder_spkg:cnncodegen:VaryingMiniBatchSize',...
    newInputSize(4),...
    oldInputSize(4),...
callerFunction...
    );
end