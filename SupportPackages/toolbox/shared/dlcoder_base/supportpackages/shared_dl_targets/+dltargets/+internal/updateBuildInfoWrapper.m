function updateBuildInfoWrapper(buildinfo,...
    codegendir,...
    networkInfo,...
    dlConfigInfo,...
    calibrationBatchFilePath,...
    dlCodegenOptionsCallback,...
    usePrecompiledLibraries)





    inputSizes=networkInfo.CodegenInputSizes;
    dlcodeCfg.DeepLearningConfig=dlConfigInfo;
    dlcodeCfg.BatchSize=inputSizes{1}(4);
    dlcodeCfg.TargetDir=codegendir;
    isYoloV2Network=any(arrayfun(@(layer)isa(layer,'nnet.cnn.layer.YOLOv2TransformLayer'),...
    networkInfo.SortedLayers));

    dltargets.internal.updateBuildInfo(buildinfo,...
    codegendir,...
    inputSizes,...
    dlcodeCfg,...
    calibrationBatchFilePath,...
    isYoloV2Network,...
    dlCodegenOptionsCallback,...
    networkInfo,...
    usePrecompiledLibraries);

end
