function[srcs,headers,datafiles]=prepareTargetSpecificFiles(dlcodeCfg,...
    inputSizes,...
    calibrationBatchFilePath,...
    isYoloV2Network,...
    dlCodegenOptionsCallback,...
    networkInfo)





    srcs={};
    headers={};
    datafiles={};

    if strcmpi(dlcodeCfg.DeepLearningConfig.TargetLibrary,'tensorrt')
        if(strcmpi(dlcodeCfg.DeepLearningConfig.DataType,'int8'))
            assert(numel(inputSizes)==1);
            inputSize=inputSizes{1};

            datafiles=dltargets.tensorrt.batchCreate(dlcodeCfg,...
            inputSize,...
            calibrationBatchFilePath,...
            isYoloV2Network);
        else
            datafiles=dltargets.tensorrt.batchCreateWithImageDatastore(inputSizes{1},...
            calibrationBatchFilePath,...
            isYoloV2Network,...
            dlCodegenOptionsCallback,...
            networkInfo);
        end
    end

end
