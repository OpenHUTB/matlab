function clNet=elabCheckMatrixLUTNetwork(this,topNet,blockInfo,dataRate)






    ufix1Type=pir_ufixpt_t(1,0);
    ufix2Type=pir_ufixpt_t(2,0);
    ufix3Type=pir_ufixpt_t(3,0);
    ufix4Type=pir_ufixpt_t(4,0);
    ufix5Type=pir_ufixpt_t(5,0);
    vType=pir_ufixpt_t(blockInfo.shiftWL,0);
    oType=pir_ufixpt_t(3,0);
    fType=pirelab.getPirVectorType(vType,blockInfo.finalVec);

    if strcmpi(blockInfo.Standard,'IEEE 802.11 n/ac/ax')
        vAddrType=pir_ufixpt_t(9,0);
        oAddrType=pir_ufixpt_t(8,0);
        fAddrType=pir_ufixpt_t(4,0);
        vSize=24;WL=6;
    else
        vAddrType=pir_ufixpt_t(6,0);
        oAddrType=pir_ufixpt_t(5,0);
        fAddrType=pir_ufixpt_t(2,0);
        vSize=16;WL=4;
    end
    v1Type=pir_ufixpt_t(blockInfo.shiftWL*WL,0);
    o1Type=pir_ufixpt_t(3*WL,0);


    clNet=pirelab.createNewNetwork(...
    'Network',topNet,...
    'Name','CheckMatrixLUT',...
    'Inportnames',{'blklen','rate','countlayer','iterind','validcount'},...
    'InportTypes',[ufix2Type,ufix2Type,ufix4Type,ufix1Type,ufix5Type],...
    'InportRates',[dataRate,dataRate,dataRate,dataRate,dataRate],...
    'Outportnames',{'vshift','oshift','fshift'},...
    'OutportTypes',[vType,oType,fType]...
    );



    blklen=clNet.PirInputSignals(1);
    coderate=clNet.PirInputSignals(2);
    countlayer=clNet.PirInputSignals(3);
    iterind=clNet.PirInputSignals(4);
    validcount=clNet.PirInputSignals(5);

    vshift=clNet.PirOutputSignals(1);
    oshift=clNet.PirOutputSignals(2);
    finalvshift=clNet.PirOutputSignals(3);








    sFlag=strcmpi(blockInfo.Standard,'IEEE 802.11 n/ac/ax');

    [vLUT1,vLUT2,vLUT3,vLUT4]=shiftPCMLUT(sFlag);
    [oLUT1,oLUT2,oLUT3,oLUT4]=offsetPCMLUT(sFlag);
    [fLUT1,fLUT2,fLUT3,fLUT4]=finalShiftPCMLUT(sFlag);

    const0=clNet.addSignal(ufix1Type,'const0');
    pirelab.getConstComp(clNet,const0,0);

    const1=clNet.addSignal(ufix1Type,'const1');
    pirelab.getConstComp(clNet,const1,1);

    countlayerd=clNet.addSignal(countlayer.Type,'cntlayer_Sub');
    pirelab.getSubComp(clNet,[countlayer,const1],countlayerd,'floor','wrap','Sub Comp');


    vaddr=clNet.addSignal(vAddrType,'vAddr');
    oaddr=clNet.addSignal(oAddrType,'oAddr');
    faddr=clNet.addSignal(fAddrType,'fAddr');

    if sFlag
        pirelab.getBitConcatComp(clNet,[iterind,blklen,coderate,countlayerd],vaddr,'vaddr');
        pirelab.getBitConcatComp(clNet,[blklen,coderate,countlayerd],oaddr,'oaddr');
        pirelab.getBitConcatComp(clNet,[blklen,coderate],faddr,'faddr');
    else
        clayer_dtc=clNet.addSignal(ufix3Type,'cntlayer_Sub');
        pirelab.getDTCComp(clNet,countlayerd,clayer_dtc,'Floor','Wrap','SI');
        pirelab.getBitConcatComp(clNet,[iterind,coderate,clayer_dtc],vaddr,'oaddr');
        pirelab.getBitConcatComp(clNet,[coderate,clayer_dtc],oaddr,'vaddr');
        pirelab.getWireComp(clNet,coderate,faddr,'faddr');
    end


    vshifttmp=shiftLUTBitExtraction(clNet,vLUT1,vLUT2,vLUT3,vLUT4,vaddr,vType,v1Type,validcount,vshift,vSize);
    oshifttmp=offsetLUTBitExtraction(clNet,oLUT1,oLUT2,oLUT3,oLUT4,oaddr,oType,o1Type,validcount,oshift,vSize);
    fshifttmp=finshiftLUTBitExtraction(clNet,fLUT1,fLUT2,fLUT3,fLUT4,faddr,vType,v1Type,vSize);

    pirelab.getUnitDelayComp(clNet,vshifttmp,vshift,0,'');

    oshifttmp1=clNet.addSignal(oshift.Type,'oShiftTmp');
    pirelab.getSwitchComp(clNet,[oshifttmp,const0],oshifttmp1,iterind,'sel','==',0,'Floor','Wrap');

    if blockInfo.VectorSize==1
        pirelab.getConstComp(clNet,oshift,0);
    else
        pirelab.getUnitDelayComp(clNet,oshifttmp1,oshift,0,'');
    end

    fshifttmp1=clNet.addSignal(finalvshift.Type,'fShiftTmp');
    this.muxSignal(clNet,fshifttmp,fshifttmp1);
    pirelab.getUnitDelayComp(clNet,fshifttmp1,finalvshift,0,'');



