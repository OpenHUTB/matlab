function oneByNVarMulLLRSc=elabOneByNVarMulLLRScNet(~,topNet,~,rate,inpWL,inpFL,inNVarFL)





    pirTyp2=pir_boolean_t;
    pirTyp1=pir_sfixpt_t(inpWL+3,inpFL);
    pirTyp4=pir_sfixpt_t(inpWL+14,inpFL);
    pirTyp5=pir_sfixpt_t(32+inpWL+3,-(31+inNVarFL)+inpFL);
    pirTyp3=pir_ufixpt_t(32,-(31+inNVarFL));

    fiMath1=fimath('RoundingMethod','Nearest','OverflowAction','Saturate','ProductMode','FullPrecision','SumMode','FullPrecision');

    nt2=numerictype(0,32,(31+inNVarFL));
    nt1=numerictype(1,inpWL+3,-inpFL);
    nt3=numerictype(1,inpWL+14,-inpFL);
    nt4=numerictype(1,32+inpWL+3,-(-(31+inNVarFL)+inpFL));

    inportNameMulLLRSc={'dataIn','validIn','divideIn'};
    controlType=pir_ufixpt_t(1,0);
    inType=pir_sfixpt_t(inpWL+3,inpFL);
    inTypeMulLLRSc=[inType,controlType,pir_ufixpt_t(32,-(31+inNVarFL))];
    inDataRateMulLLRSc=[rate,rate,rate];

    outportNameMulLLRSc={'dataOut','validOut'};
    outTypeMulLLRSc=[pirTyp4,controlType];

    oneByNVarMulLLRSc=pirelab.createNewNetwork(...
    'Network',topNet,...
    'Name','oneByNVarMulLLRSc',...
    'InportNames',inportNameMulLLRSc,...
    'InportTypes',inTypeMulLLRSc,...
    'InportRates',inDataRateMulLLRSc,...
    'OutportNames',outportNameMulLLRSc,...
    'OutportTypes',outTypeMulLLRSc...
    );


    dataLLROut_s0=oneByNVarMulLLRSc.PirInputSignals(1);
    validLLROut_s1=oneByNVarMulLLRSc.PirInputSignals(2);
    oneByNVarIn_s2=oneByNVarMulLLRSc.PirInputSignals(3);

    dataOut=oneByNVarMulLLRSc.PirOutputSignals(1);
    validOut=oneByNVarMulLLRSc.PirOutputSignals(2);

    slRate1=rate;

    SwitchLLRNVarOp_out1_s9=addSignal(oneByNVarMulLLRSc,'SwitchLLRNVarOp_out1',pirTyp4,slRate1);
    delayMatch1_out1_s11=addSignal(oneByNVarMulLLRSc,'delayMatch1_out1',pirTyp2,slRate1);

    HwModeRegister_out1_s3=addSignal(oneByNVarMulLLRSc,'HwModeRegister_out1',pirTyp1,slRate1);
    HwModeRegister1_out1_s4=addSignal(oneByNVarMulLLRSc,'HwModeRegister1_out1',pirTyp3,slRate1);
    LLRWithNVarConst_out1_s5=addSignal(oneByNVarMulLLRSc,'LLRWithNVarConst_out1',pirTyp4,slRate1);
    LLRwithNvarDTC_out1_s6=addSignal(oneByNVarMulLLRSc,'LLRwithNvarDTC _out1',pirTyp4,slRate1);
    PipelineRegister_out1_s7=addSignal(oneByNVarMulLLRSc,'PipelineRegister_out1',pirTyp5,slRate1);
    ProdLLR1ByNVar_out1_s8=addSignal(oneByNVarMulLLRSc,'ProdLLR1ByNVar_out1',pirTyp5,slRate1);
    delayMatch_out1_s10=addSignal(oneByNVarMulLLRSc,'delayMatch_out1',pirTyp2,slRate1);

    pirelab.getAnnotationComp(oneByNVarMulLLRSc,...
    'DataTypeDuplicateNVar');


    pirelab.getIntDelayComp(oneByNVarMulLLRSc,...
    dataLLROut_s0,...
    HwModeRegister_out1_s3,...
    1,'HwModeRegister',...
    fi(0,nt1,fiMath1,'hex','00000'),...
    0,0,[],0,0);


    pirelab.getIntDelayComp(oneByNVarMulLLRSc,...
    oneByNVarIn_s2,...
    HwModeRegister1_out1_s4,...
    1,'HwModeRegister1',...
    fi(0,nt2,fiMath1,'hex','00000000'),...
    0,0,[],0,0);


    pirelab.getConstComp(oneByNVarMulLLRSc,...
    LLRWithNVarConst_out1_s5,...
    fi(0,nt3,fiMath1,'hex','00000000'),...
    'LLRWithNVarConst','on',1,'','','');


    pirelab.getIntDelayComp(oneByNVarMulLLRSc,...
    ProdLLR1ByNVar_out1_s8,...
    PipelineRegister_out1_s7,...
    2,'PipelineRegister',...
    fi(0,nt4,fiMath1,'hex','0000000000000'),...
    0,0,[],0,0);


    pirelab.getIntDelayComp(oneByNVarMulLLRSc,...
    validLLROut_s1,...
    delayMatch_out1_s10,...
    3,'delayMatch',...
    false,...
    0,0,[],0,0);


    pirelab.getIntDelayComp(oneByNVarMulLLRSc,...
    validLLROut_s1,...
    delayMatch1_out1_s11,...
    3,'delayMatch1',...
    false,...
    0,0,[],0,0);


    pirelab.getDTCComp(oneByNVarMulLLRSc,...
    PipelineRegister_out1_s7,...
    LLRwithNvarDTC_out1_s6,...
    'Floor','Wrap','RWV','LLRwithNvarDTC ');


    pirelab.getMulComp(oneByNVarMulLLRSc,...
    [HwModeRegister_out1_s3,HwModeRegister1_out1_s4],...
    ProdLLR1ByNVar_out1_s8,...
    'Floor','Wrap','ProdLLR1ByNVar','**','',-1,0);


    pirelab.getSwitchComp(oneByNVarMulLLRSc,...
    [LLRwithNvarDTC_out1_s6,LLRWithNVarConst_out1_s5],...
    SwitchLLRNVarOp_out1_s9,...
    delayMatch_out1_s10,'SwitchLLRNVarOp',...
    '~=',0,'Floor','Wrap');


    pirelab.getWireComp(oneByNVarMulLLRSc,SwitchLLRNVarOp_out1_s9,dataOut);
    pirelab.getWireComp(oneByNVarMulLLRSc,delayMatch1_out1_s11,validOut);
end

function hS=addSignal(oneByNVarMulLLRSc,sigName,pirTyp,simulinkRate)
    hS=oneByNVarMulLLRSc.addSignal;
    hS.Name=sigName;
    hS.Type=pirTyp;
    hS.SimulinkHandle=0;
    hS.SimulinkRate=simulinkRate;
end