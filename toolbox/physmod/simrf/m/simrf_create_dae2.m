function dae=simrf_create_dae2(mp,sp,solver,dae,inputInfo,outputInfo)





    [tones,harmonics,sim_noise,sep_stream,noise_seed,...
    RelTol,AbsTol,MaxIter,ErrorEstimationType,SmallSignalApprox,...
    AllSimFreqs,SimFreqsInternal,spf]=simrf_solverparam(solver);

    additionalLocalSolverParams=struct('RelTol',RelTol,...
    'AbsTol',AbsTol,...
    'MaxIter',MaxIter,...
    'ErrorEstimationType',ErrorEstimationType,...
    'SmallSignalApprox',SmallSignalApprox,...
    'AllSimFreqs',AllSimFreqs,...
    'SimFreqs',SimFreqsInternal);

    [inputData,outputData]=simrf_inputoutputparam(dae,inputInfo,outputInfo);




    dae=ssc_create_frequency_domain_dae(dae,mp,sp,...
    tones,harmonics,...
    additionalLocalSolverParams,...
    sim_noise,...
    sep_stream,...
    noise_seed,...
    spf,...
    inputData,...
    outputData);
end


