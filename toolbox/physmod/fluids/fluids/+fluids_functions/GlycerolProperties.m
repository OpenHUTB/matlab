    ];


    propTables=CoolProp_MGL;


    T_valid_TLU=propTables.T_valid_TLU;
    T_TLU=interp1(c_TLU,T_valid_TLU',c_mass,'linear')';
    T_min=T_TLU(1);
    T_max=T_TLU(end);


    p_TLU=logspace(log10(p_min),log10(p_max),19);

    T_TLU=T_TLU(:);
    p_TLU=p_TLU(:);
    c_TLU=c_TLU(:);


    rho_vec=interp2(c_TLU,T_TLU,propTables.rho_T_c_TLU,c_mass,T_TLU,'linear');
    drho_dT_vec=interp2(c_TLU,T_TLU,propTables.drho_dT_TLU,c_mass,T_TLU,'linear');
    u_vec=interp2(c_TLU,T_TLU,propTables.u_T_c_TLU,c_mass,T_TLU,'linear');
    cp_vec=interp2(c_TLU,T_TLU,propTables.cp_T_c_TLU,c_mass,T_TLU,'linear');
    k_vec=interp2(c_TLU,T_TLU,propTables.k_T_c_TLU,c_mass,T_TLU,'linear');
