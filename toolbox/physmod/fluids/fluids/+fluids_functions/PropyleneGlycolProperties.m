function Properties=PropyleneGlycolProperties(concentration_param,c_mass,c_vol,beta_const,p_min,p_max,p_ref)

















































    switch concentration_param
    case fluids.thermal_liquid.utilities.enum.thermal_liquid_concentration.volume_fraction
        propTables=CoolProp_APG;
        c=c_vol;

    otherwise
        propTables=CoolProp_MPG;
        c=c_mass;
    end


    c_TLU=[
0.0
0.06
0.12
0.18
0.24
0.30
0.36
0.42
0.48
0.54
0.60
    ];


    T_valid_TLU=propTables.T_valid_TLU;
    T_TLU=interp1(c_TLU,T_valid_TLU',c,'linear')';
    T_min=T_TLU(1);
    T_max=T_TLU(end);


    p_TLU=logspace(log10(p_min),log10(p_max),19);

    T_TLU=T_TLU(:);
    p_TLU=p_TLU(:);
    c_TLU=c_TLU(:);


    rho_vec=interp2(c_TLU,T_TLU,propTables.rho_T_c_TLU,c,T_TLU,'linear');
    drho_dT_vec=interp2(c_TLU,T_TLU,propTables.drho_dT_TLU,c,T_TLU,'linear');
    u_vec=interp2(c_TLU,T_TLU,propTables.u_T_c_TLU,c,T_TLU,'linear');
    cp_vec=interp2(c_TLU,T_TLU,propTables.cp_T_c_TLU,c,T_TLU,'linear');
    k_vec=interp2(c_TLU,T_TLU,propTables.k_T_c_TLU,c,T_TLU,'linear');
    nu_vec=interp2(c_TLU,T_TLU,propTables.nu_T_c_TLU,c,T_TLU,'linear');
    mu_vec=interp2(c_TLU,T_TLU,propTables.mu_T_c_TLU,c,T_TLU,'linear');
    Pr_vec=interp2(c_TLU,T_TLU,propTables.Pr_T_c_TLU,c,T_TLU,'linear');

    rho_vec=rho_vec(:);
    drho_dT_vec=drho_dT_vec(:);



    rho_TLU=bsxfun(@times,rho_vec(:),exp((p_TLU'-p_ref)./(beta_const)));
    drho_TLU_dT=bsxfun(@times,drho_dT_vec,exp((p_TLU'-p_ref)./(beta_const)));
    alpha_TLU=bsxfun(@times,-1./rho_TLU,drho_TLU_dT);


    Properties.T_TLU=T_TLU;
    Properties.p_TLU=p_TLU;
    Properties.pT_validity_TLU=ones(size(rho_TLU));
    Properties.rho_TLU=rho_TLU;
    Properties.alpha_TLU=alpha_TLU;
    Properties.beta_TLU=repmat(beta_const,size(rho_TLU));
    Properties.u_TLU=repmat(u_vec(:),1,length(p_TLU));
    Properties.cp_TLU=repmat(cp_vec(:),1,length(p_TLU));
    Properties.k_TLU=repmat(k_vec(:),1,length(p_TLU));
    Properties.nu_TLU=repmat(nu_vec(:),1,length(p_TLU));
    Properties.mu_TLU=repmat(mu_vec(:),1,length(p_TLU));
    Properties.Pr_TLU=repmat(Pr_vec(:),1,length(p_TLU));
    Properties.T_min=T_min;
    Properties.T_max=T_max;
    Properties.p_min=p_min;
    Properties.p_max=p_max;

end

