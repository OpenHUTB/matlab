    Properties.T_TLU=T_TLU;
    Properties.p_TLU=p_TLU;
    Properties.pT_validity_TLU=ones(size(rho_TLU));
    Properties.rho_TLU=rho_TLU;
    Properties.alpha_TLU=alpha_TLU;
    Properties.beta_TLU=repmat(beta_const,size(rho_TLU));
    Properties.u_TLU=repmat(u_vec(:),1,length(p_TLU));
    Properties.cp_TLU=repmat(cp_vec(:),1,length(p_TLU));
    Properties.k_TLU=repmat(k_vec(:),1,length(p_TLU));
    Properties.nu_TLU=repmat(nu_vec(:),1,length(p_TLU));
    Properties.mu_TLU=repmat(mu_vec(:),1,length(p_TLU));
    Properties.Pr_TLU=repmat(Pr_vec(:),1,length(p_TLU));
    Properties.T_min=T_min;
