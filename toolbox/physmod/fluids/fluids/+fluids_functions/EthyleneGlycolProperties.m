function Properties=EthyleneGlycolProperties(concentration_param,c_mass,c_vol,beta_const,p_min,p_max,p_ref)



















































    switch concentration_param
    case fluids.thermal_liquid.utilities.enum.thermal_liquid_concentration.volume_fraction
        propTables=CoolProp_ZM;
