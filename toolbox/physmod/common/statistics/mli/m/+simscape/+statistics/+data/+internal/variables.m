function out=variables(s)
    import simscape.statistics.data.internal.Statistic

    out=Statistic(...
    'Data',lVariableTree(s),...
    'Name',s.Name,...
    'Description',s.Description);

    function t=lVariableTree(ch)
        t=struct(...
        'ID',{ch.ID},...
        'Name',{ch.Name},...
        'Value',{ch.Value},...
        'Description',{ch.Description},...
        'Sources',{ch.Sources},...
        'Children',{[]});
        for idx=1:numel(t)
            t(idx).Sources=simscape.statistics.data.internal.block_sources(t(idx).Sources);
        end
        for idx=1:numel(ch)
            if~isempty(ch(idx).Children)
                t(idx).Children=lVariableTree(ch(idx).Children);
            end
        end
    end
end