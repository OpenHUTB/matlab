function varargout=ee_convertedMosfetValidation(fileName,varargin)
















































































































    if~((nargout==0)||(nargout==1))
        pm_error("physmod:ee:SPICE2sscvalidation:OutputArgumentsError",nargout);
    end

    [outputData]=ee.internal.validation.mosfet.convertedMOSFETValidation(fileName,varargin{:});

    if(nargout==1)
        varargout{1}=outputData;
    end


end