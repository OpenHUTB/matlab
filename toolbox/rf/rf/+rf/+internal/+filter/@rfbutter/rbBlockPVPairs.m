function[NameArray,ValueArray]=rbBlockPVPairs(obj)




    import rf.internal.rfbudget.RFBudget.engunitsGLimited
    [Fp_norm,~,Up]=engunitsGLimited(obj.PassbandFrequency);
    [Fs_norm,~,Us]=engunitsGLimited(obj.StopbandFrequency);

    responseStr=regexprep(lower(obj.ResponseType),...
    '(ow|igh|and|ass|top)','');
    if obj.UseFilterOrder
        if strcmpi(responseStr,'bs')
            NameArray={'FilterOrder',...
            ['StopFreq_',responseStr],...
            ['StopFreq_',responseStr,'_unit']...
            ,'StopAtten'};
            ValueArray={sprintf('%.15g',obj.FilterOrder),...
            sprintf('[%.15g %.15g]',Fs_norm),...
            strcat(Us,'Hz'),...
            sprintf('%.15g',obj.StopbandAttenuation)};
        elseif strcmpi(responseStr,'bp')
            NameArray={'FilterOrder',...
            ['PassFreq_',responseStr],...
            ['PassFreq_',responseStr,'_unit'],...
            'PassAtten'};
            ValueArray={sprintf('%.15g',obj.FilterOrder),...
            sprintf('[%.15g %.15g]',Fp_norm),...
            strcat(Up,'Hz'),...
            sprintf('%.15g',obj.PassbandAttenuation)};
        else
            NameArray={'FilterOrder',...
            ['PassFreq_',responseStr],...
            ['PassFreq_',responseStr,'_unit'],...
            'PassAtten'};
            ValueArray={sprintf('%.15g',obj.FilterOrder),...
            sprintf('%.15g',Fp_norm),...
            strcat(Up,'Hz'),...
            sprintf('%.15g',obj.PassbandAttenuation)};
        end
    else
        NameArray={['PassFreq_',responseStr],...
        ['PassFreq_',responseStr,'_unit'],...
        ['StopFreq_',responseStr],...
        ['StopFreq_',responseStr,'_unit'],...
        'PassAtten',...
        'StopAtten'};
        if strcmpi(responseStr(1),'b')
            ValueArray={...
            sprintf('[%.15g %.15g]',Fp_norm),...
            strcat(Up,'Hz'),...
            sprintf('[%.15g %.15g]',Fs_norm),...
            strcat(Us,'Hz'),...
            sprintf('%.15g',obj.PassbandAttenuation),...
            sprintf('%.15g',obj.StopbandAttenuation)};
        else
            ValueArray={...
            sprintf('%.15g',Fp_norm),...
            strcat(Up,'Hz'),...
            sprintf('%.15g',Fs_norm),...
            strcat(Us,'Hz'),...
            sprintf('%.15g',obj.PassbandAttenuation),...
            sprintf('%.15g',obj.StopbandAttenuation)};
        end
    end
end
