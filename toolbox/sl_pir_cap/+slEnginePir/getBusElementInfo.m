function infos=getBusElementInfo(busName)
    infos=struct('Name',{},...
    'Complexity',{},...
    'Dimensions',{},...
    'DataType',{},...
    'Min',{},...
    'Max',{});
    try
        slBusInfo=evalin('base',busName);
        numElements=size(slBusInfo.Elements);
        for idx=1:numElements(1)
            element=slBusInfo.Elements(idx);
            info=struct('Name',element.Name,...
            'Complexity',element.Complexity,...
            'Dimensions',element.Dimensions,...
            'DataType',element.DataType,...
            'Min',element.Min,...
            'Max',element.Max);
            infos=[infos,info];
        end
    end
end