function configStruct=getVariantConfigurationStruct(zeroSize)
    configStruct=struct(...
    'Name','',...
    'Description','',...
    'ControlVariables',slvariants.internal.config.types.getControlVariableStruct(true),...
    'SubModelConfigurations',slvariants.internal.config.types.getSubModelConfigurationStruct(true));
    if nargin==1&&zeroSize
        configStruct(end)=[];
    end
end
