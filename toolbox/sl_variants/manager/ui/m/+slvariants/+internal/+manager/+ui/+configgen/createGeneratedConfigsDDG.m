function generatedConfigsDDGComp=createGeneratedConfigsDDG(modelHandle)




    modelName=getfullname(modelHandle);
    generatedConfigsDDGComp=GLUE2.DDGComponent(message('Simulink:VariantManagerUI:AutoGeneratedConfigsDDG').getString());
    generatedConfigsDDGComp.Title=message('Simulink:VariantManagerUI:AutoGeneratedConfigsTitle').getString();
    generatedConfigsDDGComp.UserFloatable=false;
    newDialogSource=slvariants.internal.manager.ui.configgen.GeneratedConfigsDialogSchema({},modelName);
    generatedConfigsDDGComp.updateSource(newDialogSource);
    studioContainer=slvariants.internal.manager.core.getStudioContainer(modelHandle);
    studioContainer.addComponent(generatedConfigsDDGComp,'right','tabbed');
end


