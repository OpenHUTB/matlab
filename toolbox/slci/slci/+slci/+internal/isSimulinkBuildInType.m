




function flag=isSimulinkBuildInType(typeName)

    assert(ischar(typeName));
    persistent simulinkTypes;

    if isempty(simulinkTypes)
        simulinkTypes={'double',...
        'single',...
        'int32',...
        'uint32',...
        'int16',...
        'uint16',...
        'int8',...
        'uint8',...
        'boolean',...
        'int',...
        'uint',...
        'char'};
    end

    flag=any(strcmp(typeName,simulinkTypes));

end
