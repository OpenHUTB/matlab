






function[bitPerChar,...
    bitPerShort,...
    bitPerInt,...
    bitPerLong,...
    bitPerLongLong,...
    bitPerFloat,...
    bitPerDouble,...
    bitPerPointer,...
    bitPerSizeT,...
    bitPerPtrDiffT,...
    wordSize,...
    endianness,...
    shiftRightIntArith,...
    largestAtomicInteger,...
    largestAtomicFloat,...
    intDivRoundTo,...
    longLongMode,...
    enabledStates]=getHardwareDetails(vendorName,...
    typeName)

    h=coder.HardwareImplementation;

    if~isempty(vendorName)&&~isempty(typeName)
        h.ProdHWDeviceType=[vendorName,'->',typeName];
    end

    bitPerChar=h.ProdBitPerChar;
    bitPerShort=h.ProdBitPerShort;
    bitPerInt=h.ProdBitPerInt;
    bitPerLong=h.ProdBitPerLong;
    bitPerLongLong=h.ProdBitPerLongLong;
    bitPerFloat=h.ProdBitPerFloat;
    bitPerDouble=h.ProdBitPerDouble;
    bitPerPointer=h.ProdBitPerPointer;
    bitPerSizeT=h.ProdBitPerSizeT;
    bitPerPtrDiffT=h.ProdBitPerPtrDiffT;
    wordSize=h.ProdWordSize;
    endianness=h.ProdEndianess;
    shiftRightIntArith=h.ProdShiftRightIntArith;
    intDivRoundTo=h.ProdIntDivRoundTo;
    largestAtomicInteger=h.ProdLargestAtomicInteger;
    largestAtomicFloat=h.ProdLargestAtomicFloat;
    longLongMode=h.ProdLongLongMode;

    enabledStates=[...
    h.isEnabledProperty('ProdBitPerChar'),...
    h.isEnabledProperty('ProdBitPerShort'),...
    h.isEnabledProperty('ProdBitPerInt'),...
    h.isEnabledProperty('ProdBitPerLong'),...
    h.isEnabledProperty('ProdBitPerLongLong'),...
    h.isEnabledProperty('ProdBitPerFloat'),...
    h.isEnabledProperty('ProdBitPerDouble'),...
    h.isEnabledProperty('ProdBitPerPointer'),...
    h.isEnabledProperty('ProdBitPerSizeT'),...
    h.isEnabledProperty('ProdBitPerPtrDiffT'),...
    h.isEnabledProperty('ProdWordSize'),...
    h.isEnabledProperty('ProdEndianess'),...
    h.isEnabledProperty('ProdShiftRightIntArith'),...
    h.isEnabledProperty('ProdIntDivRoundTo'),...
    h.isEnabledProperty('ProdLargestAtomicInteger'),...
    h.isEnabledProperty('ProdLargestAtomicFloat'),...
    h.isEnabledProperty('ProdLongLongMode')];
end
