    pk=findpackage('siggui');
    findclass(findpackage('sigdatatypes'),'parameter');

    c=schema.class(pk,'parameterdlg',pk.findclass('helpdialog'));

    p=[...
    schema.prop(c,'Name','ustring');...
    schema.prop(c,'Label','ustring');...
    schema.prop(c,'Parameters','sigdatatypes.parameter vector');...
    schema.prop(c,'HelpLocation','string vector');...
    schema.prop(c,'Tool','ustring');
    schema.prop(c,'StaticParameters','string vector');...
    schema.prop(c,'DisabledParameters','string vector');...
    ];

    p=[...
    schema.prop(c,'UserModifiedListener','handle.listener');...
    ];
    set(p,'AccessFlags.PublicGet','off','AccessFlags.PublicSet','off');


