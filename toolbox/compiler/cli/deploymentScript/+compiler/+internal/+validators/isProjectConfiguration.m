function val=isProjectConfiguration(data)
    if isa(data,"compiler.project.COMLibraryConfigurationOptions")||...
        isa(data,"compiler.project.CLibraryConfigurationOptions")||...
        isa(data,"compiler.project.CppLibraryConfigurationOptions")||...
        isa(data,"compiler.project.DotNETLibraryConfigurationOptions")||...
        isa(data,"compiler.project.JavaLibraryConfigurationOptions")||...
        isa(data,"compiler.project.PSAConfigurationOptions")||...
        isa(data,"compiler.project.PythonLibraryConfigurationOptions")||...
        isa(data,"compiler.project.StandaloneAppConfigurationOptions")||...
        isa(data,"compiler.project.WebAppConfigurationOptions")

        val=true;
    else
        val=false;
    end
end
