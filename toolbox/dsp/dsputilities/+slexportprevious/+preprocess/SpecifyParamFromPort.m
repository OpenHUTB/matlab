function SpecifyParamFromPort(obj)





    verobj=obj.ver;

    if isR2017bOrEarlier(verobj)

        blks_ma=obj.findBlocksWithMaskType(...
        'dsp.simulink.MovingAverage',...
        'ForgettingFactorPort','on');


        blks_mv=obj.findBlocksWithMaskType(...
        'dsp.simulink.MovingVariance',...
        'ForgettingFactorPort','on');


        blks_mrms=obj.findBlocksWithMaskType(...
        'dsp.simulink.MovingRMS',...
        'ForgettingFactorPort','on');


        blks_mstd=obj.findBlocksWithMaskType(...
        'dsp.simulink.MovingStandardDeviation',...
        'ForgettingFactorPort','on');


        blks_hampel=obj.findBlocksWithMaskType(...
        'dsp.simulink.HampelFilter',...
        'ThresholdPort','on');


        blks_chann=obj.findBlocksWithMaskType(...
        'dsp.simulink.Channelizer',...
        'CoefficientsPort','on');


        blks_synth=obj.findBlocksWithMaskType(...
        'dsp.simulink.ChannelSynthesizer',...
        'CoefficientsPort','on');


        blks_iirhbdecim=obj.findBlocksWithMaskType(...
        'dsp.simulink.IIRHalfbandDecimator',...
        'Specification','Coefficients',...
        'CoefficientsPort','on');


        blks_iirhbinterp=obj.findBlocksWithMaskType(...
        'dsp.simulink.IIRHalfbandInterpolator',...
        'Specification','Coefficients',...
        'CoefficientsPort','on');


        blks_allpass=obj.findBlocksWithMaskType(...
        'dsp.simulink.AllpassFilter',...
        'Structure','Minimum multiplier',...
        'AllpassCoefficientsPort','on');

        blks_allpasslattice=obj.findBlocksWithMaskType(...
        'dsp.simulink.AllpassFilter',...
        'Structure','Lattice',...
        'LatticeCoefficientsPort','on');


        blks_FreqDomainFIRFilter=obj.findBlocksWithMaskType(...
        'dsp.simulink.FrequencyDomainFIRFilter',...
        'NumeratorDomain','Time',...
        'SpecifyNumeratorFromPort','on');


        blks_parametriceqbandwidth=obj.findBlocksWithMaskType(...
        'dsp.simulink.ParametricEQFilter',...
        'Specification','Bandwidth and center frequency',...
        'BandwidthPort','on');

        blks_parametriceqcenterfrequency1=obj.findBlocksWithMaskType(...
        'dsp.simulink.ParametricEQFilter',...
        'Specification','Bandwidth and center frequency',...
        'CenterFrequencyPort','on');

        blks_parametriceqpeakgaindb=obj.findBlocksWithMaskType(...
        'dsp.simulink.ParametricEQFilter',...
        'Specification','Bandwidth and center frequency',...
        'PeakGaindBPort','on');

        blks_parametriceqcenterfrequency2=obj.findBlocksWithMaskType(...
        'dsp.simulink.ParametricEQFilter',...
        'Specification','Quality factor and center frequency',...
        'CenterFrequencyPort','on');

        blks_parametriceqqualityfactor=obj.findBlocksWithMaskType(...
        'dsp.simulink.ParametricEQFilter',...
        'Specification','Quality factor and center frequency',...
        'QualityFactorPort','on');

        blks_parametriceqbandwidthcoef=obj.findBlocksWithMaskType(...
        'dsp.simulink.ParametricEQFilter',...
        'Specification','Coefficients',...
        'BandwidthCoefficientPort','on');

        blks_parametriceqcenterfrequencycoef=obj.findBlocksWithMaskType(...
        'dsp.simulink.ParametricEQFilter',...
        'Specification','Coefficients',...
        'CenterFrequencyCoefficientPort','on');

        blks_parametriceqpeakgaincoef=obj.findBlocksWithMaskType(...
        'dsp.simulink.ParametricEQFilter',...
        'Specification','Coefficients',...
        'PeakGainPort','on');


        blks_notchpeakbandwidth=obj.findBlocksWithMaskType(...
        'dsp.simulink.NotchPeakFilter',...
        'Specification','Bandwidth and center frequency',...
        'BandwidthPort','on');

        blks_notchpeakcenterfrequency1=obj.findBlocksWithMaskType(...
        'dsp.simulink.NotchPeakFilter',...
        'Specification','Bandwidth and center frequency',...
        'CenterFrequencyPort','on');

        blks_notchpeakcenterfrequency2=obj.findBlocksWithMaskType(...
        'dsp.simulink.NotchPeakFilter',...
        'Specification','Quality factor and center frequency',...
        'CenterFrequencyPort','on');

        blks_notchpeakqualityfactor=obj.findBlocksWithMaskType(...
        'dsp.simulink.NotchPeakFilter',...
        'Specification','Quality factor and center frequency',...
        'QualityFactorPort','on');

        blks_notchpeakbandwidthcoef=obj.findBlocksWithMaskType(...
        'dsp.simulink.NotchPeakFilter',...
        'Specification','Coefficients',...
        'BandwidthCoefficientPort','on');

        blks_notchpeakcenterfrequencycoef=obj.findBlocksWithMaskType(...
        'dsp.simulink.NotchPeakFilter',...
        'Specification','Coefficients',...
        'CenterFrequencyCoefficientPort','on');


        blks_variablebandwidthfirbandwidthbandpass=obj.findBlocksWithMaskType(...
        'dsp.simulink.VariableBandwidthFIRFilter',...
        'FilterType','Bandpass',...
        'BandwidthPort','on');

        blks_variablebandwidthfirbandwidthbandstop=obj.findBlocksWithMaskType(...
        'dsp.simulink.VariableBandwidthFIRFilter',...
        'FilterType','Bandstop',...
        'BandwidthPort','on');

        blks_variablebandwidthfircenterfrequencybandpass=obj.findBlocksWithMaskType(...
        'dsp.simulink.VariableBandwidthFIRFilter',...
        'FilterType','Bandpass',...
        'CenterFrequencyPort','on');

        blks_variablebandwidthfircenterfrequencybandstop=obj.findBlocksWithMaskType(...
        'dsp.simulink.VariableBandwidthFIRFilter',...
        'FilterType','Bandstop',...
        'CenterFrequencyPort','on');

        blks_variablebandwidthfircutofffrequencylowpass=obj.findBlocksWithMaskType(...
        'dsp.simulink.VariableBandwidthFIRFilter',...
        'FilterType','Lowpass',...
        'CutoffFrequencyPort','on');

        blks_variablebandwidthfircutofffrequencyhighpass=obj.findBlocksWithMaskType(...
        'dsp.simulink.VariableBandwidthFIRFilter',...
        'FilterType','Highpass',...
        'CutoffFrequencyPort','on');



        blks_variablebandwidthiirbandwidthbandpass=obj.findBlocksWithMaskType(...
        'dsp.simulink.VariableBandwidthIIRFilter',...
        'FilterType','Bandpass',...
        'BandwidthPort','on');

        blks_variablebandwidthiirbandwidthbandstop=obj.findBlocksWithMaskType(...
        'dsp.simulink.VariableBandwidthIIRFilter',...
        'FilterType','Bandstop',...
        'BandwidthPort','on');

        blks_variablebandwidthiircenterfrequencybandpass=obj.findBlocksWithMaskType(...
        'dsp.simulink.VariableBandwidthIIRFilter',...
        'FilterType','Bandpass',...
        'CenterFrequencyPort','on');

        blks_variablebandwidthiircenterfrequencybandstop=obj.findBlocksWithMaskType(...
        'dsp.simulink.VariableBandwidthIIRFilter',...
        'FilterType','Bandstop',...
        'CenterFrequencyPort','on');

        blks_variablebandwidthiirpassbandfrequencylowpass=obj.findBlocksWithMaskType(...
        'dsp.simulink.VariableBandwidthIIRFilter',...
        'FilterType','Lowpass',...
        'PassbandFrequencyPort','on');

        blks_variablebandwidthiirpassbandfrequencyhighpass=obj.findBlocksWithMaskType(...
        'dsp.simulink.VariableBandwidthIIRFilter',...
        'FilterType','Highpass',...
        'PassbandFrequencyPort','on');
        blks=unique([blks_ma,blks_mv,blks_mrms,blks_mstd,blks_hampel,blks_chann,blks_synth,...
        blks_iirhbdecim,blks_iirhbinterp,blks_allpass,blks_allpasslattice,blks_FreqDomainFIRFilter,...
        blks_parametriceqbandwidth,blks_parametriceqcenterfrequency1,blks_parametriceqpeakgaindb,...
        blks_parametriceqcenterfrequency2,blks_parametriceqqualityfactor,blks_parametriceqbandwidthcoef,...
        blks_parametriceqcenterfrequencycoef,blks_parametriceqpeakgaincoef,blks_notchpeakbandwidth,...
        blks_notchpeakcenterfrequency1,blks_notchpeakcenterfrequency2,blks_notchpeakqualityfactor,...
        blks_notchpeakbandwidthcoef,blks_notchpeakcenterfrequencycoef,blks_variablebandwidthfirbandwidthbandpass,...
        blks_variablebandwidthfirbandwidthbandstop,blks_variablebandwidthfircenterfrequencybandpass,...
        blks_variablebandwidthfircenterfrequencybandstop,blks_variablebandwidthfircutofffrequencylowpass,...
        blks_variablebandwidthfircutofffrequencyhighpass,blks_variablebandwidthiirbandwidthbandpass,...
        blks_variablebandwidthiirbandwidthbandstop,blks_variablebandwidthiircenterfrequencybandpass,...
        blks_variablebandwidthiircenterfrequencybandstop,blks_variablebandwidthiirpassbandfrequencylowpass,...
        blks_variablebandwidthiirpassbandfrequencyhighpass]);

        for idx=1:numel(blks)
            this_blk=blks{idx};
            subsys_msg=DAStudio.message('dsp:system:Shared:EmptySubsystem_TunableParamFromPort');
            subsys_err=DAStudio.message('dsp:system:Shared:NewFeaturesNotAvailable');
            obj.replaceWithEmptySubsystem(this_blk,subsys_msg,subsys_err);
        end
    end