function[mapName,relativePathToMapFile,found]=getBlockHelpMapNameAndPath(block_type)





    blks={...
    'HeadingIndicatorBlock','headingindicator';...
    'AltimeterBlock','altimeter';...
    'ClimbIndicatorBlock','climbindicator';...
    'EGTIndicatorBlock','egtindicator';...
    'RPMIndicatorBlock','rpmindicator';...
    'AirspeedIndicatorBlock','airspeedindicator';...
    'ArtificialHorizonBlock','artificialhorizon';...
    'TurnCoordinatorBlock','turncoordinator';...
    };
    relativePathToMapFile='/aeroblks/aeroblks.map';
    found=false;


    idx=strcmp(block_type,blks(:,1));

    if~any(idx)
        mapName='User Defined';
    else
        found=true;
        mapName=blks(idx,2);
    end
