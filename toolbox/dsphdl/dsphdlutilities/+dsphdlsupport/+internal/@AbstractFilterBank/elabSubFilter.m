function subFilter=elabSubFilter(this,topNet,dataRate,blockInfo,...
    din,coef,dinVld,syncReset,dout,doutVld,...
    FOLDINGFACTOR,...
    DATA_WORDLENGTH,DATA_FRACTIONLENGTH,...
    COEF_WORDLENGTH,COEF_FRACTIONLENGTH,...
    FULLPRECISION,...
    coeffProtoType,zeroOpt)








    if strcmpi(blockInfo.FilterStructure,'Direct form transposed')


        subFilter=elabSubFilterT(this,topNet,dataRate,blockInfo,...
        din,coef,dinVld,syncReset,dout,doutVld,...
        FOLDINGFACTOR,...
        DATA_WORDLENGTH,DATA_FRACTIONLENGTH,...
        COEF_WORDLENGTH,COEF_FRACTIONLENGTH,...
        FULLPRECISION,...
        coeffProtoType,zeroOpt);
    else


        subFilter=elabSubFilterS(this,topNet,dataRate,blockInfo,...
        din,coef,dinVld,syncReset,dout,doutVld,...
        FOLDINGFACTOR,...
        DATA_WORDLENGTH,DATA_FRACTIONLENGTH,...
        COEF_WORDLENGTH,COEF_FRACTIONLENGTH,...
        FULLPRECISION,...
        coeffProtoType,zeroOpt);
    end
end

