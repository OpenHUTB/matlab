function res=isUserDefinedFcnBlock(ownerHandle)
    res=false;
    if(ishandle(ownerHandle))
        ownerUDD=get_param(ownerHandle,'Object');

        if(isa(ownerUDD,'Simulink.SFunction')||...
            isa(ownerUDD,'Simulink.MSFunction')||...
            isa(ownerUDD,'Simulink.MATLABSystem')||...
            isa(ownerUDD,'Simulink.FMU'))||...
            (slfeature('CustomCodeIntegrationHarness')&&isa(ownerUDD,'Simulink.CCaller'))||...
            (slfeature('CFunctionBlockHarness')&&isa(ownerUDD,'Simulink.CFunction'))||...
            (slfeature('SimscapeBlockHarness')&&isa(ownerUDD,'Simulink.SimscapeBlock'))
            res=true;
        end
    end
end
