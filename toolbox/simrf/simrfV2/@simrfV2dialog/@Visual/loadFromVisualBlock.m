function loadFromVisualBlock(this)







    this.SourceFreq=this.Block.SourceFreq;
    this.PlotFreq=this.Block.PlotFreq;
    this.PlotFreq_unit=this.Block.PlotFreq_unit;
    this.PlotType=this.Block.PlotType;
    this.YParam1=get_param(this.Block.Handle,'YParam1');


    this.YParam2=get_param(this.Block.Handle,'YParam2');

    this.YFormat1=this.Block.YFormat1;
    this.YFormat2=this.Block.YFormat2;
    this.YOption=this.Block.YOption;
    this.XOption=this.Block.XOption;

