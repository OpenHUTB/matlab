function sl_postprocess(h)







    blocks_RF=find_system(h,'regexp','on',...
    'MatchFilter',@Simulink.match.internal.filterOutInactiveVariantSubsystemChoices,...
    'BlockType','SimscapeBlock','Mask','on','Name','.*_RF');
    arrayfun(@(i)set_param(i,'MaskHelp',''),blocks_RF,...
    'uniformoutput',false);


    hBlk='simrfV2_lib/Elements/Electrical Reference';
    set_param(hBlk,'DialogController','')




    blocks=find_system(h,'regexp','on',...
    'MatchFilter',@Simulink.match.internal.filterOutInactiveVariantSubsystemChoices,...
    'BlockType','SimscapeBlock','Mask','on','MaskType','.');
    idx_RF=arrayfun(@(i)any(i==blocks_RF),blocks);
    blocks=blocks(~idx_RF);

    arrayfun(@(i)set_param(i,'MaskHelp','eval(''simrfV2_help(gcbh)'')'),...
    blocks,'uniformoutput',false);








    fwdTable={...
    {'simrfV2_lib/Elements/INDUCTOR',...
    'simrfV2elements/L','simrfV2_ce_forward'},...
    {'simrfV2_lib/Elements/CAPACITOR',...
    'simrfV2elements/C','simrfV2_ce_forward'},...
    {'simrfV2_lib/Elements/Ideal Transformer',...
    'simrfV2elements/Ideal Transformer','simrfV2_ce_forward'},...
    {'simrfV2_lib/Elements/Mutual Inductor',...
    'simrfV2elements/Mutual Inductor','simrfV2_ce_forward'},...
    {'simrfV2_lib/Elements/Three-Winding Transformer',...
    'simrfV2elements/Three-Winding Transformer','simrfV2_ce_forward'}};
    set_param(h,'ForwardingTable',fwdTable);

end
