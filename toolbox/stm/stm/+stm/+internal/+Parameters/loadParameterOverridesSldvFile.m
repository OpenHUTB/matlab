function paramList=loadParameterOverridesSldvFile(file,index)
    paramList=[];
    [~,scenario]=stm.internal.MRT.share.loadSldvFile(file,index,false);
    if isfield(scenario,'paramValues')&&~isempty(scenario.paramValues)
        paramNames={scenario.paramValues.name};
        paramList=struct(...
        'Name',paramNames,...
        'ClassName',class({scenario.paramValues.value}),...
        'CanShow',false,...
        'DerivedDisplayValue',''...
        );

        for idx=1:length(paramNames)
            [paramList(idx).CanShow,paramList(idx).DerivedDisplayValue]=...
            stm.internal.util.getDisplayValue(scenario.paramValues(idx).value);
        end
    end
end