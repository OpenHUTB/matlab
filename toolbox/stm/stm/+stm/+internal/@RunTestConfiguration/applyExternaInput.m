


function matlabWarnings=applyExternaInput(obj,simWatcher,inputDataSetsRunFile,inputSignalGroupRunFile)

    lastwarn('');

    [simWatcher.cleanupIteration.LoadExternalInput,...
    simWatcher.cleanupIteration.ExternalInput,...
    simWatcher.cleanupIteration.SignalBuilder,...
    simWatcher.cleanupIteration.SigBuilderIndex,...
    simWatcher.cleanupIteration.VarsLoaded,...
    simWatcher.cleanupIteration.StopTime,...
    sldvParams,...
    warnMessage,...
    logOrError,obj.out.ExternalInputRunData,obj.out.SigBuilderInfo]=...
    obj.modelUtil.loadInputs(obj.testSettings.input,inputDataSetsRunFile,inputSignalGroupRunFile);


    matlabWarnings={};
    tmp=lastwarn;
    if(~isempty(tmp))
        matlabWarnings={tmp};
    end

    obj.addMessages(warnMessage,logOrError);
end
