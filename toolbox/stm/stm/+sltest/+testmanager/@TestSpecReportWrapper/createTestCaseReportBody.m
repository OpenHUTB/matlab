function createTestCaseReportBody(reportObj, testObj, section)

    import mlreportgen.report.*;
    import mlreportgen.dom.*;
    import sltest.testmanager.*;

    if(isequal(testObj.TestType,'baseline'))
        if(testObj.RunOnTarget{1})
            testCaseRptr = TestCaseReporter('Object',testObj,...
                                            'ReportTempDir', reportObj.workingPath,...
                                            'IncludeConfigSettingsOverrides',false,...
                                            'IncludeEquivalenceCriteria', false,...
                                            'IncludeTestDetails', reportObj.IncludeTestDetails,...
                                            'IncludeCoverageSettings', reportObj.IncludeCoverageSettings,...
                                            'IncludeSystemUnderTest', reportObj.IncludeSystemUnderTest,...
                                            'IncludeCallbacks', reportObj.IncludeCallbackScripts,...
                                            'IncludeParameterOverrides', reportObj.IncludeParameterOverrides,...
                                            'IncludeExternalInputs', reportObj.IncludeExternalInputs,...
                                            'IncludeSimulationOutputs', reportObj.IncludeLoggedSignals,...
                                            'IncludeBaselineCriteria', reportObj.IncludeBaselineCriteria,...
                                            'IncludeIterations', reportObj.IncludeIterations,...
                                            'IncludeCustomCriteria', reportObj.IncludeCustomCriteria,...
                                            'IncludeLogicalAndTemporalAssessments', reportObj.IncludeLogicalAndTemporalAssessments);
                                            
        else
            testCaseRptr = TestCaseReporter('Object',testObj,...
                                            'ReportTempDir', reportObj.workingPath,...
                                            'IncludeEquivalenceCriteria', false,...
                                            'IncludeConfigSettingsOverrides',reportObj.IncludeConfigSettingsOverrides,...
                                            'IncludeTestDetails', reportObj.IncludeTestDetails,...
                                            'IncludeCoverageSettings', reportObj.IncludeCoverageSettings,...
                                            'IncludeSystemUnderTest', reportObj.IncludeSystemUnderTest,...
                                            'IncludeCallbacks', reportObj.IncludeCallbackScripts,...
                                            'IncludeParameterOverrides', reportObj.IncludeParameterOverrides,...
                                            'IncludeExternalInputs', reportObj.IncludeExternalInputs,...
                                            'IncludeSimulationOutputs', reportObj.IncludeLoggedSignals,...
                                            'IncludeBaselineCriteria', reportObj.IncludeBaselineCriteria,...
                                            'IncludeIterations', reportObj.IncludeIterations,...
                                            'IncludeCustomCriteria', reportObj.IncludeCustomCriteria,...
                                            'IncludeLogicalAndTemporalAssessments', reportObj.IncludeLogicalAndTemporalAssessments);
        end
    elseif(isequal(testObj.TestType,'equivalence'))
        if(testObj.RunOnTarget{1} || testObj.RunOnTarget{2})
            testCaseRptr = TestCaseReporter('Object',testObj,...
                                            'ReportTempDir', reportObj.workingPath,...
                                            'IncludeConfigSettingsOverrides',false,...
                                            'IncludeBaselineCriteria',false,...
                                            'IncludeTestDetails', reportObj.IncludeTestDetails,...
                                            'IncludeCoverageSettings', reportObj.IncludeCoverageSettings,...
                                            'IncludeSystemUnderTest', reportObj.IncludeSystemUnderTest,...
                                            'IncludeCallbacks', reportObj.IncludeCallbackScripts,...
                                            'IncludeParameterOverrides', reportObj.IncludeParameterOverrides,...
                                            'IncludeExternalInputs', reportObj.IncludeExternalInputs,...
                                            'IncludeEquivalenceCriteria', reportObj.IncludeEquivalenceCriteria,...
                                            'IncludeSimulationOutputs', reportObj.IncludeLoggedSignals,...
                                            'IncludeIterations', reportObj.IncludeIterations,...
                                            'IncludeCustomCriteria', reportObj.IncludeCustomCriteria,...
                                            'IncludeLogicalAndTemporalAssessments', reportObj.IncludeLogicalAndTemporalAssessments);
        else
            testCaseRptr = TestCaseReporter('Object',testObj,...
                                            'ReportTempDir', reportObj.workingPath,...
                                            'IncludeBaselineCriteria',false,...
                                            'IncludeTestDetails', reportObj.IncludeTestDetails,...
                                            'IncludeCoverageSettings', reportObj.IncludeCoverageSettings,...
                                            'IncludeSystemUnderTest', reportObj.IncludeSystemUnderTest,...
                                            'IncludeCallbacks', reportObj.IncludeCallbackScripts,...
                                            'IncludeParameterOverrides', reportObj.IncludeParameterOverrides,...
                                            'IncludeConfigSettingsOverrides', reportObj.IncludeConfigSettingsOverrides,...
                                            'IncludeExternalInputs', reportObj.IncludeExternalInputs,...
                                            'IncludeEquivalenceCriteria', reportObj.IncludeEquivalenceCriteria,...
                                            'IncludeSimulationOutputs', reportObj.IncludeLoggedSignals,...
                                            'IncludeIterations', reportObj.IncludeIterations,...
                                            'IncludeCustomCriteria', reportObj.IncludeCustomCriteria,...
                                            'IncludeLogicalAndTemporalAssessments', reportObj.IncludeLogicalAndTemporalAssessments);
        end

    elseif(isequal(testObj.TestType,'simulation'))
        if(testObj.RunOnTarget{1})
            testCaseRptr = TestCaseReporter('Object',testObj,...
                                            'ReportTempDir', reportObj.workingPath,...
                                            'IncludeConfigSettingsOverrides',false,...
                                            'IncludeBaselineCriteria',false,...
                                            'IncludeEquivalenceCriteria',false,...
                                            'IncludeTestDetails', reportObj.IncludeTestDetails,...
                                            'IncludeCoverageSettings', reportObj.IncludeCoverageSettings,...
                                            'IncludeSystemUnderTest', reportObj.IncludeSystemUnderTest,...
                                            'IncludeCallbacks', reportObj.IncludeCallbackScripts,...
                                            'IncludeParameterOverrides', reportObj.IncludeParameterOverrides,...
                                            'IncludeExternalInputs', reportObj.IncludeExternalInputs,...
                                            'IncludeSimulationOutputs', reportObj.IncludeLoggedSignals,...
                                            'IncludeIterations', reportObj.IncludeIterations,...
                                            'IncludeCustomCriteria', reportObj.IncludeCustomCriteria,...
                                            'IncludeLogicalAndTemporalAssessments', reportObj.IncludeLogicalAndTemporalAssessments);
        else
            testCaseRptr = TestCaseReporter('Object',testObj,...
                                            'ReportTempDir', reportObj.workingPath,...
                                            'IncludeBaselineCriteria',false,...
                                            'IncludeEquivalenceCriteria',false,...
                                            'IncludeTestDetails', reportObj.IncludeTestDetails,...
                                            'IncludeCoverageSettings', reportObj.IncludeCoverageSettings,...
                                            'IncludeSystemUnderTest', reportObj.IncludeSystemUnderTest,...
                                            'IncludeCallbacks', reportObj.IncludeCallbackScripts,...
                                            'IncludeParameterOverrides', reportObj.IncludeParameterOverrides,...
                                            'IncludeConfigSettingsOverrides', reportObj.IncludeConfigSettingsOverrides,...
                                            'IncludeExternalInputs', reportObj.IncludeExternalInputs,...
                                            'IncludeSimulationOutputs', reportObj.IncludeLoggedSignals,...
                                            'IncludeIterations', reportObj.IncludeIterations,...
                                            'IncludeCustomCriteria', reportObj.IncludeCustomCriteria,...
                                            'IncludeLogicalAndTemporalAssessments', reportObj.IncludeLogicalAndTemporalAssessments);
                                            
        end
    end
    
    if(~isempty(reportObj.TestCaseReporterTemplate))
        testCaseRptr.TemplateSrc = reportObj.TestCaseReporterTemplate;
    end

    add(section, testCaseRptr);

end

