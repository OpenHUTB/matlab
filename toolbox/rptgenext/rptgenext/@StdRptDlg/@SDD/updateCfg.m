function updateCfg(dlgsrc)







    cfg=dlgsrc.updateBaseCfg();

    cfg.includeDetails=dlgsrc.includeDetails;
    cfg.includeRequirementsLinks=dlgsrc.includeRequirementsLinks;
    cfg.includeCustomLibraries=dlgsrc.includeCustomLibraries;
    cfg.includeGlossary=dlgsrc.includeGlossary;
    cfg.includeModelRefs=dlgsrc.includeModelRefs;

end










