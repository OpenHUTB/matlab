classdef BDLocation<slxmlcomp.internal.highlight.Location




    properties(GetAccess=public,SetAccess=private)
Type
        Location(1,1)string
        BDInfo(1,:)slxmlcomp.internal.highlight.window.BDInfo
    end

    methods(Access=public)
        function obj=BDLocation(...
            type,...
            location,...
bdInfo...
            )
            obj.Type=type;
            obj.Location=location;
            obj.BDInfo=bdInfo;
        end
    end

    methods(Access=public,Static)
        function location=fromJHighlightData(jHighlightData)

            import slxmlcomp.internal.highlight.window.BDLocation;
            import slxmlcomp.internal.highlight.window.BDInfo;

            location=BDLocation(...
            string(jHighlightData.getHighlightType()),...
            string(jHighlightData.getNodeHighlightPath()),...
            BDInfo.fromJHighlightData(jHighlightData)...
            );
        end

        function location=from(...
            type,...
            locationPath,...
            isTestHarness,...
            modelName,...
            harnessName,...
            modelFile,...
allowRename...
            )

            import slxmlcomp.internal.highlight.window.BDLocation;
            import slxmlcomp.internal.highlight.window.BDInfo;

            bdInfo=BDInfo(...
            isTestHarness,...
            modelName,...
            harnessName,...
            modelFile,...
allowRename...
            );

            location=BDLocation(...
            type,...
            locationPath,...
bdInfo...
            );
        end

    end
end
