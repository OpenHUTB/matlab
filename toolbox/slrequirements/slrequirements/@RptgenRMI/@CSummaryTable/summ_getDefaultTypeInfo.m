function i=summ_getDefaultTypeInfo(typeID)






    switch typeID
    case 'System'
        i=rptgen.summsrc(typeID,...
        rptgen_sl.propsrc_sl_sys,...
        RptgenRMI.CSystemLoop,...
        {'RmiLinkedName','RequirementInfo'},...
        false);
    case 'Block'
        i=rptgen_sl.summsrc_sl_blk(typeID,...
        rptgen_sl.propsrc_sl_blk,...
        RptgenRMI.CBlockLoop,...
        {'RmiLinkedName','RequirementInfo'},...
        true);
    case 'Stateflow'
        i=rptgen.summsrc(typeID,...
        rptgen_sf.propsrc_sf,...
        rptgen_sf.csf_hier_loop('isSFFilterList',true,...
        'SFFilterTerms',{'-function','eval(''@rmisf.has_req'')'}),...
        {'RmiLinkedName','RequirementInfo'},...
        true);
    otherwise
        error(message('Slvnv:reqmgt:rptgen:InvalidSummsrc',typeID));
    end

