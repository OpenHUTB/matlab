















































































































































































































































classdef(Sealed)Client<oslc.internal.BaseClient


    methods(Access=public)

        function this=Client()
            this=this@oslc.internal.BaseClient;





            this.passmanager=@oslc.passcode;
            this.passwrapper=@oslc.Client.unconfusePasscode;





            this.setServiceRoot(rmipref('OslcServerRMRoot'));
            this.setCatalogPath('/oslc_rm/catalog');


        end
    end

    methods(Static,Access=private)
        function out=unconfusePasscode(in,key)



            cl=clock();
            day=cl(3);
            out=in;
            for i=1:length(in)
                j=mod(i,length(key))+1;
                diff=int32(key(j))-day;
                out(i)=char(int32(in(i))-diff);
            end
        end
    end
end


