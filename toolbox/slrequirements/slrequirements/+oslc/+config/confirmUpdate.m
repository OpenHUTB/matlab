function matched=confirmUpdate(projName,newConfig)

    matched=false;

    proj=oslc.Project.get(projName);
    currentContext=proj.getContext();

    userClick=questdlg({...
    getString(message('Slvnv:oslc:ConfigContextInBrowser',newConfig.name)),...
    getString(message('Slvnv:oslc:ConfigContextInSimulink',currentContext.name)),...
    getString(message('Slvnv:oslc:ConfigContextUpdateSimulinkQ')),...
    '',...
    getString(message('Slvnv:oslc:ConfigContextUpdateAlways')),...
    ''},...
    getString(message('Slvnv:oslc:ConfigContextUpdateDlgTitle')),...
    getString(message('Slvnv:oslc:ConfigContextDlgButtonUpdate')),...
    getString(message('Slvnv:oslc:ConfigContextDlgButtonAlwaysUpdate')),...
    getString(message('Slvnv:oslc:Cancel')),...
    getString(message('Slvnv:oslc:ConfigContextDlgButtonUpdate')));
    if isempty(userClick)
        return;
    else
        switch userClick
        case getString(message('Slvnv:oslc:ConfigContextDlgButtonUpdate'))
            proj.setContext(newConfig.url,newConfig.name);
            matched=true;
        case getString(message('Slvnv:oslc:ConfigContextDlgButtonAlwaysUpdate'))
            proj.setContext(newConfig.url,newConfig.name);
            rmipref('OslcMatchBrowserContext',true);
            matched=true;
        otherwise

        end
    end
end
