function isTLCKey=isKeywordInTLC(name)




    name=convertStringsToChars(name);

    tlcKeywords={'COMPLEX','COMPLEX32','COMPLEX16','EXISTS','Inf','InfF','InfFi',...
    'Infi','MATRIX','Matrix','NaN','NaNF','NaNFi','NaNi',...
    'REAL','REAL32','REAL16','RTW_AN','RTW_IN','RTW_INF','RTW_MINUS_INF',...
    'RTW_NA','RTW_NAN','RTW_NF','RTW_NONE','SLDATA','SLDATAMODELNAME',...
    'SLData','SLDataModelName','TP_EXISTS','VECTOR','Vector','inf',...
    'infF','infFi','infi','nan','nanF','nanFi','ynani','rtInf',...
    'rtInfF','rtInfFi','rtInfi','rtMinusInf','rtMinusInfF',...
    'rtMinusInfFi','rtMinusInfi','rtNaN','rtNaNF','rtNaNFi',...
    'rtNaNi'};

    isTLCKey=ismember(name,tlcKeywords);

