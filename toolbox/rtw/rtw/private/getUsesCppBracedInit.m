function val=getUsesCppBracedInit(model)

    val=slfeature('RTWCGCppBracedInit')&&...
    strcmp(get_param(model,'TargetLang'),'C++')&&...
    (strcmp(get_param(model,'TargetLangStandard'),'C++11 (ISO)')||...
    (strcmp(get_param(model,'AutosarCompliant'),'on')&&...
    strcmp(get_param(model,'CodeInterfacePackaging'),'C++ class')));
end
