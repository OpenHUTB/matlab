function out=getSummary(obj)



    if~Simulink.report.ReportInfo.featureReportV2||isempty(obj.ReportFolder)
        out={DAStudio.message('RTW:report:ModelVersion'),...
        obj.ModelVersion;...
        DAStudio.message('RTW:report:SimulinkCoderVersion'),...
        obj.CoderVersion;...
        DAStudio.message('RTW:report:SourceCodeGeneratedOn'),...
        obj.TimeStamp;...
        ['<span id="sourceLocationTitle">',DAStudio.message('RTW:report:SourceCodeGeneratedAt'),'</span>'],...
        '<span id="sourceLocation" style="display:none"><script>document.write(top.getCodeLocation())</script></span>';...
        };
    else


        rptFolder=obj.ReportFolder;
        pathList=split(rptFolder,filesep);
        codeLoc=fullfile(pathList{1:end-2});
        out={DAStudio.message('RTW:report:ModelVersion'),...
        obj.ModelVersion;...
        DAStudio.message('RTW:report:SimulinkCoderVersion'),...
        obj.CoderVersion;...
        DAStudio.message('RTW:report:SourceCodeGeneratedOn'),...
        obj.TimeStamp;...
        ['<span id="sourceLocationTitle">',DAStudio.message('RTW:report:SourceCodeGeneratedAt'),'</span>'],...
        sprintf('<span id="sourceLocation">%s</span>',codeLoc);...
        };
    end
end
