function pilStoreTargetInfo(h,modelname,ccobj,pjtname)






    pilConfigFile=[modelname,'_pilinfo.dat'];

    fid=fopen(pilConfigFile,'w');

    if fid==-1
        error(message('TICCSEXT:util:FileOpenError',pilConfigFile));
    end

    if h.isExportIDEObj,
        isObjExported=1;
        objName=h.getExportIDEObjName;
    else
        isObjExported=0;
        objName='';
    end

    fprintf(fid,...
    ['boardname=''%s'';\n',...
    'procname=''%s'';\n',...
    'appname=''%s'';\n',...
    'isObjExported=%d;\n',...
    'objName=''%s'';\n',...
    'objTimeout=%d;\n',...
    'buildTimeout=%d;\n',...
    ],...
    ccobj.targetinfo.boardname,...
    ccobj.targetinfo.procname,...
    [pjtname,'.out'],...
    isObjExported,...
    objName,...
    ccobj.timeout,...
    ccobj.get('buildtimeout')...
    );

    fclose(fid);

