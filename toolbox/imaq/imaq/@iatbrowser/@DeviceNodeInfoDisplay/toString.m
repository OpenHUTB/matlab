function displayString=toString(this)

    deviceNode=this.node;

    stringTemplate=...
    ['<html>'...
    ,'<h3>%s</h3>'...
    ,'<table>'...
    ,'<tr><td align="left">Device:</td><td>%s</td></tr>'...
    ,'<tr><td align="left">Adaptor:</td><td>%s</td></tr>'...
    ,'<tr><td align="left">Device ID:</td><td>%d</td></tr>'...
    ,'<tr><td align="left">%s</td><td></td></tr>'...
    ,'</table>'...
    ,'</html>'];

    camFileString='';
    if deviceNode.CameraFileSupport==1
        camFileString='This device supports camera files.';
    end

    displayString=sprintf(stringTemplate,deviceNode.DisplayName,...
    deviceNode.DeviceName,...
    deviceNode.Adaptor,...
    deviceNode.DeviceID,...
    camFileString);

end