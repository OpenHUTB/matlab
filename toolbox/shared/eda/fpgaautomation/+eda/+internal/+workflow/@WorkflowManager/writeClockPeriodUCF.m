function ucfFile=writeClockPeriodUCF(h)




    hdlcData=h.mWorkflowInfo.hdlcData;
    userParam=h.mWorkflowInfo.userParam;
    tdkParam=h.mWorkflowInfo.tdkParam;


    moduleName=[hdlcData.dutName,tdkParam.clkWrapperName];
    ucfFile=[moduleName,'.ucf'];


    fname=fullfile(hdlcData.codegenDir,ucfFile);
    fid=fopen(fname,'w');


    msg=['Writing constraint file ',hdlgetfilelink(fname)];
    hdldisp(msg);
    disp(' ');


    mdlname=hdlcData.modelName;
    fnamestr=strrep(fname,'\','\\');
    createdate=datestr(now,31);
    genby=getGenByText;


    if isempty(mdlname)
        mdltxt='';
    else
        mdltxt=['#',blanks(5),'Model: ',mdlname,'.mdl\n'];
    end

    headertxt=...
    [repmat('#',1,60),'\n',...
    '#',blanks(1),'UCF Constraint File \n',...
    '#',blanks(5),'Module: ',moduleName,'\n',...
    mdltxt,...
    '#','\n',...
    '#',blanks(5),'File Name: ',fnamestr,'\n',...
    '#',blanks(5),'Created: ',createdate,'\n',...
    '#',blanks(5),genby,'\n',...
    '#','\n',...
    repmat('#',1,60),'\n\n',...
    repmat('#',1,60),'\n',...
    '#',blanks(1),'Clock period constraint\n',...
    repmat('#',1,60),'\n'];

    shead=sprintf(headertxt);
    fprintf(fid,'%s',shead);



    clkperiod=userParam.clkinPeriod;
    clkname=hdlcData.clockname;
    tnm_name=['"TN_',clkname,'"'];
    timespec_name=['"TS_',clkname,'"'];

    clkucf=['NET "',clkname,'" TNM_NET = ',tnm_name,';',char(10)...
    ,'TIMESPEC ',timespec_name,' = PERIOD ',tnm_name,' '...
    ,clkperiod,' ns HIGH 50%;',char(10),char(10)];

    fprintf(fid,'%s',clkucf);
    fclose(fid);


    function str=getGenByText(~)

        mver=ver('matlab');
        hver=ver('hdlverifier');
        str=['Generated by ',mver.Name,' ',mver.Version,' and '...
        ,hver.Name,' ',hver.Version];
