function str=hdlCopyRightHeader(srcFile)

    str='';

    tmp=date;
    yearStr=tmp(end-3:end);
    [~,fileName,ext]=fileparts(srcFile);
    if~isempty(ext)
        if strcmpi(ext,'.vhd')
            str=[...
            '\n-- ----------------------------------------------\n',...
            '-- File Name: ',fileName,'.vhd\n',...
            '-- Created:   ',datestr(now,'local'),'\n',...
            '-- Copyright  ',yearStr,' MathWorks, Inc.\n',...
            '-- ----------------------------------------------\n\n'];
        elseif strcmpi(ext,'.v')
            str=[...
            '\n/*-- ----------------------------------------------\n',...
            '-- File Name: ',fileName,'.v\n',...
            '-- Created:   ',datestr(now,'local'),'\n',...
            '-- Copyright  ',yearStr,' MathWorks, Inc.\n',...
            '-- ----------------------------------------------*/\n\n'];
        elseif strcmpi(ext,'.ucf')
            str=[...
            '\n# -----------------------------------------------\n',...
            '# File Name: ',fileName,'.ucf\n',...
            '# Created:   ',datestr(now,'local'),'\n',...
            '# Copyright  ',yearStr,' MathWorks, Inc.\n',...
            '# -------------------------------------------------\n\n'];
        end

    elseif hdlgetparameter('isvhdl')
        str=[...
        '\n-- ----------------------------------------------\n',...
        '-- File Name: ',fileName,'.vhd\n',...
        '-- Created:   ',datestr(now,'local'),'\n',...
        '-- Copyright  ',yearStr,' MathWorks, Inc.\n',...
        '-- ----------------------------------------------\n\n'];
    else
        str=[...
        '\n/*-- ----------------------------------------------\n',...
        '-- File Name: ',fileName,'.v\n',...
        '-- Created:   ',datestr(now,'local'),'\n',...
        '-- Copyright  ',yearStr,' MathWorks, Inc.\n',...
        '-- ----------------------------------------------*/\n\n'];
    end


end

