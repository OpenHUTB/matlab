function saveBuildInfo(obj,matFilePath)





    if~isa(obj,'eda.internal.workflow.FILBuildInfo')
        error(message('EDALink:saveBuildInfo:InvalidInput'));
    end


    filStruct=struct;
    prop={'Board',...
    'BoardObj',...
    'FPGASystemClockFrequency',...
    'IPAddress',...
    'MACAddress',...
    'DUTName',...
    'ResetAssertedLevel',...
    'ClockEnableAssertedLevel',...
    'AutoPortInfo',...
    'FILVersion',...
    'BuildInfoVersion',...
    'SourceFiles',...
    'DUTPorts',...
    'TopLevelIndex',...
    'FPGAVendor',...
    'Tool',...
    'OutputDataTypes'};

    for n=1:numel(prop)
        filStruct.(prop{n})=obj.(prop{n});
    end

    if~obj.isDefaultOutputFolder
        filStruct.OutputFolder=obj.OutputFolder;
    end




    filStruct.BoardClass=class(obj.BoardObj);

    save(matFilePath,'filStruct');
