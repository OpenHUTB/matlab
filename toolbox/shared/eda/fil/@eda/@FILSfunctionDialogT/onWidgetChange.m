function onWidgetChange(this,dialog,tag,value)

    switch(tag)
    case 'procCombo'
        enumVal=eda.internal.filhost.ProcessingModeT(value);
        switch(enumVal)
        case eda.internal.filhost.ProcessingModeT.ProcessAsSamples;
            this.params.processingMode=enumVal;
            dialog.setEnabled('inFSCombo',false);
            dialog.setEnabled('outFSCombo',false);
        case eda.internal.filhost.ProcessingModeT.ProcessAsFrames;
            this.params.processingMode=enumVal;
            dialog.setEnabled('inFSCombo',true);
            dialog.setEnabled('outFSCombo',true);
        end
    end

