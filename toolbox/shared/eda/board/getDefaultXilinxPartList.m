    family=getFamily;
    family_v=getVendorFamily;
    device={};

    if nargin>0
        idx1=find(strcmpi(varargin{1},family));
        idx1_v=find(strcmpi(varargin{1},family_v));
        if~isempty(idx1)
            device=getDevice(idx1);
        end
    end

    if nargin==0
        partList=family;
    end

    if nargin==1
        partList=device;
    end

    if nargin==2
        if strcmpi(varargin{2},'vendorName')
            partList='';
            if~isempty(idx1)
                partList=family_v{idx1};
            end
        end
        if strcmpi(varargin{2},'customerName')
            partList='';
            if~isempty(idx1_v)
                partList=family{idx1_v};
            end
        end
    end

    if nargin==3
        idx2=find(strcmpi(varargin{2},device));
        if~isempty(idx2)
            if strcmpi(varargin{3},'speed')
                partList=getSpeed(idx1,idx2);
            end
            if strcmpi(varargin{3},'package')
                partList=getPackage(idx1,idx2);
            end
        end
    end

