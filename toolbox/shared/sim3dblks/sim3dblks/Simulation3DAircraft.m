            self.AircraftColor=s.AircraftColor;
            self.ActorTag=s.ActorTag;
            self.LightConfiguration=s.LightConfiguration;
            self.ModelName=s.ModelName;

            loadObjectImpl@Simulation3DActor(self,s,wasInUse);
        end

