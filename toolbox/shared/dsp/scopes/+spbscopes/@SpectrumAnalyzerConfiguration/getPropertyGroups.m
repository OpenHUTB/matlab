function groups=getPropertyGroups(obj)





    mainProps={...
    'NumInputPorts',...
    'InputDomain',...
    'SpectrumType',...
    'ViewType',...
    'SampleRateSource',...
    'SampleRate',...
    'Method',...
    'PlotAsTwoSidedSpectrum',...
    'FrequencyScale'};

    advancedProps={...
    'FrequencySpan',...
    'Span',...
    'CenterFrequency',...
    'StartFrequency',...
    'StopFrequency',...
    'FrequencyResolutionMethod',...
    'RBWSource',...
    'RBW',...
    'WindowLength',...
    'FFTLengthSource',...
    'FFTLength',...
    'NumTapsPerBand',...
    'FrequencyVectorSource',...
    'FrequencyVector',...
    'InputUnits',...
    'OverlapPercent',...
    'Window',...
    'CustomWindow',...
    'SidelobeAttenuation',...
    'SpectrumUnits',...
    'FullScaleSource',...
    'FullScale',...
    'AveragingMethod',...
    'SpectralAverages',...
    'ForgettingFactor',...
    'ReferenceLoad',...
    'FrequencyOffset',...
    'TreatMby1SignalsAsOneChannel'};

    spectrogramProps={...
    'SpectrogramChannel',...
    'TimeResolutionSource',...
    'TimeResolution',...
    'TimeSpanSource',...
    'TimeSpan'};

    measurementsProps={...
    'MeasurementChannel',...
    'SpectralMask',...
    'PeakFinder',...
    'CursorMeasurements',...
    'ChannelMeasurements',...
    'DistortionMeasurements',...
    'CCDFMeasurements'};

    visualizationProps={...
    'Name',...
    'Position',...
    'PlotType',...
    'PlotNormalTrace',...
    'PlotMaxHoldTrace',...
    'PlotMinHoldTrace',...
    'ReducePlotRate',...
    'Title',...
    'YLabel',...
    'ShowLegend',...
    'ChannelNames',...
    'ShowGrid',...
    'YLimits',...
    'ColorLimits',...
    'AxesScaling',...
    'AxesScalingNumUpdates',...
    'AxesLayout',...
    'OpenAtSimulationStart',...
    'Visible'};

    inactiveProps=getInactiveProperties(obj);

    mainProps=setdiff(mainProps,inactiveProps,'stable');
    advancedProps=setdiff(advancedProps,inactiveProps,'stable');
    spectrogramProps=setdiff(spectrogramProps,inactiveProps,'stable');
    measurementsProps=setdiff(measurementsProps,inactiveProps,'stable');
    visualizationProps=setdiff(visualizationProps,inactiveProps,'stable');

    if obj.SimscapeMode
        dstProps=getDSTOnlyProperties(obj);
        mainProps=setdiff(mainProps,dstProps,'stable');
        advancedProps=setdiff(advancedProps,dstProps,'stable');
        spectrogramProps=setdiff(spectrogramProps,dstProps,'stable');
        measurementsProps=setdiff(measurementsProps,dstProps,'stable');
        visualizationProps=setdiff(visualizationProps,dstProps,'stable');
    end

    advancedGroupTitle=getString(message('dspshared:SpectrumAnalyzer:AdvancedProperties'));
    spectrogramGroupTitle=getString(message('dspshared:SpectrumAnalyzer:SpectrogramProperties'));
    measurementsGroupTitle=getString(message('dspshared:SpectrumAnalyzer:MeasurementsProperties'));
    visualizationGroupTitle=getString(message('dsp:system:Shared:VisualizationProperties'));

    mainGroup=matlab.mixin.util.PropertyGroup(mainProps,'');
    advancedGroup=matlab.mixin.util.PropertyGroup(advancedProps,advancedGroupTitle);
    spectrogramGroup=matlab.mixin.util.PropertyGroup(spectrogramProps,spectrogramGroupTitle);
    measurementsGroup=matlab.mixin.util.PropertyGroup(measurementsProps,measurementsGroupTitle);
    visualizationGroup=matlab.mixin.util.PropertyGroup(visualizationProps,visualizationGroupTitle);

    groups=[mainGroup,advancedGroup,spectrogramGroup,measurementsGroup,visualizationGroup];
end
