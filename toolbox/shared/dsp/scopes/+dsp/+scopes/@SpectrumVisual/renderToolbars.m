function renderToolbars(this)




    hScope=this.Application;
    hToolbar=hScope.Handles.mainToolbar;



    if~this.SimscapeMode
        this.Handles.SpectralMaskButton=uitoggletool(hToolbar,...
        'Tag','uimgr.uitoggletool_SpectralMask',...
        'CData',getIcon(hScope,'spectralmask'),...
        'TooltipString',getString(message('dspshared:SpectrumAnalyzer:SpectralMaskToolTip')),...
        'Interruptible','off',...
        'Separator','on',...
        'ClickedCallback',makeCallback(this,@toggleSpectralMaskDialog));
    end

    if isToolbarCompact(this.Application.Specification,'properties')
        orig_state=warning('off','MATLAB:uitogglesplittool:DeprecatedFunction');
        this.Handles.SpectrumSettingsSplitButton=uitogglesplittool(hToolbar,...
        'Tag','uimgr.uitogglesplittool_SpectrumSettings',...
        'CData',getIcon(hScope,'spectrumsettings'),...
        'TooltipString',getString(message('dspshared:SpectrumAnalyzer:SpectrumSettingsToolTip')),...
        'Interruptible','off',...
        'ClickedCallback',makeCallback(this,@toggleSpectrumSettingsDialog),...
        'Callback',@this.onPropertiesButtonOpening);
        warning(orig_state);

        oldOrder=allchild(hToolbar);
        if length(oldOrder)>1
            newOrder=[oldOrder(2:end);oldOrder(1)];
            oldOrder(end).Separator='on';
            hToolbar.Children=newOrder;
        end
    else
        this.Handles.PropertiesButton=uipushtool(hToolbar,...
        'Tag','uimgr.uipushtool_Properties',...
        'CData',getIcon(hScope,'parameters'),...
        'TooltipString',uiscopes.message('DisplayPropertiesContextMenuLabel'),...
        'Interruptible','off',...
        'ClickedCallback',makeCallback(this,@onEditOptions));


        this.Handles.SpectrumSettingsButton=uitoggletool(hToolbar,...
        'Tag','uimgr.uitoggletool_SpectrumSettings',...
        'CData',getIcon(hScope,'spectrumsettings'),...
        'TooltipString',getString(message('dspshared:SpectrumAnalyzer:SpectrumSettingsToolTip')),...
        'Interruptible','off',...
        'ClickedCallback',makeCallback(this,@toggleSpectrumSettingsDialog));

        oldOrder=allchild(hToolbar);
        if length(oldOrder)>2
            newOrder=[oldOrder(3:end);oldOrder(1:2)];
            oldOrder(end).Separator='on';
            hToolbar.Children=newOrder;
        end
    end
