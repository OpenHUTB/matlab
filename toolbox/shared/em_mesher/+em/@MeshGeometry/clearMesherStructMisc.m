function clearMesherStructMisc(obj)

    obj.MesherStruct.MeshingFrequency=[];
    obj.MesherStruct.MeshingChoice='auto';
    obj.MesherStruct.HasMeshChanged=0;
    obj.MesherStruct.Parent=[];
    obj.MesherStruct.IsDynamicPropertyAdded=false;
    obj.MesherStruct.infGP=false;
    obj.MesherStruct.infGPconnected=false;
    obj.MesherStruct.CacheFlag=1;
    obj.MesherStruct.DisplayWaitBar=0;