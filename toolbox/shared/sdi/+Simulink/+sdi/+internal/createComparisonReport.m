function charArray=enumArrayToChar(enumArray)
    enumArray=cellstr(enumArray);
    for n=1:numel(enumArray)
        if numel(enumArray)>1
            if n==1
                charArray=['[''',enumArray{n},''', '];
            elseif n==numel(enumArray)
                charArray=[charArray,'''',enumArray{n},''']'];%#ok<AGROW>
            else
                charArray=[charArray,'''',enumArray{n},''', '];%#ok<AGROW>
            end
        else
            charArray=['''',enumArray{n},''''];
        end
    end
end


