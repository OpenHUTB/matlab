function setSignalInterpMethod(this,id,value)
    this.helperSetTol(id,value,@(id,value)this.sigRepository.setSignalInterpMethod(id,value),...
    'SDI:sdi:MGAbsTolLbl','interp');
    Simulink.sdi.WebClient.refreshInterpolation(id);
    notify(this,'propertyChangeEvent',...
    Simulink.sdi.internal.SDIEvent('propertyChangeEvent',id,...
    Simulink.sdi.internal.StringDict.mgInterpMethod,...
    value));
end