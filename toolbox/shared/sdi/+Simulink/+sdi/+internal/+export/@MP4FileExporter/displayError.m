function displayError(~,msgStr,bCmdLine)

    if~bCmdLine
        titleStr=getString(message('SDI:sdi:ExportError'));
        okStr=getString(message('SDI:sdi:OKShortcut'));
        Simulink.sdi.internal.controllers.SessionSaveLoad.displayMsgBox(...
        'default',...
        titleStr,...
        getString(msgStr),...
        {okStr},...
        0,...
        -1,...
        []);
    end
    error(msgStr);
end
