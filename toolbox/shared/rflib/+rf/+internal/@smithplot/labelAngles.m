function labelAngles(p)




    values=p.GridValue;
    RR=values(1,:);
    XX=values(1,:);
    switch lower(p.GridType)
    case{'z','zy'}
        sign=1;
    case{'y','yz'}
        sign=-1;
    end
    xc=(sign*RR)./(1+RR);
    rd=sign./(1+RR);
    xc=xc-rd;
    tv='on';
    mappoints=p.bilinearmap(complex(0,XX));

    fmt='%g';
    N=numel(RR);
    cstrUser=cell(1,N);
    for i=1:N
        cstrUser{i}=sprintf(fmt,RR(i));
    end

    if ischar(cstrUser)
        cstrUser={cstrUser};
    end

    cstr=cstrUser;








    ht=p.hAngleText;
    delete(ht);

    angleFontSize=getAngleFontSize(p);

    z0=0.294;
    hc1=text(...
    sign*real(mappoints),sign*imag(mappoints),...
    z0*ones(size(xc)),strcat('+j',cstr),...
    'Parent',p.hAxes,...
    'Tag',sprintf('ArcTicks%d',p.pAxesIndex),...
    'HandleVisibility','off',...
    'HorizontalAlignment','center',...
    'FontName',p.FontName,...
    'FontSize',angleFontSize,...
    'Clipping','on',...
    'Color',p.pAngleTickLabelColor);
    hc2=text(...
    sign*real(mappoints),-sign*imag(mappoints),...
    z0*ones(size(xc)),strcat('-j',cstr),...
    'Parent',p.hAxes,...
    'Tag',sprintf('ArcTicks%d',p.pAxesIndex),...
    'HandleVisibility','off',...
    'HorizontalAlignment','center',...
    'FontName',p.FontName,...
    'FontSize',angleFontSize,...
    'Clipping','on',...
    'Color',p.pAngleTickLabelColor);
    hc2(end+1)=text(...
    'Position',[-1.1*sign,0,z0],...
    'String','0.0',...
    'Parent',p.hAxes,...
    'Tag',sprintf('CircleTicks%d',p.pAxesIndex),...
    'HandleVisibility','off',...
    'horizontalalignment','center',...
    'VerticalAlignment','middle',...
    'FontName',p.FontName,...
    'color',p.pAngleTickLabelColor,...
    'Visible',tv,...
    'Clipping','on',...
    'FontSize',angleFontSize);
    if strcmp(get(p.hAxes,'defaultTextInterpreter'),'latex')
        infinitystn='$\infty$';
    elseif strcmp(get(p.hAxes,'defaultTextInterpreter'),'tex')
        infinitystn='\infty';
    else
        infinitystn=native2unicode([226,136,158],'UTF-8');
    end
    hc2(end+1)=text('Position',[1.1*sign,0,z0],...
    'String',infinitystn,...
    'Parent',p.hAxes,...
    'Tag',sprintf('CircleTicks%d',p.pAxesIndex),...
    'HandleVisibility','off',...
    'horizontalalignment','center',...
    'VerticalAlignment','middle',...
    'FontName',p.FontName,...
    'color',p.pAngleTickLabelColor,...
    'Visible',tv,...
    'Clipping','on',...
    'FontSize',angleFontSize);
    ht=[hc1;hc2];
    p.hAngleText=ht;


    for i=1:numel(ht)
        b=hggetbehavior(ht(i),'Plotedit');
        b.Enable=false;
    end

    overrideAngleTickLabelVis(p,'default');
