function object=create(objectName)







    permittedObjects={...
    'CommunicationModeSupportedInfoInterleaved',...
    'CommunicationModeSupportedInfoBlock',...
    'DaqInfo',...
    'DaqListInfo',...
    'EventInfo',...
    'IFDataXCPInfo',...
    'OdtEntryInfo',...
    'OdtInfo',...
    'PagInfo',...
    'ProtocolLayerInfo',...
    'StimInfo',...
    'TimeStampSupportedInfo',...
    'XCPOnSxIInfo',...
    'XCPOnTCPIPInfo',...
    'XCPOnUDPIPInfo',...
    'XCPOnCANInfo',...
    'DaqListCanIdInfo'};

    if~ismember(objectName,permittedObjects)
        error(...
        message('asam_mcd2mc:Errors:CreateInvalidObject',...
        objectName,...
        strjoin(permittedObjects,', ')));
    end

    object=asam.mcd2mc.ObjectFactory.create(objectName);

end
