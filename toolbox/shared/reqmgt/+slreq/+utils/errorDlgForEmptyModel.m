function errorDlgForEmptyModel(type)
    switch type
    case 'open perspective'
        errordlg(getString(message('Slvnv:slreq:PerspectiveDisabledForEmptyFile')),...
        getString(message('Slvnv:slreq:PerspectiveActionFailed')),...
        'modal');
    case 'create links'
        errordlg(getString(message('Slvnv:slreq:MustSaveBeforLinking')),...
        getString(message('Slvnv:slreq:LinkActionFailed')),...
        'modal');
    end
end