function rootData=initRootData(srcName)




    [~,~,ext]=fileparts(srcName);
    switch ext
    case '.m'
        rootData={...
        'source','linktype_rmi_matlab';...
        'lastDay',num2str(floor(now));...
        'lastId',num2str(0)...
        };
    case '.sldd'
        rootData={...
        'source','linktype_rmi_data';...
        'lastDay',num2str(floor(now))...
        };
    case '.mldatx'
        rootData={...
        'source','linktype_rmi_testmgr';...
        'lastDay',num2str(floor(now))...
        };
    otherwise

        [isMatlabInSl,mdlName]=rmisl.isSidString(srcName,false);
        if isMatlabInSl&&rmidata.isExternal(mdlName)
            rootData={...
            'source','linktype_rmi_matlab';...
            'lastDay',num2str(floor(now));...
            'lastId',num2str(0)...
            };
        else
            rootData=[];
        end
    end
end




