function funHeaderStr=objFunctionHeader(objectiveFcnName,hasExtraParams,...
    objectiveValue,gradientValue,inputVariables,extraParamsName,...
    ADType,derivativeName)

















    if any(strcmpi(ADType,{'finite-differences','none'}))
        headerMsgId='shared_adlib:codeComments:ObjectiveFunctionHeader';
    else
        headerMsgId='shared_adlib:codeComments:ObjectiveFunctionHeaderWithGradient';
    end





    helpThreeSpaces="   ";





    if isstrprop(objectiveFcnName,"lower")
        helpFcnName=upper(objectiveFcnName);
    else
        helpFcnName=objectiveFcnName;
    end

    if~hasExtraParams

        functionSignature="function ["+objectiveValue+", "+gradientValue+"] = "+...
        objectiveFcnName+"("+inputVariables+")"+newline;

        helpText=helpFcnName+" "+...
        getString(message(headerMsgId,derivativeName))+newline+...
        newline+...
        helpThreeSpaces+upper(objectiveValue)+" = "+helpFcnName+"("+upper(inputVariables)+") "+...
        getString(message('shared_adlib:codeComments:ObjectiveFunctionSyntax',...
        upper(objectiveValue),upper(inputVariables)))+newline+...
        newline+...
        helpThreeSpaces+"["+upper(objectiveValue)+", "+upper(gradientValue)+"] = "+...
        helpFcnName+"("+upper(inputVariables)+") ";
    else

        functionSignature="function ["+objectiveValue+", "+gradientValue+"] = "+...
        objectiveFcnName+"("+inputVariables+", "+extraParamsName+")"+newline;

        helpText=helpFcnName+" "+...
        getString(message(headerMsgId,derivativeName))+newline+...
        newline+...
        helpThreeSpaces+upper(objectiveValue)+" = "+helpFcnName+"("+upper(inputVariables)+", "+upper(extraParamsName)+") "+...
        getString(message('shared_adlib:codeComments:ObjectiveFunctionSyntaxWithExtraParams',...
        upper(objectiveValue),upper(inputVariables),upper(extraParamsName)))+newline+...
        newline+...
        helpThreeSpaces+"["+upper(objectiveValue)+", "+upper(gradientValue)+"] = "+...
        helpFcnName+"("+upper(inputVariables)+", "+upper(extraParamsName)+") ";
    end


    helpText=helpText+getString(message('shared_adlib:codeComments:ObjectiveGradientSyntax',...
    derivativeName,upper(gradientValue)))+newline+...
    newline+...
    helpThreeSpaces+getString(message('shared_adlib:codeComments:AutoGenerated',datestr(now)));

    helpText=matlab.internal.display.printWrapped(helpText,73);
    helpText(end)=[];


    helpText=strjoin("%"+splitlines(helpText),'\n')+newline+newline;


    funHeaderStr=functionSignature+helpText;
end