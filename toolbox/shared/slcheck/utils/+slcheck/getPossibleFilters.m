function filters=getPossibleFilters(designData)


    filters={};

    if isempty(designData)
        return;
    end

    persistent filterMap;

    if isempty(filterMap)
        keySet={...
        'Block',...
        'SubSystem',...
        'Library',...
        'Transition',...
        'Event',...
        'State',...
        'Junction',...
        'GraphicalFunction',...
        'TruthTable',...
        'SimulinkFunction',...
        'MATLABFunction',...
        'SimulinkBasedState',...
        'Chart',...
        'Subchart',...

        };
        valueSet={...
        {'Block','BlockType'}...
        ,{'Block','Subsystem','MaskType'}...
        ,{'Library'}...
        ,{'Transition','Event'}...
        ,{'Event'}...
        ,{'State','Event'}...
        ,{'Junction'}...
        ,{'GraphicalFunction'}...
        ,{'TruthTable'}...
        ,{'SimulinkFunction'}...
        ,{'MATLABFunction'}...
        ,{'SimulinkBasedState'}...
        ,{'Chart'}...
        ,{'Subchart'}...
        };
        filterMap=containers.Map(keySet,valueSet);
    end
    filters=filterMap(designData);
end