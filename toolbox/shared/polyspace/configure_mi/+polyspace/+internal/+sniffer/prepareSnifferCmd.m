

function[psConfigureOutDir,psConfigureCmd]=prepareSnifferCmd()


    psConfigureOutDir=polyspace.internal.createTempDir();

    psConfigureCmd=fullfile(matlabroot,'bin',computer('arch'),'ps_configure');

    psConfigureTmpPath=fullfile(psConfigureOutDir,'tmp_path');
    psConfigureBuildTraceFile=fullfile(psConfigureOutDir,'trace_file.txt');
    psConfigureCachePath=fullfile(psConfigureOutDir,'cache');

    psConfigureCmd=...
    {psConfigureCmd,...
    '-tmp-path',...
    psConfigureTmpPath,...
    '-build-trace',...
    psConfigureBuildTraceFile,...
    '-no-project',...
    '-cache-path',...
    psConfigureCachePath};
end
