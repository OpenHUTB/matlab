function schema





    pkg=findpackage('rptgen');

    h=schema.class(pkg,...
    'cml_eval',...
    pkg.findclass('rptcomponent'));


    rptgen.makeProp(h,'EvalString','ustring',...
    sprintf('%%%s\n\n',...
    getString(message('rptgen:cml_eval:evalStringLabel_a'))),...
    getString(message('rptgen:cml_eval:evalStringLabel_b')));


    rptgen.makeProp(h,'isCatch','bool',true,...
    getString(message('rptgen:cml_eval:isCatchLabel')));

    rptgen.makeProp(h,'CatchString','ustring',...
    sprintf(['%% The exception generated above can be referenced by evalException\n',...
    '%% See the default code below for an example\n',...
    'warningMessageLevel = 2;\n',...
    'displayWarningMessage = true;\n',...
    'failGenerationWithException = false;\n',...
    'failGenerationWithoutException = false;\n\n',...
    'if (displayWarningMessage)\n',...
    '    rptgen.displayMessage(sprintf(''%%s : %%s'',''',getString(message('rptgen:cml_eval:errorDuringEvalMessage')),''', evalException.message),warningMessageLevel);\n',...
    'end\n\n',...
    'if (failGenerationWithException)\n',...
    '    rptgen.displayMessage(''',getString(message('rptgen:cml_eval:evalFailedMessage')),''', warningMessageLevel);\n',...
    '    rethrow(evalException);\n\n',...
    'elseif (failGenerationWithoutException)\n',...
    '    rptgen.displayMessage(sprintf(''%%s\\n%%s'',''',getString(message('rptgen:cml_eval:evalFailedMessage')),''',''',getString(message('rptgen:cml_eval:haltingGenerationMessage')),'''), warningMessageLevel, false);\n',...
    '    rptgen.haltGenerate;\n',...
    'end']));





    rptgen.makeProp(h,'isDiary','bool',true,...
    getString(message('rptgen:cml_eval:isDiaryLabel')));


    rptgen.makeProp(h,'isInsertString','bool',true,...
    getString(message('rptgen:cml_eval:isInsertStringLabel')));


    rptgen.makeStaticMethods(h,{
    },{
    });
