function createDefaultSteps(hSetup,workflow,filterCriteria,disableBackButton)














    hSetup.Steps=hwconnectinstaller.Step('Root',...
    [],...
    [],...
    [],...
    'setup','hwconnectinstaller',hSetup);
    hSetup.Steps.Selectable=true;

    tSteps(1)=hwconnectinstaller.Step('Install',...
    @hwconnectinstaller.internal.getInstallLocationSchema,...
    @(varargin)hwconnectinstaller.internal.executeInstallLocation(filterCriteria,varargin{:}),...
    [],...
    'setup','hwconnectinstaller',hSetup);
    tSteps(1).Selectable=true;


    tSteps(1).Children(end+1)=hwconnectinstaller.Step('MWALogin',...
    @hwconnectinstaller.internal.getMWALoginSchema,...
    @hwconnectinstaller.internal.executeMWALogin,...
    @(~)~hSetup.MWALogin.IsLoggedIn&&(hSetup.isInstallFromInternet()||hSetup.canAccessInternet())&&~hSetup.InstallerWorkflow.isUninstall,...
    'setup','hwconnectinstaller',hSetup);

    tSteps(1).Children(end+1)=hwconnectinstaller.Step('MWLicense',...
    @hwconnectinstaller.internal.getMWLicenseSchema,...
    @hwconnectinstaller.internal.executeMWLicense,...
    @(~)~hSetup.IsUSRPWorkflow&&...
    ~hSetup.InstallerWorkflow.isDownload&&~hSetup.InstallerWorkflow.isUninstall,...
    'setup','hwconnectinstaller',hSetup);


    tSteps(1).Children(end+1)=hwconnectinstaller.Step('USRP_CustomTPLicense',...
    @hwconnectinstaller.internal.getCustomLicenseTPSchema,...
    @hwconnectinstaller.internal.executeCustomLicenseTP,...
    @(~)hSetup.IsUSRPWorkflow&&~hSetup.InstallerWorkflow.isUninstall,...
    'setup','hwconnectinstaller',hSetup);

    tSteps(1).Children(end+1)=hwconnectinstaller.Step('USRP_CustomMWLicense',...
    @hwconnectinstaller.internal.getCustomLicenseMWSchema,...
    @hwconnectinstaller.internal.executeCustomLicenseMW,...
    @(~)hSetup.IsUSRPWorkflow&&~hSetup.InstallerWorkflow.isUninstall,...
    'setup','hwconnectinstaller',hSetup);


    tSteps(1).Children(end+1)=hwconnectinstaller.Step('VerifyDetails',...
    @hwconnectinstaller.internal.getVerifyDetailsSchema,...
    @hwconnectinstaller.internal.executeVerifyDetails,...
    @hwconnectinstaller.internal.visibleVerifyDetails,...
    'setup','hwconnectinstaller',hSetup);

    tSteps(1).Children(end+1)=hwconnectinstaller.Step('Confirm',...
    @hwconnectinstaller.internal.getConfirmSchema,...
    @hwconnectinstaller.internal.executeConfirm,...
    [],...
    'setup','hwconnectinstaller',hSetup);

    tSteps(1).Children(end+1)=hwconnectinstaller.Step('InstallComplete',...
    @hwconnectinstaller.internal.getInstallCompleteSchema,...
    @hwconnectinstaller.internal.executeInstallComplete,...
    [],...
    'setup','hwconnectinstaller',hSetup);

    tSteps(2)=hwconnectinstaller.Step('Update',...
    @hwconnectinstaller.internal.getUpdateFirmwareSchema,...
    @hwconnectinstaller.internal.executeUpdateFirmware,...
    [],...
    'setup','hwconnectinstaller',hSetup);
    tSteps(2).Selectable=true;

    hSetup.Steps.Children=tSteps;

    hSetup.Steps.Parent=hSetup;
end
