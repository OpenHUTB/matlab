function[Mesh,Parts]=makeDielectricMesh(obj,antobj,varargin)





    if isa(antobj,'em.Antenna')||isa(antobj,'em.PCBStructures')
        if isa(antobj,'cavity')
            [Mesh,Parts]=makeBoxMesh(obj,antobj,varargin{:});
        elseif isa(antobj,'cavityCircular')
            [Mesh,Parts]=makeCylinderMesh(obj,antobj,varargin{:});
        else
            [Mesh,Parts]=makeAntennaDielectricMesh(obj,antobj,varargin{1});
        end
    elseif isa(antobj,'em.Array')
        [Mesh,Parts]=makeArrayDielectricMesh(obj,antobj,varargin{1});
    end


































































































































































































