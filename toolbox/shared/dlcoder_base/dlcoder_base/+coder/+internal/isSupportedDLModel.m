function validDLObject=isSupportedDLModel(dlObj)




    isDeepLearningNetworkFcn=@(obj)isa(obj,'SeriesNetwork')||...
    isa(obj,'DAGNetwork')||...
    isa(obj,'dlnetwork');

    validDLObject=isa(dlObj,'yolov2ObjectDetector')||...
    isa(dlObj,'yolov3ObjectDetector')||...
    isa(dlObj,'yolov4ObjectDetector')||...
    isa(dlObj,'ssdObjectDetector')||...
    isa(dlObj,'pointPillarsObjectDetector')||...
    isDeepLearningNetworkFcn(dlObj);

    if coder.internal.hasPublicMethod(class(dlObj),'matlabCodegenPrivateNetwork')
        net=matlabCodegenPrivateNetwork(dlObj);
        validDLObject=validDLObject||isDeepLearningNetworkFcn(net);
    end

end