function figH=makeFigureOneD(xData,xLabel,yData,yLabel)



    if length(xData)~=length(yData)
        xData=1:length(yData);
        xLabel=[xLabel,' [indices]'];
    end

    figH=figure(...
    'Visible','off',...
    'CloseRequestFcn','set(gcbf,''Visible'',''off'')',...
    'Name','Report Generator Temporary Drawing Canvas');

    set(figH,...
    'Color','white',...
    'InvertHardcopy','off',...
    'HandleVisibility','on');

    axHandle=axes('Parent',figH,...
    'HandleVisibility','on',...
    'Box','on',...
    'Color',[1,1,1],...
    'XlimMode','auto',...
    'YlimMode','auto');

    line('Parent',axHandle,...
    'Xdata',xData,...
    'Ydata',yData,...
    'Color',[0,0,1],...
    'LineWidth',2,...
    'Marker','.');

    set(get(axHandle,'xlabel'),'String',xLabel);
    set(get(axHandle,'ylabel'),'String',yLabel);

end
