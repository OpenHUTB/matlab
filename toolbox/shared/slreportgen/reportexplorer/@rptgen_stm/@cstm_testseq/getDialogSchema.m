function dlgStruct=getDialogSchema(this,name)




    if~builtin('license','checkout','SIMULINK_Report_Gen')
        dlgStruct=this.buildErrorMessage(name,true);
        return;
    end

    w=this.dlgWidget;



    [wTitleMode,tTitleMode]=this.dlgWidget('TitleMode',...
    'ColSpan',[1,2],...
    'RowSpan',[1,1],...
    'DialogRefresh',true);

    wTitle=this.dlgWidget('Title',...
    'ColSpan',[3,3],...
    'RowSpan',[1,1],...
    'Enabled',strcmp(this.TitleMode,'manual'));

    wStepContent=this.dlgWidget('StepContent',...
    'ColSpan',[1,3],...
    'RowSpan',[2,2],...
    'Type','radiobutton');

    wStepRequirements=this.dlgWidget('StepRequirements',...
    'ColSpan',[1,3],...
    'RowSpan',[3,3],...
    'Type','checkbox');


    dlgStruct=this.dlgMain(name,{
tTitleMode
wTitleMode
wTitle
wStepContent
wStepRequirements
    },...
    'LayoutGrid',[4,3],...
    'ColStretch',[0,0,1],...
    'RowStretch',[0,0,0,1]);
