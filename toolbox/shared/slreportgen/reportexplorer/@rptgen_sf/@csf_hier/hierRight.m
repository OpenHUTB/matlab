function[nRight,isAll]=hierRight(this,thisNode)




    if isa(thisNode,'Stateflow.Machine')
        nRight=rptgen_sf.csf_hier.hierSibling(thisNode,slroot,false);
        isAll=true;
    elseif isa(thisNode,'Stateflow.Chart')
        nRight=rptgen_sf.csf_hier.hierSibling(thisNode,thisNode.Machine,false);
        isAll=true;
    elseif isa(thisNode,'Stateflow.Object')
        if isa(thisNode,"handle.handle")
            nRight=right(thisNode);
        else
            nRight=thisNode.getNext;
        end
        isAll=false;

        if this.SkipAutogenerated&&...
            ~isempty(nRight)&&...
            ~rptgen_sf.isNotAutogenerated(nRight)
            [nRight,isAll]=hierRight(this,nRight);
        end

    else
        nRight=[];
        isAll=false;
    end

