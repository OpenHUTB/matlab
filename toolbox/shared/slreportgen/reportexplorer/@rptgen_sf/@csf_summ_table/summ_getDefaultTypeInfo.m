function defaultTypeInfo=summ_getDefaultTypeInfo(typeID)








    psSF=rptgen_sf.propsrc_sf;

    switch typeID
    case{'Chart','AtomicSubchart'}
        defaultTypeInfo=rptgen.summsrc(typeID,...
        psSF,...
        rptgen_sf.csf_chart_loop,...
        {'Name'},...
        false);
    case 'Machine'
        defaultTypeInfo=rptgen.summsrc(typeID,...
        psSF,...
        rptgen_sf.csf_machine_loop,...
        {'Name'},...
        false);
    case{'State'
'Function'
'Box'
'EMFunction'
'SLFunction'
        'TruthTable'}

        defaultTypeInfo=rptgen.summsrc(typeID,...
        psSF,...
        setObjectType(rptgen_sf.csf_state_loop,typeID),...
        {'Name'},...
        false);
    case listTerminalTypes(rptgen_sf.appdata_sf)
        defaultTypeInfo=rptgen.summsrc(typeID,...
        psSF,...
        setObjectType(rptgen_sf.csf_obj_loop,typeID),...
        {'Name'},...
        true);
    otherwise
        error(message('RptgenSL:rsf_csf_summ_table:unrecognizedSummSrcType',typeID));
    end
