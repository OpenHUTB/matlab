function dlgStruct=getDialogSchema(this,name)






    this.updateErrorState;

    if~builtin('license','checkout','SIMULINK_Report_Gen')
        dlgStruct=this.buildErrorMessage(name,true);
        return;
    end

    wImportType=this.dlgWidget(...
    'ImportType',...
    'RowSpan',[1,1],...
    'ColSpan',[1,1],...
    'DialogRefresh',1);

    wLinkingAnchor=this.dlgWidget(...
    'LinkingAnchor',...
    'RowSpan',[2,2],...
    'ColSpan',[1,1]);

    wConvertHTML=this.dlgWidget(...
    'ConvertHTML',...
    'RowSpan',[3,3],...
    'ColSpan',[1,1]);

    wEmbedFile=this.dlgWidget(...
    'EmbedFile',...
    'RowSpan',[4,4],...
    'ColSpan',[1,1]);

    dlgStruct=this.dlgMain(name,{
wImportType
wLinkingAnchor
wConvertHTML
wEmbedFile
    },...
    'LayoutGrid',[3,1],...
    'RowStretch',[0,0,1]);

