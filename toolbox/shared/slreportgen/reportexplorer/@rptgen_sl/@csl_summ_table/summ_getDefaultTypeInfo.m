function defaultTypeInfo=summ_getDefaultTypeInfo(typeID)







    switch typeID
    case 'Model'
        mdlLooper=rptgen_sl.csl_mdl_loop;
        mdlLooper.LoopList(1).MdlName='$all';

        defaultTypeInfo=rptgen.summsrc(typeID,...
        rptgen_sl.propsrc_sl_mdl,...
        mdlLooper,...
        {'Name','Description','FileName'},...
        false);
    case 'System'
        defaultTypeInfo=rptgen.summsrc(typeID,...
        rptgen_sl.propsrc_sl_sys,...
        rptgen_sl.csl_sys_loop,...
        {'Name','Description'},...
        false);
    case 'Block'
        defaultTypeInfo=rptgen_sl.summsrc_sl_blk(typeID,...
        rptgen_sl.propsrc_sl_blk,...
        rptgen_sl.csl_blk_loop,...
        {'Name','Parent','%<SplitDialogParameters>'},...
        true);
    case 'Signal'
        defaultTypeInfo=rptgen.summsrc(typeID,...
        rptgen_sl.propsrc_sl_sig,...
        rptgen_sl.csl_sig_loop('SortBy','alphabetical-exclude-empty'),...
        {'Name','ParentBlock'},...
        true);
    case 'Annotation'
        defaultTypeInfo=rptgen.summsrc(typeID,...
        rptgen_sl.propsrc_sl_annotation,...
        rptgen_sl.CAnnotationLoop,...
        {'Text'},...
        true);
    case 'ConfigSet'
        defaultTypeInfo=rptgen.summsrc(typeID,...
        rptgen_sl.propsrc_sl_configset,...
        rptgen_sl.CConfigSetLoop,...
        {'Name'},...
        true);
    otherwise
        error(message('RptgenSL:rsl_csl_summ_table:invalidSummsrcError',typeID));
    end
