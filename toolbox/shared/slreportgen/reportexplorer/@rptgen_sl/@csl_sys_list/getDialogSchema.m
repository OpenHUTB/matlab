function dlgStruct=getDialogSchema(this,name)





    if~builtin('license','checkout','SIMULINK_Report_Gen')
        dlgStruct=this.buildErrorMessage(name,true);
        return;
    end


    [wStartSys,tStartSys]=this.dlgWidget('StartSys',...
    'RowSpan',[1,1],...
    'ColSpan',[2,2]);
    wHighlightStartSys=this.dlgWidget('HighlightStartSys',...
    'RowSpan',[2,2],...
    'ColSpan',[1,2]);

    pStartingSystem=this.dlgContainer({
tStartSys
wStartSys
wHighlightStartSys
    },getString(message('RptgenSL:rsl_csl_sys_list:startingSystemLabel')),...
    'LayoutGrid',[2,2],...
    'RowSpan',[1,1],...
    'ColSpan',[1,1],...
    'ColStretch',[0,1]);


    [wParentDepth,tParentDepth]=this.dlgWidget('ParentDepth',...
    'RowSpan',[1,1],...
    'ColSpan',[2,2]);
    wIsPeers=this.dlgWidget('isPeers',...
    'RowSpan',[2,2],...
    'ColSpan',[1,2]);
    [wChildDepth,tChildDepth]=this.dlgWidget('ChildDepth',...
    'RowSpan',[3,3],...
    'ColSpan',[2,2]);

    pDisplaySystem=this.dlgContainer({
tParentDepth
wParentDepth
wIsPeers
tChildDepth
wChildDepth
    },getString(message('RptgenSL:rsl_csl_sys_list:displaySystemsLabel')),...
    'LayoutGrid',[3,2],...
    'RowSpan',[2,2],...
    'ColSpan',[1,1],...
    'ColStretch',[0,1]);


    dlgStruct=this.dlgMain(name,{
pStartingSystem
pDisplaySystem
    this.list_getDialogSchema(name);
    },...
    'LayoutGrid',[4,1],...
    'RowStretch',[0,0,0,1]);

