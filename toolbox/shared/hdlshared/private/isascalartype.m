function isScalar=isascalartype(portDims)





    if((isequal(portDims,[1,1]))||...
        (isequal(portDims,[2,1,1]))||...
        (isequal(portDims,0))||...
        (isequal(portDims,1))||...
        (isequal(portDims,[0,0]))||...
        (isequal(portDims,[0,1]))||...
        (isequal(portDims,[1,0]))...
        )
        isScalar=true;
    else
        isScalar=false;
    end
end
