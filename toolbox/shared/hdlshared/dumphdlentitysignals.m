function dumphdlentitysignals(entry)



    if hdlispirbased
        error(message('HDLShared:directemit:slhdlcodercall',mfilename));
    end

    signalTable=hdlgetsignaltable;
    if~(signalTable.isEmpty)
        lastsig=hdllastsignal;
        if nargin==0
            entry=1:lastsig;
        end
        entry(entry>lastsig)=0;

        fprintf(1,'\n');
        for n=entry
            if n~=0
                if hdlisinportsignal(n)
                    portchar='i';
                elseif hdlisoutportsignal(n)
                    portchar='o';
                else
                    portchar=' ';
                end

                isvector=signalTable.getVector(n);
                name=hdlsignalname(n);
                complex=signalTable.getComplex(n);
                vtype=signalTable.getVType(n);
                sltype=signalTable.getSLType(n);
                rate=signalTable.getRate(n);
                forward=signalTable.getForwards(n);
                handle=hdlsignalhandle(n);

                if isvector==0
                    fprintf(1,'%4d: %s %-32s %-20e c=%d %s %s r=%-10e',...
                    n,...
                    portchar,...
                    name,...
                    handle,...
                    complex,...
                    vtype,...
                    sltype,...
                    rate);
                elseif(length(isvector)==1)
                    fprintf(1,'%4d: %s %-32s %-20e c=%d [%d] %s %s r=%-10e',...
                    n,...
                    portchar,...
                    name,...
                    handle,...
                    complex,...
                    isvector(1),...
                    vtype,...
                    sltype,...
                    rate);
                else
                    fprintf(1,'%4d: %s %-32s %-20e c=%d [%d %d] %s %s r=%-10e',...
                    n,...
                    portchar,...
                    name,...
                    handle,...
                    complex,...
                    isvector(1),isvector(2),...
                    vtype,...
                    sltype,...
                    rate);
                end

                if forward~=0
                    fprintf(1,' forwarded to: [ ');
                    fprintf(1,'%d ',forward);
                    fprintf(1,']');
                end

                fprintf(1,'\n');

            end
        end
    end
end
