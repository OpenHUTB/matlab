function hdlcode=baseEmit(this)





    oldsc=hdlsequentialcontext;
    hdlsequentialcontext(true);

    this.regOutputs;

    hdlcode=this.preBody;


    if(this.isProcessNeeded)
        hdlcode=hdlcodeconcat([hdlcode...
        ,this.processStartExpr,...
        this.asyncResetExpr,...
        this.resetBody('async'),...
        this.clockExpr,...
        this.syncResetExpr,...
        this.resetBody('sync'),...
        this.clockEnableExpr,...
        this.bodyExpr,...
        this.endClockEnableExpr,...
        this.endSyncResetExpr,...
        this.endClockExpr,...
        this.endAsyncResetExpr,...
        this.processEndExpr]);
    end

    hdlsequentialcontext(oldsc);


    hdlcode=hdlcodeconcat([hdlcode,this.postBody,this.signalDecl]);

