function str=ramFileHeader(this)


    cs=[hdlgetparameter('comment_char'),blanks(1)];
    nl=hdl.newline;

    filename=this.fullFileName;
    blockname=this.entityName;
    blockpath=this.fullPathName;

    mver=ver('matlab');
    sver=ver('hdlcoder');

    if hdlgetparameter('datecomment')==1
        createdate=['Created: ',datestr(now,31),nl];
    else
        createdate='';
    end

    str=[...
    cs,repmat('-',1,61),nl...
    ,cs,nl...
    ,cs,'File Name: ',filename,nl...
    ,cs,createdate...
    ,cs,'Generated by MATLAB ',mver.Version,' and HDL Coder ',sver.Version,nl...
    ,cs,nl...
    ,hdlgetparameter('rcs_cvs_tag'),...
    cs,nl...
    ,cs,repmat('-',1,61),nl...
    ,nl...
    ,nl...
    ,cs,repmat('-',1,61),nl...
    ,cs,nl...
    ,cs,'Module: ',blockname,nl...
    ,cs,'Source Path: ',blockpath,nl...
    ,cs,nl...
    ,cs,nl...
    ,cs,repmat('-',1,61),nl...
    ];

    str=strrep(str,'%','%%');
    str=strrep(str,'\','\\');
