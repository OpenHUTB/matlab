function str=antappfileheader(varargin)






















    desc='';
    tbx='antenna';
    comment='%';
    dateform=0;

    if~isempty(varargin)
        if isnumeric(varargin{end})
            dateform=varargin{end};
            varargin(end)=[];
        end
    end
    if~isempty(varargin)
        desc=varargin{1};
    end
    if length(varargin)>1
        tbx=varargin{2};
    end
    if length(varargin)>2
        comment=varargin{3};
    end


    minfo=ver('matlab');
    tinfo=ver(tbx);
    tinfo=tinfo(1);



    if isempty(desc)
        descStr=desc;
        str1=sprintf('Generated by MATLAB(R) %s and %s %s.',...
        minfo.Version,tinfo.Name,tinfo.Version);
    elseif length(desc)<10&&desc(end)~='.'
        descStr='';
        str1=sprintf('%s generated by MATLAB(R) %s and %s %s.',...
        desc,minfo.Version,tinfo.Name,tinfo.Version);
    else
        descStr=sprintf(' %s\n%s',desc,comment);
        str1=sprintf('Generated by MATLAB(R) %s and %s %s.',...
        minfo.Version,tinfo.Name,tinfo.Version);
    end



    switch dateform
    case{3,4,5,10,11,12,17,18,27,28}
        str2=sprintf('%s Generated in: %s',comment,...
        datestr(now,dateform));
    case{13,14,15,16}
        str2=sprintf('%s Generated at: %s',comment,...
        datestr(now,dateform));
    otherwise
        str2=sprintf('%s Generated on: %s',comment,...
        datestr(now,dateform));
    end

    str=sprintf('%s%s %s\n%s',comment,descStr,str1,str2);

