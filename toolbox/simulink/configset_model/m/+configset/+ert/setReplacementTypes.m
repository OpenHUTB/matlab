function newVal=setReplacementTypes(~,val)
    valStruct={'double',...
    'single',...
    'int32',...
    'int16',...
    'int8',...
    'uint32',...
    'uint16',...
    'uint8',...
    'boolean',...
    'int',...
    'uint',...
    'char'}';

    newVal=struct('double','',...
    'single','',...
    'int32','',...
    'int16','',...
    'int8','',...
    'uint32','',...
    'uint16','',...
    'uint8','',...
    'boolean','',...
    'int','',...
    'uint','',...
    'char','',...
    'uint64','',...
    'int64','');

    if isempty(val)
        return;
    end

    if isstruct(val)
        structLength=length(fieldnames(val));

        if structLength==14
            valStruct=[valStruct;'uint64';'int64'];
        end

        if(structLength==14||structLength==12)&&...
            isequal(sort(fieldnames(val)),sort(valStruct))
            for i=1:length(valStruct)
                newVal.(valStruct{i})=val.(valStruct{i});
            end
        else
            DAStudio.error('RTW:configSet:configSetReplacementTypesSetValidation');
        end
    else
        DAStudio.error('RTW:configSet:configSetReplacementTypesSetValidation');
    end
end

