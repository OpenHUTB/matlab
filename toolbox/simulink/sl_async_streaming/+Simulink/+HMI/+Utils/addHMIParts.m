function[targets,bAdded]=addHMIParts(obj,hmiID)




    targets{1}=...
    Simulink.HMI.Utils.FileKeeper.getHMIRelTarget(hmiID);
    id=...
    [Simulink.HMI.Utils.FileKeeper.HMI_GUI_ID_PREFIX,hmiID];
    bAdded=i_addDynPart(obj,...
    targets{1},...
    Simulink.HMI.Utils.FileKeeper.HMI_GUI_REL,...
    id,...
    Simulink.HMI.Utils.FileKeeper.HMI_GUI_CONTENT_TYPE);

    targets{2}=...
    Simulink.HMI.Utils.FileKeeper.getHMIBndRelTarget(hmiID);
    id=...
    [Simulink.HMI.Utils.FileKeeper.HMI_BND_ID_PREFIX,hmiID];
    i_addDynPart(obj,...
    targets{2},...
    Simulink.HMI.Utils.FileKeeper.HMI_BND_REL,...
    id,...
    Simulink.HMI.Utils.FileKeeper.HMI_BND_CONTENT_TYPE);
end



function bAdded=i_addDynPart(obj,target,relID,partID,contentType)


    existingPart=obj.getParts('id',partID);
    if isempty(existingPart)
        p=Simulink.SLXPart(...
        target,...
        Simulink.HMI.Utils.FileKeeper.HMI_PARENT_IN_SLX,...
        partID,...
        relID,...
        contentType);
        obj.addPart(p);
        bAdded=true;
    else
        assert(strcmp(target,existingPart.name));
        bAdded=false;
    end
end


