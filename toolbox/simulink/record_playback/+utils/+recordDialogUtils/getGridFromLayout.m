

function grid=getGridFromLayout(blockHandle)
    layout=get_param(blockHandle,'Layout');
    if strcmp(layout,DAStudio.message('record_playback:params:Auto'))||...
        strcmp(layout,DAStudio.message('record_playback:params:RowTop'))||...
        strcmp(layout,DAStudio.message('record_playback:params:RowRight'))||...
        strcmp(layout,DAStudio.message('record_playback:params:RowBottom'))||...
        strcmp(layout,DAStudio.message('record_playback:params:RowLeft'))||...
        strcmp(layout,DAStudio.message('record_playback:params:OverlayTop'))||...
        strcmp(layout,DAStudio.message('record_playback:params:OverlayRight'))||...
        strcmp(layout,DAStudio.message('record_playback:params:OverlayBottom'))||...
        strcmp(layout,DAStudio.message('record_playback:params:OverlayLeft'))
        grid='';
    else
        grid=layout;
    end
end

