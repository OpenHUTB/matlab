function[bool]=isDerivedSignal(aVar)




    if nargin>0
        aVar=convertStringsToChars(aVar);
    end

    bool=false;



    if(isa(aVar,'Simulink.derivedSignal.Constant')...
        ||isa(aVar,'Simulink.derivedSignal.SineWave')...
        ||isa(aVar,'Simulink.derivedSignal.Addition')...
        ||isa(aVar,'Simulink.derivedSignal.Subtraction')...
        ||isa(aVar,'Simulink.derivedSignal.Multiplication')...
        ||isa(aVar,'Simulink.derivedSignal.Division')...
        ||isa(aVar,'Simulink.derivedSignal.Step')...
        ||isa(aVar,'derivedSignals.derivedSignal')...
        ||isa(aVar,'Simulink.derivedSignals.Pulse')...
        ||isa(aVar,'Simulink.derivedSignals.SquareWave')...
        ||isa(aVar,'Simulink.derivedSignal.AbsoluteValue'))
        bool=true;
    end

end