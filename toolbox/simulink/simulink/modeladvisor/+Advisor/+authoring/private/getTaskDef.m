function[mcode]=getTaskDef(checkDef)
    tab=sprintf('\t');
    lb=sprintf('\n');

    cleanID=regexprep(checkDef.ID,'\W','_');
    taskObjName=['MAT',cleanID];

    mcode=[tab,taskObjName,' = ModelAdvisor.Task(''',checkDef.ID,'.task'');',lb,...
    tab,taskObjName,'.DisplayName = ''',checkDef.Title,''';',lb,...
    tab,taskObjName,'.Description = ''',checkDef.ToolTip,''';',lb,...
    tab,'setCheck(',taskObjName,', ''',checkDef.ID,''');',lb,...
    tab,'mdladvRoot.register(',taskObjName,');',lb,lb,...
    tab,'MAG.addTask(',taskObjName,');',lb,lb,...
    ];
end