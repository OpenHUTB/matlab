function convertProjectResultDataIntoResultDetailObjs(CheckObj,TaskObj)
    ElementResults=Advisor.Utils.createResultDetailObjs(CheckObj.ProjectResultData,...
    'TaskID',TaskObj.ID,...
    'CheckID',CheckObj.ID,...
    'Description',CheckObj.Description,...
    'Title',CheckObj.Title,...
    'Information',CheckObj.Description,...
    'Status',getString(message('ModelAdvisor:engine:WarnAccuracyOfAutoConvertedResultDetails')));
    CheckObj.setResultDetails(ElementResults);
end
