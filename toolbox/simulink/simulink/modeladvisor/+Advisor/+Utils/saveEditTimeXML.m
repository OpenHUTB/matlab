function saveEditTimeXML(this,xmlFileName)




    import matlab.io.xml.dom.*

    docNode=Document('MAConfiguration');
    docRootNode=docNode.getDocumentElement;

    docRootNode.setAttribute('Version','1.0');


    CheckCellArray=docNode.createElement('CheckCellArray');
    CheckCellArray.appendChild(docNode.createTextNode(''));
    docRootNode.appendChild(CheckCellArray);

    edittimeChecks=modeladvisorprivate('modeladvisorutil2','GetEdittimeTasks');
    for i=1:length(edittimeChecks)
        taskObj=this.getTaskObj(edittimeChecks{i});
        if~isempty(taskObj)
            thisElement=docNode.createElement('Check');
            extractConfigUIObj(docNode,thisElement,taskObj);
            CheckCellArray.appendChild(thisElement);
        end
    end

    docNode.appendChild(docNode.createComment('generated by Model Advisor XML export utility. Copyright 2015 The MathWorks, Inc.'));


    xmlwrite(xmlFileName,docNode);
end

function extractConfigUIObj(docNode,thisElement,currentTAObj)
    IDElement=docNode.createElement('ID');
    IDElement.appendChild(docNode.createTextNode(currentTAObj.MAC));
    thisElement.appendChild(IDElement);

    SelectedElement=docNode.createElement('Selected');
    if currentTAObj.Selected
        SelectedElement.appendChild(docNode.createTextNode('true'));
    else
        SelectedElement.appendChild(docNode.createTextNode('false'));
    end
    thisElement.appendChild(SelectedElement);

end
