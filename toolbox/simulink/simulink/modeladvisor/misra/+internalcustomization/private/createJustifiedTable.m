






function justifiedTable=createJustifiedTable(checkID,isJustifyActive)%#ok<INUSL>

    if nargin==1
        isJustifyActive=false;
    end

    justifiedTable=ModelAdvisor.FormatTemplate('TableTemplate');
    justifiedTable.setSubBar(false);

    if isJustifyActive
        justifiedTable.setColTitles({...
        DAStudio.message('RTW:misra:Common_JustifiedTableLocation'),...
        DAStudio.message('RTW:misra:Common_JustifiedTableStandard'),...
        DAStudio.message('RTW:misra:Common_JustifiedTableGuidelines'),...
        DAStudio.message('RTW:misra:Common_JustifiedTableStatus'),...
        DAStudio.message('RTW:misra:Common_JustifiedTableSeverity'),...
        DAStudio.message('RTW:misra:Common_JustifiedTableComment'),...
        DAStudio.message('RTW:misra:Common_JustifiedTableAction')});
    else
        justifiedTable.setColTitles({...
        DAStudio.message('RTW:misra:Common_JustifiedTableLocation'),...
        DAStudio.message('RTW:misra:Common_JustifiedTableStandard'),...
        DAStudio.message('RTW:misra:Common_JustifiedTableGuidelines'),...
        DAStudio.message('RTW:misra:Common_JustifiedTableStatus'),...
        DAStudio.message('RTW:misra:Common_JustifiedTableSeverity'),...
        DAStudio.message('RTW:misra:Common_JustifiedTableComment')});
    end

end

