function fg=getGroupForChecks(HISLCheckData,prefix,group,isdo254)

    fg=ModelAdvisor.FactoryGroup([prefix,'.',group]);
    fg.DisplayName=DAStudio.message(['ModelAdvisor:hism:ByTaskHISM_',group,'_title']);
    fg.Description=DAStudio.message(['ModelAdvisor:hism:ByTaskHISM_',group,'_description']);
    fg.CSHParameters.MapKey='ma.hism';
    fg.CSHParameters.TopicID=['mathworks.hism.task_',group];

    for i=1:size(HISLCheckData,1)
        if strcmp(HISLCheckData{i,2},group)
            if isdo254
                if HISLCheckData{i,3}==true
                    fg.addCheck(HISLCheckData{i,1});
                end
            elseif strcmp(prefix,'hisl_security')
                if HISLCheckData{i,4}==true
                    fg.addCheck(HISLCheckData{i,1});
                end
            else
                fg.addCheck(HISLCheckData{i,1});
            end
        end
    end

end
