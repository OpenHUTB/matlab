function isSubsysType=getIsSubsystemType(blockType)




    isSubsysType=(blockType==sl_variants.analyzer.datamodel.NodeType.SubSystem)||...
    (blockType==sl_variants.analyzer.datamodel.NodeType.VariantSubsystem)||...
    (blockType==sl_variants.analyzer.datamodel.NodeType.VariantSimulinkFunction)||...
    (blockType==sl_variants.analyzer.datamodel.NodeType.VariantInitialize)||...
    (blockType==sl_variants.analyzer.datamodel.NodeType.VariantTerminate)||...
    (blockType==sl_variants.analyzer.datamodel.NodeType.VariantReset);
end
