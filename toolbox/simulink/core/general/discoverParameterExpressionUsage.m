function propVal=discoverParameterExpressionUsage(propVal)



    [selectedButton,~]=uigetpref(...
    'SimulinkControlDesign',...
    'ShowParameterExpressionHintDialog',...
    DAStudio.message('Simulink:dialog:ParameterExpressionHintDialog'),...
    {''
''
    DAStudio.message('Simulink:dialog:ShowParameterExpressionInfo',propVal,['=',propVal])},...
    {DAStudio.message('Simulink:dialog:Ok')},...
    'DefaultButton','Cancel'...
    );

    switch selectedButton
    case 'ok'
        return;
    case 'cancel'
        propVal='n/a';
    end
end
