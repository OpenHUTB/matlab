    incChecksum=CGXE.Utils.md5();

    props={'TargetBitPerChar',...
    'TargetBitPerShort',...
    'TargetBitPerInt',...
    'TargetBitPerLong',...
    'TargetBitPerFloat',...
    'TargetBitPerDouble',...
