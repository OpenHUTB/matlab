function result=lConvertToStruct(blocks)

    map=containers.Map;

    for idx=1:size(blocks,1)
        if map.isKey(blocks{idx,1})
            v=map(blocks{idx,1});
            v{end+1}=blocks{idx,2};%#ok<AGROW>
        else
            v=blocks(idx,2);
        end
        map(blocks{idx,1})=v;
    end

    keys=map.keys;
    result=cell(numel(keys),1);
    for idx=1:numel(keys)
        result{idx,1}=keys{idx};
        result{idx,2}=map(keys{idx});
    end

end
