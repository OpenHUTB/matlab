function writeSelfContentsForSaveVars(obj,vs)








    vs.writeProperty('FirstPoint',obj.FirstPoint);
    vs.writeProperty('Spacing',obj.Spacing);
    vs.writeProperty('FirstPointName',obj.FirstPointName);
    vs.writeProperty('SpacingName',obj.SpacingName);
    vs.writeProperty('TunableSizeName',obj.TunableSizeName);

end

