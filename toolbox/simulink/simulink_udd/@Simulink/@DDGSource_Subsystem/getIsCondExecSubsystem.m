function ret=getIsCondExecSubsystem(~,block)
    ret=false;
    ssType=Simulink.SubsystemType(block.handle);
    if ssType.isEnabledSubsystem()||ssType.isTriggeredSubsystem()...
        ||ssType.isForEachSubsystem()||ssType.isEnabledAndTriggeredSubsystem()...
        ||ssType.isResettableSubsystem()||ssType.isActionSubsystem()...
        ||ssType.isIteratorSubsystem()||ssType.isFunctionCallSubsystem()...
        ||ssType.isSimulinkFunction()
        ret=true;
    end
end
