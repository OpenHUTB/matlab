    'MaskTunableValueString','off',...
    'MaskEnableString','on',...
    'MaskVisibilityString','on',...
    'MaskToolTipString','on',...
    'MaskVariables','NumInputPorts=@1;',...
    'MaskSelfModifiable','on',...
    'MaskIconFrame','on',...
    'MaskIconOpaque','on',...
    'MaskIconRotate','none',...
    'MaskPortRotate','default',...
    'MaskIconUnits','normalized',...
