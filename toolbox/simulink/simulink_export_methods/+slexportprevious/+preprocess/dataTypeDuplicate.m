function dataTypeDuplicate(obj)







    if isR2009bOrEarlier(obj.ver)

        blockType='DataTypeDuplicate';
        maskType='Data Type Duplicate';
        fcnName='sfix_dtprop';
        refRule='simulink/Signal\nAttributes/Data Type\nDuplicate';
        setFcn=@setupDataTypeDuplicate;

        pre2009bRules=blockToSFunction(obj,blockType,...
        maskType,...
        fcnName,...
        refRule,...
        setFcn);

        obj.appendRules(pre2009bRules);

    end

end


