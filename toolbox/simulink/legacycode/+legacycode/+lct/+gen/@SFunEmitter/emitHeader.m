



function emitHeader(this,codeWriter)


    thisDate=datestr(now,0);
    slVer=legacycode.lct.spec.Common.SLVer;


    codeWriter.wMultiCmtStart();
    hdrTxt={...
    sprintf('%s%s Simple C-MEX S-function for function call.',this.Filename),...
    '',...
    '   ABSTRACT:',...
    '     The purpose of this sfunction is to call a simple legacy',...
    '     function during simulation:',...
    '',...
    sprintf('        %s',this.LctSpecInfo.Specs.OutputFcnSpec),...
    '',...
    sprintf('   Simulink version           : %s %s %s',slVer.Version,slVer.Release,slVer.Date),...
    sprintf('   %s source code generated on : %s',this.LctSpecInfo.Specs.Options.language,thisDate),...
    '',...
'THIS S-FUNCTION IS GENERATED BY THE LEGACY CODE TOOL AND MAY NOT WORK IF MODIFIED'...
    };
    cellfun(@(aLine)codeWriter.wMultiCmtMiddle(aLine),hdrTxt);
    codeWriter.wMultiCmtEnd();


    codeWriter.wNewLine;
    codeWriter.wMultiCmtStart();
    codeWriter.wLine('     %%%-MATLAB_Construction_Commands_Start');
    codeWriter.wLine(legacycode.LCT.generateSpecConstructionCmd(this.LctSpecInfo.Specs,'c'));
    codeWriter.wLine('     %%%-MATLAB_Construction_Commands_End');
    codeWriter.wMultiCmtEnd();


