function[sysSummary,blkSummary]=condition_summary(testCnt,options)








    if testCnt==1
        totalCol=1;
    elseif options.cumulativeReport
        totalCol=3;
    else
        totalCol=testCnt+1;
    end
    txtCondition=getString(message('Slvnv:simcoverage:conditionMetric'));
    txtConditionOutcomes=getString(message('Slvnv:simcoverage:cvhtml:ConditionOutcomes'));
    txtNA=getString(message('Slvnv:simcoverage:cvhtml:NA'));


    blkSummary={...
    ['$',txtCondition],...
    {'Cat',...
    {'&in_covperratios',...
    {'#condition.localHits',totalCol},...
    {'#condition.justifiedLocalHits',totalCol},...
    '#condition.localCnt'},...
    ['$ ',txtConditionOutcomes]...
    },'\n'...
    };

    sysSummary={...
    ['$',txtCondition],...
    {'If',{'&isempty','#condition'},...
    ['$',txtNA],...
    ['$',txtNA],...
    'Else',...
    {'If',{'&isempty','#condition.localHits'},...
    ['$',txtNA],...
    'Else',...
    {'Cat',...
    {'&in_covperratios',...
    {'#condition.localHits',totalCol},...
    {'#condition.justifiedLocalHits',totalCol},...
    '#condition.localCnt'},...
    ['$ ',txtConditionOutcomes]...
    }...
    },...
    {'Cat',{'&in_covperratios',...
    {'#condition.totalHits',totalCol},...
    {'#condition.justifiedTotalHits',totalCol},...
    '#condition.totalCnt'},['$ ',txtConditionOutcomes]},...
    },...
'\n'...
    };


