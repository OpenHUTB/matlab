function[sysSummary,blkSummary]=decision_summary_script(metricName,totalCol,txtOutcomes,metricSummAbbrev)




    fieldName=['#',metricName];
    metricSummAbbrev=['$',metricSummAbbrev];
    txtNA=getString(message('Slvnv:simcoverage:cvhtml:NA'));

    blkSummary={...
    metricSummAbbrev,...
    {'Cat',...
    {'&in_covperratios',{[fieldName,'.outHitCnts'],totalCol},...
    {[fieldName,'.justifiedOutHitCnts'],totalCol},...
    [fieldName,'.totalCnts']},...
    ['$ ',txtOutcomes]...
    },'\n'...
    };
    sysSummary={...
    metricSummAbbrev,...
    {'If',{'&isempty',fieldName},...
    ['$',txtNA],...
    ['$',txtNA],...
    'Else',...
    {'If',{'&isempty',[fieldName,'.outlocalCnts']},...
    ['$',txtNA],...
    'Else',...
    {'Cat',...
    {'&in_covperratios',{[fieldName,'.outlocalCnts'],totalCol},...
    {[fieldName,'.justifiedOutlocalCnts'],totalCol},...
    [fieldName,'.totalLocalCnts']},...
    ['$ ',txtOutcomes]...
    }...
    }...
    ,{'Cat',...
    {'&in_covperratios',{[fieldName,'.outTotalCnts'],totalCol},...
    {[fieldName,'.justifiedOutTotalCnts'],totalCol},...
    [fieldName,'.totalTotalCnts']},...
    ['$ ',txtOutcomes]...
    },...
    },...
'\n'...
    };


