function[sysSummary,blkSummary]=tableExec_summary(testCnt,options)









    if testCnt==1
        totalCol=1;
    elseif options.cumulativeReport
        totalCol=3;
    else
        totalCol=testCnt+1;
    end
    txtLookupTable=getString(message('Slvnv:simcoverage:tableMetric'));
    txtInterpolationIntervals=getString(message('Slvnv:simcoverage:cvhtml:InterpolationIntervals'));
    txtNA=getString(message('Slvnv:simcoverage:cvhtml:NA'));
    blkSummary={...
    ['$',txtLookupTable],...
    {'Cat',...
    {'&in_covperratios',...
    {'#tableExec.localHits',totalCol},...
    {'#tableExec.justifiedLocalHits',totalCol},...
    '#tableExec.localCnt'},...
    ['$ ',txtInterpolationIntervals]...
    },'\n'...
    };

    sysSummary={...
    ['$',txtLookupTable],...
    {'If',{'&isempty','#tableExec'},...
    ['$',txtNA],...
    ['$',txtNA],...
    'Else',...
    {'If',{'&isempty','#tableExec.localHits'},...
    ['$',txtNA],...
    'Else',...
    {'Cat',...
    {'&in_covperratios',...
    {'#tableExec.localHits',totalCol},...
    {'#tableExec.justifiedLocalHits',totalCol},...
    '#tableExec.localCnt'},...
    ['$ ',txtInterpolationIntervals]...
    }...
    },...
    {'Cat',{'&in_covperratios',...
    {'#tableExec.totalHits',totalCol},...
    {'#tableExec.justifiedTotalHits',totalCol},...
    '#tableExec.totalCnt'},['$ ',txtInterpolationIntervals]},...
    },...
'\n'...
    };




