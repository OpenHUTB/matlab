function dlgstruct=getDialogSchema(this,~)




    dlgstruct.LayoutGrid=[4,3];
    dlgstruct.RowStretch=[0,0,0,0];
    dlgstruct.ColStretch=[0,0,0];
    descrGroup=struct('Type','group','LayoutGrid',[2,2]);

    msdlg=this.modelSlicerDlg;
    scfg=msdlg.getSource.Model;

    descr=struct('Type','text',...
    'Name',getString(message('Sldv:ModelSlicer:gui:SpecifyStopTime')),...
    'Tag','SpecifyStopTimeMessage',...
    'WordWrap',true);

    descrGroup.Items={descr};
    descrGroup.RowSpan=[1,1];
    descrGroup.ColSpan=[1,3];
    descrGroup.Tag='DecriptionTextGroup';

    if~scfg.initializedInFastRestart()
        modelName=get_param(scfg.modelH,'Name');
        descrFastRestart=struct('Type','text',...
        'Name',getString(message('Sldv:ModelSlicer:gui:ConfigureFastRestartSim',modelName)),...
        'Tag','FastRestartSuggestion',...
        'WordWrap',true);
        descrGroup.Items{end+1}=descrFastRestart;
    end
    if~slavteng('feature','EnhancedCoverageSlicer')
        startTimeText=struct('Type','text',...
        'Name',getString(message('Sldv:ModelSlicer:gui:StartTime')),...
        'RowSpan',[3,3],...
        'ColSpan',[1,1]);
        startTimeEdit=struct('Type','edit',...
        'Value',this.SimStartTime,...
        'Enabled',true,...
        'Tag','SimStartTime',...
        'RowSpan',[3,3],...
        'ColSpan',[2,2]);
    else
        logSeedsCheck=struct('Type','checkbox',...
        'Name',getString(message('Sldv:ModelSlicer:gui:LogSeedsInSDI')),...
        'Tag','CheckLogSeeds',...
        'Value',false,...
        'ToolTip',getString(message('Sldv:ModelSlicer:gui:LogSeedsInSDI')),...
        'RowSpan',[3,3],...
        'ColSpan',[1,2]);
    end

    stopTimeText=struct('Type','text',...
    'Name',getString(message('Sldv:ModelSlicer:gui:StopTime')),...
    'Tag','StopTimeText',...
    'RowSpan',[2,2],...
    'ColSpan',[1,1]);
    stopTimeEdit=struct('Type','edit',...
    'Value',this.SimStopTime,...
    'Tag','SimStopTime',...
    'RowSpan',[2,2],...
    'ColSpan',[2,2]);

    msdlg=this.modelSlicerDlg;
    scfg=msdlg.getSource.Model;
    this.SaveFilePath=this.Model.getSlicexArtifactName();
    saveFileNameEdit=struct('Type','edit',...
    'Name',getString(message('Sldv:ModelSlicer:gui:SaveAs')),...
    'Value',this.SaveFilePath,...
    'Tag','CvSaveFileName',...
    'Enabled',true,...
    'RowSpan',[4,4],...
    'ColSpan',[1,2]);

    bt_save.Name=getString(message('Sldv:ModelSlicer:gui:Change'));
    bt_save.Type='pushbutton';
    bt_save.RowSpan=[4,4];
    bt_save.ColSpan=[3,3];
    bt_save.ObjectMethod='saveButtonCallback';
    bt_save.MethodArgs={'%dialog'};
    bt_save.ArgDataTypes={'handle'};
    bt_save.Tag='cv_save_as';


    dlgstruct.DialogTitle=...
    getString(message('Sldv:ModelSlicer:gui:RecordSimulationTimeWindow',scfg.modelSlicer.model));
    dlgstruct.StandaloneButtonSet={'OK','Cancel'};

    if~slavteng('feature','EnhancedCoverageSlicer')
        dlgstruct.Items={descrGroup,startTimeText,startTimeEdit,stopTimeText,stopTimeEdit};
    else
        dlgstruct.Items={descrGroup,stopTimeText,stopTimeEdit,logSeedsCheck,saveFileNameEdit,bt_save};
    end

    dlgstruct.PreApplyMethod='dlgPreApplyMethod';
    dlgstruct.PreApplyArgs={'%dialog'};
    dlgstruct.PreApplyArgsDT={'handle'};
    dlgstruct.CloseMethod='dlgCloseMethod';
    dlgstruct.CloseMethodArgs={'%dialog','%closeaction'};
    dlgstruct.CloseMethodArgsDT={'handle','string'};
    dlgstruct.DisableDialog=false;
end
