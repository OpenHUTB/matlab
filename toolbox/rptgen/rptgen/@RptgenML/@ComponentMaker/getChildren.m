function c=getChildren(dao)




    persistent COMPONENT_MAKER_DATA_LIBRARY;

    if isempty(COMPONENT_MAKER_DATA_LIBRARY)
        COMPONENT_MAKER_DATA_LIBRARY=RptgenML.Library;
        numCat=RptgenML.LibraryCategory(getString(message('rptgen:RptgenML_ComponentMaker:numericPropsLabel')),...
        'HelpMapKey','obj.RptgenML.ComponentMakerData');
        connect(numCat,COMPONENT_MAKER_DATA_LIBRARY,'up');

        connect(RptgenML.ComponentMakerData('PropertyName','New_Double_Property',...
        'DataTypeString','double',...
        'FactoryValueString','1.1',...
        'Description',getString(message('rptgen:RptgenML_ComponentMaker:newDoubleLabel'))),...
        numCat,'up');

        connect(RptgenML.ComponentMakerData('PropertyName','New_Integer_Property',...
        'DataTypeString','int32',...
        'FactoryValueString','1',...
        'Description',getString(message('rptgen:RptgenML_ComponentMaker:newIntLabel'))),...
        numCat,'up');

        strCat=RptgenML.LibraryCategory(getString(message('rptgen:RptgenML_ComponentMaker:stringPropsLabel')),...
        'HelpMapKey','obj.RptgenML.ComponentMakerData');
        connect(strCat,COMPONENT_MAKER_DATA_LIBRARY,'up');

        connect(RptgenML.ComponentMakerData('PropertyName','New_String_Property',...
        'DataTypeString','string',...
        'FactoryValueString','''Default value''',...
        'Description',getString(message('rptgen:RptgenML_ComponentMaker:newStringLabel'))),...
        strCat,'up');
        connect(RptgenML.ComponentMakerData('PropertyName','New_ParsedString_Property',...
        'DataTypeString',rptgen.makeStringType,...
        'FactoryValueString','''pi = %<pi>''',...
        'Description',getString(message('rptgen:RptgenML_ComponentMaker:newWSStringLabel'))),...
        strCat,'up');
        connect(RptgenML.ComponentMakerData('PropertyName','New_StringVector_Property',...
        'DataTypeString','string vector',...
        'FactoryValueString','{''default'',''value''}',...
        'Description',getString(message('rptgen:RptgenML_ComponentMaker:newVectorLabel'))),...
        strCat,'up');

        enumCat=RptgenML.LibraryCategory(getString(message('rptgen:RptgenML_ComponentMaker:enumeratedPropsLabel')),...
        'HelpMapKey','obj.RptgenML.ComponentMakerData');
        connect(enumCat,COMPONENT_MAKER_DATA_LIBRARY,'up');

        connect(RptgenML.ComponentMakerData('PropertyName','New_Enumerated_Property',...
        'DataTypeString','!ENUMERATION',...
        'FactoryValueString','''value1''',...
        'EnumNames',{'Value 1','Value 2'},...
        'EnumValues',{'value1','value2'},...
        'Description',getString(message('rptgen:RptgenML_ComponentMaker:newEnumLabel'))),...
        enumCat,'up');

        connect(RptgenML.ComponentMakerData('PropertyName','New_TrueFalse_Property',...
        'DataTypeString','bool',...
        'FactoryValueString','true',...
        'Description',getString(message('rptgen:RptgenML_ComponentMaker:newBoolLabel'))),...
        enumCat,'up');
    end

    c=COMPONENT_MAKER_DATA_LIBRARY.getChildren;

