function dlgStruct=getDialogSchema(this,varargin)




    if isLibrary(this)
        globalEnable=0;
    else
        globalEnable=1;
    end

    if isempty(this.DescriptionShort)
        headText=this.ID;
    else
        headText=[this.ID,' - ',this.DescriptionShort];
    end

    dlgStruct=this.dlgContainer({
    this.dlgText(headText,...
    'WordWrap',1,...
    'RowSpan',[1,1],...
    'ColSpan',[1,1])
    this.dlgWidget('UseAttributeSets',...
    'RowSpan',[2,2],...
    'ColSpan',[1,1],...
    'Enabled',globalEnable)
    this.dlgText(getString(message('rptgen:RptgenML_StylesheetAttributeSet:attributesLabel')),...
    'RowSpan',[3,3],...
    'ColSpan',[1,1])
    RptgenML.dlgChildContainer(this,'',...
    'Enabled',globalEnable,...
    'RowSpan',[4,4],...
    'ColSpan',[1,1])
    },getString(message('rptgen:RptgenML_StylesheetAttributeSet:valueLabel')),...
    'LayoutGrid',[4,1],'RowStretch',[0,1,0,0],...
    varargin{:});
