function cm=getContextMenu(this,selectedHandles)




    r=RptgenML.Root;
    e=r.getEditor;

    am=DAStudio.ActionManager;
    cm=am.createPopupMenu(e);




    cm.addMenuItem(am.createAction(e,...
    'Text',getString(message('rptgen:RptgenML_LibraryFile:sendToConverterLabel')),...
    'Callback','exploreAction(getSelectedListNodes(RptgenML.Root,1));',...
    'Icon',fullfile(matlabroot,'toolbox','rptgen','resources','Convert.png'),...
    'StatusTip',''));

    cm.addMenuItem(am.createAction(e,...
    'Text',getString(message('rptgen:RptgenML_LibraryFile:editAsTextLabel')),...
    'Callback',sprintf('edit(''%s'');',fullfile(this.PathName,this.FileName)),...
    'Icon',fullfile(matlabroot,'toolbox','rptgen','resources','open.png'),...
    'StatusTip',getString(message('rptgen:RptgenML_LibraryFile:openInEditorLabel'))));
