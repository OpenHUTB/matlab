classdef ReportFormat<handle





    enumeration
        HTML(...
        'html-file',...
        fullfile(getTemplateDir(),'reportTemplate','documents','root.html'),...
        fullfile(getTemplateDir(),'diffTemplate','documents','root.html'),...
        fullfile(getTemplateDir(),'sectionTemplate','documents','root.html'),...
        fullfile(getTemplateDir(),'subsectionTemplate','documents','root.html')...
        );
        DOCX(...
        'docx',...
        fullfile(getTemplateDir(),'reportTemplate.dotx'),...
        fullfile(getTemplateDir(),'diffTemplate.dotx'),...
        fullfile(getTemplateDir(),'sectionTemplate.dotx'),...
        fullfile(getTemplateDir(),'subsectionTemplate.dotx')...
        );
        PDF(...
        'pdf',...
        fullfile(getTemplateDir(),'reportTemplate.pdftx'),...
        fullfile(getTemplateDir(),'reportTemplate.pdftx'),...
        fullfile(getTemplateDir(),'reportTemplate.pdftx'),...
        fullfile(getTemplateDir(),'reportTemplate.pdftx')...
        );
    end

    properties(Constant,Access=private)
        SupportedFormats={'html','docx','pdf'};
    end

    properties(GetAccess=public,SetAccess=immutable)
RPTGenType
RPTGenReportTemplate
RPTGenDiffTemplate
RPTGenSectionTemplate
RPTGenSubsectionTemplate
    end

    methods
        function obj=ReportFormat(...
            rptgenType,...
            reportTemplate,...
            diffTemplate,...
            sectionTemplate,...
subsectionTemplate...
            )
            obj.RPTGenType=rptgenType;
            obj.RPTGenReportTemplate=reportTemplate;
            obj.RPTGenDiffTemplate=diffTemplate;
            obj.RPTGenSectionTemplate=sectionTemplate;
            obj.RPTGenSubsectionTemplate=subsectionTemplate;
        end

        function ext=getFileExtension(obj)
            ext=lower(char(obj));
        end
    end

end

function templateDir=getTemplateDir()
    templateDir=fullfile(...
    matlabroot,...
    'toolbox',...
    'comparisons',...
    'tree',...
    'view',...
    'rptgen',...
    '+comparisons',...
    '+internal',...
    '+report',...
    '+tree',...
'templates'...
    );
end
