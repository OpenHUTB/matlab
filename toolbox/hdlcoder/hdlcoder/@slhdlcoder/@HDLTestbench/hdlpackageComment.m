function hdlcode=hdlpackageComment(this,node,nname)



    comment_char=hdlgetparameter('comment_char');
    separatorline=[comment_char,' ','-'*ones(1,63-length(comment_char)),'\n'];

    if hdlgetparameter('datecomment')==1
        createdate=[comment_char,' Created: ',datestr(now,31),'\n'];
    else
        createdate='';
    end
    rcs_cvs_tag=hdlgetparameter('rcs_cvs_tag');
    package_suffix=hdlgetparameter('package_suffix');

    pathstr=fileparts(fullfile(this.CodeGenDirectory,...
    [this.TestBenchName,this.TBFileNameSuffix]));
    fp=strrep(pathstr,'\','\\');
    dispname=strrep(fp,char(10),'_');

    slVer=hdlgetparameter('tool_file_comment');

    if hdlgetparameter('isvhdl')
        pkg_desc=' Description: test bench package\n';
        data_desc=' Description: test bench data package\n';
    else
        pkg_desc=' Description: test bench tasks, parameters\n';
        data_desc=' Description: test bench data\n';
    end

    if isempty(node)
        hdlcode=[...
        separatorline,...
        comment_char,'\n',...
        comment_char,' Module: ',nname,package_suffix,'\n',...
        comment_char,' Path: ',dispname,'\n',...
        createdate,...
        slVer,...
        comment_char,pkg_desc,...
        comment_char,'\n',...
        rcs_cvs_tag,...
        comment_char,'\n',...
        separatorline,...
        hdlannotationcomments(node)];
    else
        hdlcode=[...
        separatorline,...
        comment_char,'\n',...
        comment_char,' Module: ',nname,this.TestBenchDataPostfix,'\n',...
        comment_char,' Path: ',dispname,'\n',...
        createdate,...
        slVer,...
        comment_char,data_desc,...
        comment_char,'\n',...
        rcs_cvs_tag,...
        comment_char,'\n',...
        separatorline];
    end


