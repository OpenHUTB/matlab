function v=validBlockMask(~,slbh)





    v=true;
    if slbh<0
        return;
    end
    blkFunction=get_param(slbh,'Function');
    invalidFunctions={...
    'exp',...
    'log',...
    '10^u',...
    'log10',...
    'hermitian',...
    'square',...
    'pow',...
    'conj',...
    'reciprocal',...
    'hypot',...
    'rem',...
    'mod',...
    'transpose',...
'sqrt'...
    };
    validFunctions={...
'magnitude^2'...
    };

    switch blkFunction
    case invalidFunctions
        v=false;
    end
