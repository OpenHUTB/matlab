function str=ramFileHeader(this,filename,blockname,blockpath)






    cs=[hdlgetparameter('comment_char'),blanks(1)];

    mver=ver('matlab');
    sver=ver('hdlcoder');

    str=[...
    cs,repmat('-',1,61),'\n'...
    ,cs,'\n'...
    ,cs,'File Name: ',regexprep(filename,'(\\)',['\\$1']),'\n'...
    ,cs,'Created: ',datestr(now,31),'\n'...
    ,cs,'Generated by MATLAB ',mver.Version,' and HDL Coder ',sver.Version,'\n'...
    ,cs,'\n'...
    ,cs,'\n'...
    ,cs,repmat('-',1,61),'\n'...
    ,'\n'...
    ,'\n'...
    ,cs,repmat('-',1,61),'\n'...
    ,cs,'\n'...
    ,cs,'Module: ',blockname,'\n'...
    ,cs,'Source Path: ',blockpath,'\n'...
    ,cs,'\n'...
    ,cs,'\n'...
    ,cs,repmat('-',1,61),'\n'...
    ];



