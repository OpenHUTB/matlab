function v=validBlockMask(~,slbh)





    v=true;
    if slbh<0
        return;
    end
    blkFunction=get_param(slbh,'Function');



    invalidFunctions={...
    'hermitian',...
    'transpose',...
    'magnitude^2',...
    };
    validFunctions={...
    'exp',...
    '10^u',...
    'log',...
    'log10',...
    'reciprocal',...
    'rem',...
    'mod',...
    'square',...
    'conj',...
    'pow',...
    'sqrt',...
    'hypot',...
    };

    switch blkFunction
    case invalidFunctions
        v=false;
        return;
    end

    if strcmpi(blkFunction,'reciprocal')&&strcmpi(get_param(slbh,'AlgorithmMethod'),'Newton-Raphson')
        v=false;
    end
end
