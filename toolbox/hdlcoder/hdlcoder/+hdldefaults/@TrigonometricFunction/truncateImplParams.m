function implInfo=truncateImplParams(~,slbh,implInfo)

    params={};
    if slbh<0
        return;
    end

    fcnName=get_param(slbh,'Function');
    handlesDenormFcns={...
'atan2'...
    };
    noDenormFcns={...
    'sin',...
    'cos',...
    'tan',...
    'atan',...
    'asin',...
    'acos',...
    'sincos',...
    'cos + jsin',...
    'sinh',...
    'cosh',...
'tanh'...
    ,'asinh',...
'acosh'...
    ,'atanh'...
    };
    NFPNotSupportedFcns={...
...
    };

    NFPArgumentReductionFcns={'sin','cos','sincos','tan','cos + jsin'};
    NFPPartMultOptFcns={'sin','cos','sincos','cos + jsin'};

    if~any(strcmp(fcnName,NFPArgumentReductionFcns))
        params{end+1}='inputrangereduction';
    end

    if~any(strcmp(fcnName,NFPPartMultOptFcns))
        params{end+1}='multiplystrategy';
    end

    if~any(strcmp(fcnName,handlesDenormFcns))
        if any(strcmp(fcnName,noDenormFcns))
            params{end+1}='handledenormals';
        elseif any(strcmp(fcnName,NFPNotSupportedFcns))
            params{end+1,:}={'handledenormals','latencystrategy'};
        end
    end

    if~isempty(params)
        implInfo.remove(params);
    end

    return






