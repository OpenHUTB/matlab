function v=validBlockMask(~,slbh)





    v=true;
    if slbh<0
        return;
    end
    blkFunction=get_param(slbh,'Function');
    invalidFunctions={...
    };


    validApproxNoneFunctions={...
    'sin',...
    'cos',...
    'atan2',...
    'sincos',...
    'cos + jsin',...
    };

    validFunctions={...
    'tan',...
    'asin',...
    'acos',...
    'atan',...
'sinh'...
    ,'cosh'...
    ,'tanh',...
    'asinh',...
    'acosh',...
'atanh'...
    };

    switch blkFunction
    case invalidFunctions
        v=false;

    case validApproxNoneFunctions
        approxMethod=get_param(slbh,'ApproximationMethod');
        if(strcmp(approxMethod,'CORDIC'))
            v=false;
        end
    end

