    hInputDataT,hBoolT,hCounterT,slRate,blockInfo)





    hgaussJordanSwappingLogicN=pirelab.createNewNetwork(...
    'Name','gaussJordanSwappingLogic',...
    'InportNames',{'diagValidIn','rowCount','readData','colCount','swapreadEnable'},...
    'InportTypes',[hBoolT,hCounterT,gaussJordanSwapLogicInSigs(3).Type,hCounterT,hBoolT],...
