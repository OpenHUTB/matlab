
function LowerTriangDataValidIn(~,hN,LTDataValidInInSigs,LTDataValidInOutSigs,...
    slRate)


    hLTDataValidInN=pirelab.createNewNetwork(...
    'Name','LowerTriangDataValidIn',...
    'InportNames',{'lowerTriangEnb','readEnable','rowCount','colCount'},...
    'InportTypes',[LTDataValidInInSigs(1).Type,LTDataValidInInSigs(2).Type,...
    LTDataValidInInSigs(3).Type,LTDataValidInInSigs(4).Type],...
    'InportRates',slRate*ones(1,4),...
    'OutportNames',{'diagValidIn','nonDiagValidIn'},...
    'OutportTypes',[LTDataValidInOutSigs(1).Type,LTDataValidInOutSigs(2).Type]);

    hLTDataValidInN.setTargetCompReplacementCandidate(true);


    for ii=1:numel(hLTDataValidInN.PirOutputSignals)
        hLTDataValidInN.PirOutputSignals(ii).SimulinkRate=slRate;
    end

    hLTDataValidInNinSigs=hLTDataValidInN.PirInputSignals;
    hLTDataValidInNoutSigs=hLTDataValidInN.PirOutputSignals;


    lowerTriangEnb=hLTDataValidInNinSigs(1);
    readEnable=hLTDataValidInNinSigs(2);
    rowCount=hLTDataValidInNinSigs(3);
    colCount=hLTDataValidInNinSigs(4);

    diagValidIn=hLTDataValidInNoutSigs(1);
    nonDiagValidIn=hLTDataValidInNoutSigs(2);


    hBoolT=pir_boolean_t;

    LogicalOperator_out1_s6=l_addSignal(hLTDataValidInN,sprintf('Logical\nOperator_out1'),hBoolT,slRate);
    LogicalOperator1_out1_s7=l_addSignal(hLTDataValidInN,sprintf('Logical\nOperator1_out1'),hBoolT,slRate);
    RelationalOperator_out1_s8=l_addSignal(hLTDataValidInN,sprintf('Relational\nOperator_out1'),hBoolT,slRate);
    RelationalOperator1_out1_s9=l_addSignal(hLTDataValidInN,sprintf('Relational\nOperator1_out1'),hBoolT,slRate);


    pirelab.getIntDelayComp(hLTDataValidInN,...
    LogicalOperator_out1_s6,...
    diagValidIn,...
    1,'Delay',...
    false,...
    0,0,[],0,0);


    pirelab.getIntDelayComp(hLTDataValidInN,...
    LogicalOperator1_out1_s7,...
    nonDiagValidIn,...
    1,'Delay1',...
    false,...
    0,0,[],0,0);

    pirelab.getLogicComp(hLTDataValidInN,...
    [lowerTriangEnb,readEnable,RelationalOperator_out1_s8],...
    LogicalOperator_out1_s6,...
    'and',sprintf('Logical\nOperator'));

    pirelab.getLogicComp(hLTDataValidInN,...
    [lowerTriangEnb,readEnable,RelationalOperator1_out1_s9],...
    LogicalOperator1_out1_s7,...
    'and',sprintf('Logical\nOperator1'));

    pirelab.getRelOpComp(hLTDataValidInN,...
    [rowCount,colCount],...
    RelationalOperator_out1_s8,...
    '==',0,sprintf('Relational\nOperator'));

    pirelab.getRelOpComp(hLTDataValidInN,...
    [rowCount,colCount],...
    RelationalOperator1_out1_s9,...
    '>',0,sprintf('Relational\nOperator1'));



    pirelab.instantiateNetwork(hN,hLTDataValidInN,LTDataValidInInSigs,LTDataValidInOutSigs,...
    [hLTDataValidInN.Name,'_inst']);


end


function hS=l_addSignal(hN,sigName,pirTyp,simulinkRate)
    hS=hN.addSignal;
    hS.Name=sigName;
    hS.Type=pirTyp;
    hS.SimulinkHandle=-1;
    hS.SimulinkRate=simulinkRate;
end
