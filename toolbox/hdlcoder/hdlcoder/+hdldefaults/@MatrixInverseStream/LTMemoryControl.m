
function LTMemoryControl(this,hN,LTMemoryControlInSigs,LTMemoryControlOutSigs,...
    hBoolT,hAddrT,hCounterT,hInputDataT,slRate,blockInfo)



    hLTMemoryControlN=pirelab.createNewNetwork(...
    'Name','LTMemoryControl',...
    'InportNames',{'lowerTriangEnb','colCount','rowCount','writeCount','diagDataOut',...
    'diagValidOut','nonDiagValidOut','nonDiagDataOut','reciprocalValid',...
    'reciprocalData'},...
    'InportTypes',[hBoolT,hCounterT,hCounterT,hCounterT,hInputDataT,hBoolT,hBoolT,hInputDataT,...
    hBoolT,hInputDataT],...
    'InportRates',slRate*ones(1,10),...
    'OutportNames',{'wrEnbLT','wrAddrLT','wrDataLT','rdAddrLT'},...
    'OutportTypes',[pirelab.createPirArrayType(hBoolT,[blockInfo.RowSize+1,0]),...
    pirelab.createPirArrayType(hAddrT,[blockInfo.RowSize+1,0]),...
    pirelab.createPirArrayType(hInputDataT,[blockInfo.RowSize+1,0]),...
    pirelab.createPirArrayType(hAddrT,[blockInfo.RowSize+1,0])]);

    hLTMemoryControlN.setTargetCompReplacementCandidate(true);


    for ii=1:numel(hLTMemoryControlN.PirOutputSignals)
        hLTMemoryControlN.PirOutputSignals(ii).SimulinkRate=slRate;
    end

    hLTMemoryControlNinSigs=hLTMemoryControlN.PirInputSignals;
    hLTMemoryControlNoutSigs=hLTMemoryControlN.PirOutputSignals;


    WrEnbLTInSigs=[hLTMemoryControlNinSigs(1),hLTMemoryControlNinSigs(6),...
    hLTMemoryControlNinSigs(7),hLTMemoryControlNinSigs(3),...
    hLTMemoryControlNinSigs(9)];
    WrEnbLTOutSigs=hLTMemoryControlNoutSigs(1);

    this.WrEnbLowerTriang(hLTMemoryControlN,WrEnbLTInSigs,WrEnbLTOutSigs,...
    hBoolT,hCounterT,slRate,blockInfo);


    WrAddrLTInSigs=[hLTMemoryControlNinSigs(3),hLTMemoryControlNinSigs(1),...
    hLTMemoryControlNinSigs(4)];
    WrAddrLTOutSigs=hLTMemoryControlNoutSigs(2);


    this.WrAddrLowerTriang(hLTMemoryControlN,WrAddrLTInSigs,WrAddrLTOutSigs,...
    hBoolT,hCounterT,hAddrT,slRate,blockInfo);

    WrDataLTInSigs=[hLTMemoryControlNinSigs(6),hLTMemoryControlNinSigs(7),...
    hLTMemoryControlNinSigs(5),hLTMemoryControlNinSigs(8),...
    hLTMemoryControlNinSigs(10)];
    WrDataLTOutSigs=hLTMemoryControlNoutSigs(3);

    this.WrDataLowerTriang(hLTMemoryControlN,WrDataLTInSigs,WrDataLTOutSigs,...
    hBoolT,hInputDataT,slRate,blockInfo);


    RdAddrLTInSigs=[hLTMemoryControlNinSigs(1),hLTMemoryControlNinSigs(2)];
    RdAddrLTOutSigs=hLTMemoryControlNoutSigs(4);

    this.RdAddrLowerTriang(hLTMemoryControlN,RdAddrLTInSigs,RdAddrLTOutSigs,hBoolT,hCounterT,...
    hAddrT,slRate,blockInfo);


    pirelab.instantiateNetwork(hN,hLTMemoryControlN,LTMemoryControlInSigs,LTMemoryControlOutSigs,...
    [hLTMemoryControlN.Name,'_inst']);

end


