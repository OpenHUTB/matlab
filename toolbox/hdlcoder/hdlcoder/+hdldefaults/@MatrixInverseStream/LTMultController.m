

function LTMultController(this,hN,LTMultControlInSigs,LTMultControlOutSigs,...
    hBoolT,hCounterT,slRate,blockInfo)


    hLTMultControlN=pirelab.createNewNetwork(...
    'Name','LTMultController',...
    'InportNames',{'fwdSubDone','invDone'},...
    'InportTypes',[hBoolT,hBoolT],...
    'InportRates',slRate*ones(1,2),...
    'OutportNames',{'colCount','matMultRdEnb','rowCountReg',...
    'colCountReg','matMultEnbReg'},...
    'OutportTypes',[hCounterT,hBoolT,hCounterT,hCounterT,hBoolT]);

    hLTMultControlN.setTargetCompReplacementCandidate(true);


    for ii=1:numel(hLTMultControlN.PirOutputSignals)
        hLTMultControlN.PirOutputSignals(ii).SimulinkRate=slRate;
    end

    hLTMultControlNinSigs=hLTMultControlN.PirInputSignals;
    hLTMultControlNoutSigs=hLTMultControlN.PirOutputSignals;


    matMultRdDoneS=l_addSignal(hLTMultControlN,'matMultRdDone',hBoolT,slRate);
    rowCountEnS=l_addSignal(hLTMultControlN,'rowCountEn',hBoolT,slRate);

    Constant_out1_s1=l_addSignal(hLTMultControlN,'Constant_out1',hBoolT,slRate);
    Constant1_out1_s2=l_addSignal(hLTMultControlN,'Constant1_out1',hBoolT,slRate);
    Switch_out1_s10=l_addSignal(hLTMultControlN,'Switch_out1',hBoolT,slRate);
    Switch9_out1_s11=l_addSignal(hLTMultControlN,'Switch9_out1',hBoolT,slRate);
    rowCounter_out1_s12=l_addSignal(hLTMultControlN,'rowCounter_out1',hCounterT,slRate);


    pirelab.getConstComp(hLTMultControlN,...
    Constant_out1_s1,...
    1,...
    'Constant','on',0,'','','');

    pirelab.getConstComp(hLTMultControlN,...
    Constant1_out1_s2,...
    0,...
    'Constant1','on',1,'','','');

    pirelab.getIntDelayComp(hLTMultControlN,...
    Switch_out1_s10,...
    hLTMultControlNoutSigs(2),...
    1,'Delay',...
    false,...
    0,0,[],0,0);

    pirelab.getIntDelayComp(hLTMultControlN,...
    rowCounter_out1_s12,...
    hLTMultControlNoutSigs(3),...
    1,'Delay1',...
    0,...
    0,0,[],0,0);

    pirelab.getIntDelayComp(hLTMultControlN,...
    hLTMultControlNoutSigs(1),...
    hLTMultControlNoutSigs(4),...
    1,'Delay2',...
    0,...
    0,0,[],0,0);

    pirelab.getIntDelayComp(hLTMultControlN,...
    hLTMultControlNoutSigs(2),...
    hLTMultControlNoutSigs(5),...
    1,'Delay3',...
    false,...
    0,0,[],0,0);

    pirelab.getCounterComp(hLTMultControlN,...
    [hLTMultControlNinSigs(2),rowCountEnS],...
    rowCounter_out1_s12,...
    'Count limited',1,1,blockInfo.RowSize,1,0,1,0,'rowCounter',1);

    pirelab.getSwitchComp(hLTMultControlN,...
    [Constant_out1_s1,Switch9_out1_s11],...
    Switch_out1_s10,...
    hLTMultControlNinSigs(1),'Switch',...
    '~=',0,'Floor','Wrap');

    pirelab.getSwitchComp(hLTMultControlN,...
    [Constant1_out1_s2,hLTMultControlNoutSigs(2)],...
    Switch9_out1_s11,...
    matMultRdDoneS,'Switch9',...
    '~=',0,'Floor','Wrap');


    LTMultCntInSigs=[hLTMultControlNoutSigs(2),rowCounter_out1_s12];
    LTMultCntOutSigs=[matMultRdDoneS,hLTMultControlNoutSigs(1),rowCountEnS];

    this.LTMultCounters(hLTMultControlN,LTMultCntInSigs,LTMultCntOutSigs,hBoolT,...
    hCounterT,slRate,blockInfo);




    pirelab.instantiateNetwork(hN,hLTMultControlN,LTMultControlInSigs,LTMultControlOutSigs,...
    [hLTMultControlN.Name,'_inst']);


end



function hS=l_addSignal(hN,sigName,pirTyp,simulinkRate)
    hS=hN.addSignal;
    hS.Name=sigName;
    hS.Type=pirTyp;
    hS.SimulinkHandle=-1;
    hS.SimulinkRate=simulinkRate;
end
