
function swappingDataLogic(~,hN,swappingDataLogicInSigs,swappingDataLogicOutSigs,...
    hCounterT,hInputDataT,hBoolT,slRate,blockInfo)





    hswapDataLogicN=pirelab.createNewNetwork(...
    'Name','swappingDataLogic',...
    'InportNames',{'swapEnb','diagRowData','swapDone','diagRowDataEyeMem','maxRowDataEyeMem',...
    'diagRowAddr','maxFinish','maxRowData','maxRowAddr'},...
    'InportTypes',[hBoolT,swappingDataLogicInSigs(2).Type,hBoolT,swappingDataLogicInSigs(4).Type,...
    swappingDataLogicInSigs(5).Type,hCounterT,hBoolT,swappingDataLogicInSigs(8).Type,hCounterT],...
    'InportRates',slRate*ones(1,9),...
    'OutportNames',{'swapEnableOut','swapDataOut1','swapAddrOut','swapDataOut2'},...
    'OutportTypes',[swappingDataLogicOutSigs(1).Type,swappingDataLogicOutSigs(2).Type,...
    swappingDataLogicOutSigs(3).Type,swappingDataLogicOutSigs(4).Type]);

    hswapDataLogicN.setTargetCompReplacementCandidate(true);


    for ii=1:numel(hswapDataLogicN.PirOutputSignals)
        hswapDataLogicN.PirOutputSignals(ii).SimulinkRate=slRate;
    end

    hswapDataLogicNinSigs=hswapDataLogicN.PirInputSignals;
    hswapDataLogicNoutSigs=hswapDataLogicN.PirOutputSignals;

    swapEnb=hswapDataLogicNinSigs(1);
    diagRowData=hswapDataLogicNinSigs(2);
    swapDone=hswapDataLogicNinSigs(3);
    diagRowDataEyeMem=hswapDataLogicNinSigs(4);
    maxRowDataEyeMem=hswapDataLogicNinSigs(5);
    diagRowAddr=hswapDataLogicNinSigs(6);
    maxFinish=hswapDataLogicNinSigs(7);
    maxRowData=hswapDataLogicNinSigs(8);
    maxRowAddr=hswapDataLogicNinSigs(9);

    swapEnableOut=hswapDataLogicNoutSigs(1);
    swapDataOut1=hswapDataLogicNoutSigs(2);
    swapAddrOut=hswapDataLogicNoutSigs(3);
    swapDataOut2=hswapDataLogicNoutSigs(4);


    pirTyp1=pir_boolean_t;
    pirTyp2=hInputDataT;
    pirTyp3=hCounterT;

    if blockInfo.MatrixSize==1
        hArrayT=pirTyp2;
    else
        hArrayT=pirelab.createPirArrayType(pirTyp2,[blockInfo.MatrixSize,0]);
    end




    Constant2_out1_s9=l_addSignal(hswapDataLogicN,'Constant2_out1',pirTyp1,slRate);
    Constant3_out1_s10=l_addSignal(hswapDataLogicN,'Constant3_out1',pirTyp1,slRate);
    Delay5_out1_s12=l_addSignal(hswapDataLogicN,'Delay5_out1',pirTyp1,slRate);
    LogicalOperator7_out1_s16=l_addSignal(hswapDataLogicN,sprintf('Logical\nOperator7_out1'),pirTyp1,slRate);
    Switch12_out1_s17=l_addSignal(hswapDataLogicN,'Switch12_out1',pirTyp1,slRate);
    Switch13_out1_s18=l_addSignal(hswapDataLogicN,'Switch13_out1',pirTyp1,slRate);
    Switch18_out1_s19=l_addSignal(hswapDataLogicN,'Switch18_out1',hArrayT,slRate);
    Switch19_out1_s20=l_addSignal(hswapDataLogicN,'Switch19_out1',hArrayT,slRate);
    Switch5_out1_s21=l_addSignal(hswapDataLogicN,'Switch5_out1',hArrayT,slRate);
    Switch6_out1_s22=l_addSignal(hswapDataLogicN,'Switch6_out1',hArrayT,slRate);
    Switch7_out1_s23=l_addSignal(hswapDataLogicN,'Switch7_out1',pirTyp3,slRate);
    Switch8_out1_s24=l_addSignal(hswapDataLogicN,'Switch8_out1',pirTyp3,slRate);


    pirelab.getConstComp(hswapDataLogicN,...
    Constant2_out1_s9,...
    0,...
    'Constant2','on',1,'','','');



    pirelab.getConstComp(hswapDataLogicN,...
    Constant3_out1_s10,...
    1,...
    'Constant3','on',0,'','','');


    pirelab.getIntDelayComp(hswapDataLogicN,...
    Switch18_out1_s19,...
    swapDataOut2,...
    1,'Delay19',...
    single(0),...
    0,0,[],0,0);



    pirelab.getIntDelayComp(hswapDataLogicN,...
    maxFinish,...
    Delay5_out1_s12,...
    1,'Delay5',...
    false,...
    0,0,[],0,0);



    pirelab.getIntDelayComp(hswapDataLogicN,...
    Switch5_out1_s21,...
    swapDataOut1,...
    1,'Delay6',...
    single(0),...
    0,0,[],0,0);


    pirelab.getIntDelayComp(hswapDataLogicN,...
    Switch8_out1_s24,...
    swapAddrOut,...
    1,'Delay7',...
    0,...
    0,0,[],0,0);



    pirelab.getIntDelayComp(hswapDataLogicN,...
    Switch12_out1_s17,...
    swapEnableOut,...
    1,'Delay9',...
    false,...
    0,0,[],0,0);



    pirelab.getLogicComp(hswapDataLogicN,...
    [swapEnb,maxFinish],...
    LogicalOperator7_out1_s16,...
    'and',sprintf('Logical\nOperator7'));



    pirelab.getSwitchComp(hswapDataLogicN,...
    [Constant2_out1_s9,Switch13_out1_s18],...
    Switch12_out1_s17,...
    swapDone,'Switch12',...
    '~=',0,'Floor','Wrap');



    pirelab.getSwitchComp(hswapDataLogicN,...
    [Constant3_out1_s10,swapEnableOut],...
    Switch13_out1_s18,...
    LogicalOperator7_out1_s16,'Switch13',...
    '~=',0,'Floor','Wrap');



    pirelab.getSwitchComp(hswapDataLogicN,...
    [diagRowDataEyeMem,Switch19_out1_s20],...
    Switch18_out1_s19,...
    Delay5_out1_s12,'Switch18',...
    '~=',0,'Floor','Wrap');



    pirelab.getSwitchComp(hswapDataLogicN,...
    [maxRowDataEyeMem,swapDataOut2],...
    Switch19_out1_s20,...
    maxFinish,'Switch19',...
    '~=',0,'Floor','Wrap');



    pirelab.getSwitchComp(hswapDataLogicN,...
    [diagRowData,Switch6_out1_s22],...
    Switch5_out1_s21,...
    Delay5_out1_s12,'Switch5',...
    '~=',0,'Floor','Wrap');



    pirelab.getSwitchComp(hswapDataLogicN,...
    [maxRowData,swapDataOut1],...
    Switch6_out1_s22,...
    maxFinish,'Switch6',...
    '~=',0,'Floor','Wrap');



    pirelab.getSwitchComp(hswapDataLogicN,...
    [diagRowAddr,swapAddrOut],...
    Switch7_out1_s23,...
    maxFinish,'Switch7',...
    '~=',0,'Floor','Wrap');



    pirelab.getSwitchComp(hswapDataLogicN,...
    [maxRowAddr,Switch7_out1_s23],...
    Switch8_out1_s24,...
    Delay5_out1_s12,'Switch8',...
    '~=',0,'Floor','Wrap');






    pirelab.instantiateNetwork(hN,hswapDataLogicN,swappingDataLogicInSigs,...
    swappingDataLogicOutSigs,[hswapDataLogicN.Name,'_inst']);


end


function hS=l_addSignal(hN,sigName,pirTyp,simulinkRate)
    hS=hN.addSignal;
    hS.Name=sigName;
    hS.Type=pirTyp;
    hS.SimulinkHandle=-1;
    hS.SimulinkRate=simulinkRate;
end


