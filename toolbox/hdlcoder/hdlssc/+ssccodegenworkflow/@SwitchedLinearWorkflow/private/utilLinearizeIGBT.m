function utilLinearizeIGBT(hIGBTSystem,IGBTValues,hIGBT,hSPS,IGBTnum)



    Rs=IGBTValues.Rs;
    IGBTSystem=getfullname(hIGBTSystem);

    portConnectivity=get_param(hIGBT,'PortConnectivity');
    hLconn=portConnectivity(2).DstBlock;
    hRconn=portConnectivity(3).DstBlock;
    hIn1=get_param(get_param(get_param(portConnectivity(1).DstBlock,"portHandles").Inport,"Line"),"srcBlockHandle");

    set_param(hLconn,'Position',[195,178,225,192])
    set_param(hLconn,'Orientation','right')

    set_param(hRconn,'Position',[630,198,660,212])
    set_param(hIn1,'Position',[30,-97,60,-83])


    IGBTPorts=get_param(hIGBT,'porthandles');
    delete_line(get_param(IGBTPorts.LConn,'Line'));
    delete_line(get_param(IGBTPorts.RConn(1),'Line'));
    delete_line(get_param(IGBTPorts.RConn(2),'Line'));

    delete_block(hIGBT);

    spsPorts=get_param(hSPS,'PortHandles');
    delete_line(get_param(spsPorts.Inport,'Line'));

    delete_block(hSPS);






    hResistor=add_block('fl_lib/Electrical/Electrical Elements/Resistor',strcat(IGBTSystem,'/Rs'),...
    'MakeNameUnique','on',...
    'R',num2str(Rs,17),...
    'Position',[325,211,365,239]);
    hCurrentSource=add_block('fl_lib/Electrical/Electrical Sources/Controlled Current Source',strcat(IGBTSystem,'/Controlled Current Source'),...
    'MakeNameUnique','on',...
    'Orientation','right',...
    'Position',[325,165,365,205]);
    hCurrentSensor=add_block('fl_lib/Electrical/Electrical Sensors/Current Sensor',strcat(IGBTSystem,'/Current Sensor'),...
    'MakeNameUnique','on',...
    'Orientation','right',...
    'Position',[420,175,460,215]);
    hVoltageSensor=add_block('fl_lib/Electrical/Electrical Sensors/Voltage Sensor',strcat(IGBTSystem,'/Voltage Sensor'),...
    'MakeNameUnique','on',...
    'Orientation','right',...
    'Position',[325,100,365,140]);


    hCurrentSensorPSS=add_block('nesl_utility/PS-Simulink Converter',strcat(IGBTSystem,'/HDLLinIGBTCurrent',num2str(IGBTnum)),...
    'MakeNameUnique','on',...
    'Orientation','up',...
    'Position',[462,140,478,155]);
    hVoltageSensorPSS=add_block('nesl_utility/PS-Simulink Converter',strcat(IGBTSystem,'/HDLLinIGBTVoltage',num2str(IGBTnum)),...
    'MakeNameUnique','on',...
    'Orientation','up',...
    'Position',[-113,110,-97,125]);
    hCurrentSourceSPS=add_block('nesl_utility/Simulink-PS Converter',strcat(IGBTSystem,'/HDLLinIGBTJ',num2str(IGBTnum)),...
    'MakeNameUnique','on',...
    'Orientation','down',...
    'Position',[557,115,573,130]);

    hCurrentDelay=add_block('simulink/Discrete/Unit Delay',strcat(IGBTSystem,'/Unit Delay'),...
    'MakeNameUnique','on',...
    'Orientation','up',...
    'Position',[453,85,487,120]);
    hVoltageDelay=add_block('simulink/Discrete/Unit Delay',strcat(IGBTSystem,'/Unit Delay1'),...
    'MakeNameUnique','on',...
    'Position',[-90,58,-55,92]);
    hSDelay=add_block('simulink/Discrete/Unit Delay',strcat(IGBTSystem,'/Unit Delay2'),...
    'MakeNameUnique','on',...
    'Position',[360,-118,390,-82]);



    add_line(IGBTSystem,strcat(get_param(hLconn,'name'),'/Rconn1'),strcat(get_param(hResistor,'name'),'/Lconn1'),...
    'AutoRouting','on');
    add_line(IGBTSystem,strcat(get_param(hResistor,'name'),'/Rconn1'),strcat(get_param(hCurrentSensor,'name'),'/Lconn1'),...
    'AutoRouting','on');

    add_line(IGBTSystem,strcat(get_param(hLconn,'name'),'/Rconn1'),strcat(get_param(hCurrentSource,'name'),'/Lconn1'),...
    'AutoRouting','on');
    add_line(IGBTSystem,strcat(get_param(hCurrentSource,'name'),'/Rconn2'),strcat(get_param(hCurrentSensor,'name'),'/Lconn1'),...
    'AutoRouting','on');

    add_line(IGBTSystem,strcat(get_param(hLconn,'name'),'/Rconn1'),strcat(get_param(hVoltageSensor,'name'),'/Lconn1'),...
    'AutoRouting','on');
    add_line(IGBTSystem,strcat(get_param(hVoltageSensor,'name'),'/Rconn2'),strcat(get_param(hCurrentSensor,'name'),'/Lconn1'),...
    'AutoRouting','on');

    add_line(IGBTSystem,strcat(get_param(hCurrentSensor,'name'),'/Rconn2'),strcat(get_param(hRconn,'name'),'/Rconn1'),...
    'AutoRouting','on');



    utilDiodeLogic(strcat(get_param(hVoltageDelay,'name'),'/1'),...
    strcat(get_param(hCurrentDelay,'name'),'/1'),...
    strcat(get_param(hSDelay,'name'),'/1'),...
    strcat(get_param(hCurrentSourceSPS,'name'),'/1'),...
    strcat(get_param(hSDelay,'name'),'/1'),...
    IGBTSystem,IGBTValues,'double',...
    strcat(get_param(hIn1,'name'),'/1'));


    outPorts={strcat(get_param(hCurrentSensorPSS,'name'),'/Lconn1'),...
    strcat(get_param(hVoltageSensorPSS,'name'),'/Lconn1'),...
    strcat(get_param(hCurrentSourceSPS,'name'),'/Rconn1'),...
    strcat(get_param(hCurrentSensorPSS,'name'),'/1'),...
    strcat(get_param(hVoltageSensorPSS,'name'),'/1')};


    inPorts={strcat(get_param(hCurrentSensor,'name'),'/Rconn1'),...
    strcat(get_param(hVoltageSensor,'name'),'/Rconn1'),...
    strcat(get_param(hCurrentSource,'name'),'/Rconn1'),...
    strcat(get_param(hCurrentDelay,'name'),'/1'),...
    strcat(get_param(hVoltageDelay,'name'),'/1')};


    add_line(IGBTSystem,outPorts,inPorts,'AutoRouting','smart')

end


