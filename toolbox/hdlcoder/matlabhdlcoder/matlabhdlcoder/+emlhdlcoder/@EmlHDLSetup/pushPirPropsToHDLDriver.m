function pushPirPropsToHDLDriver(hdlDrv)





    hPir=pir;
    params={'clockname','resetname','clockenablename','clockenableoutputname','enableprefix'};
    cellfun(@hdlsetparameter,params,cellfun(@hPir.getParamValue,params,'UniformOutput',false));


    uniquifyClockParams(hdlDrv);




    cpObj=hdlDrv.getCPObj;
    hINI=cpObj.INI;
    fNames=fieldnames(struct(hINI));
    for ii=1:length(fNames)
        param=fNames{ii};
        if hPir.hasParam(param)&&~isempty(findProp(hINI,param))
            hdlDrv.setParameter(param,hPir.getParamValue(param));
        end
    end




    mver=ver('matlab');
    cver=ver('matlabcoder');
    hver=ver('hdlcoder');
    tfc=sprintf('Generated by %s %s, %s %s and %s %s\n',mver.Name,mver.Version,cver.Name,cver.Version,hver.Name,hver.Version);

    prms.tool_file_comment=hdlformatcomment(tfc);
    prms.rcs_cvs_tag=hdlformatcomment(hdlDrv.getParameter('rcs_cvs_tag'));
    hPir.initParams(prms);
    hdlDrv.setParameter('tool_file_comment',prms.tool_file_comment);
    hdlDrv.setParameter('rcs_cvs_tag',prms.rcs_cvs_tag);
end