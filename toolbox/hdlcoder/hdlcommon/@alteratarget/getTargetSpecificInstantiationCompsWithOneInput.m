function[hC,num]=getTargetSpecificInstantiationCompsWithOneInput(targetCompInventory,hN,hInSignals,hOutSignals,altMegaFunctionName)




    [dimlen,~]=pirelab.getVectorTypeInfo(hInSignals(1));
    if dimlen>1
        hC=alteratarget.getVectorMegaFunctionComp(targetCompInventory,hN,hInSignals,hOutSignals,altMegaFunctionName,@getScalarTargetSpecificMegafunctionCompWithOneInput);
    else
        hC=getScalarTargetSpecificMegafunctionCompWithOneInput(targetCompInventory,hN,hInSignals,hOutSignals,altMegaFunctionName);
    end
    num=dimlen*hN.getNumOfInstances();

    function hC=getScalarTargetSpecificMegafunctionCompWithOneInput(~,hN,hInSignals,hOutSignals,altMegaFunctionName)

