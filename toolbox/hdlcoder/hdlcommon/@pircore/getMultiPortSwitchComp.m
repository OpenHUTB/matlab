function mpsComp=getMultiPortSwitchComp(hN,hInSignals,hOutSignals,inputmode,...
    dataPortOrder,rndMode,satMode,compName,portSel,dpForDefault,diagForDefaultErr,codingStyle)


    mpsComp=hN.addComponent2(...
    'kind','multiportswitch_comp',...
    'name',compName,...
    'InputSignals',hInSignals,...
    'OutputSignals',hOutSignals,...
    'InputMode',inputmode,...
    'DataPortOrder',dataPortOrder,...
    'PortIndices',portSel,...
    'DataPortForDefault',dpForDefault,...
    'RoundingMode',rndMode,...
    'OverflowMode',satMode,...
    'DiagnosticForDefaultError',diagForDefaultErr,...
    'CodingStyle',codingStyle);
end
