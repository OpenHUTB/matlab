function assertionComp=getAssertionComp(hN,hInSignals,compName,label,enabled,assertFailFcn,stopSimulation)


    if(strcmp(label,''))
        label='AssertionFailed';
    end

    assertionComp=hN.addComponent2(...
    'kind','assertion_comp',...
    'name',compName,...
    'InputSignals',hInSignals,...
    'Label',label,...
    'Enabled',enabled,...
    'AssertionFailFcn',assertFailFcn,...
    'StopWhenAssertionFail',stopSimulation);
    assertionComp.setPreserve(true);
end
