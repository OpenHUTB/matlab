function recipComp=getReciprocalComp(hN,hInSignals,hOutSignals,newtonInfo,slbh,nfpOptions)


    narginchk(6,6);
    recipComp=hN.addComponent2(...
    'kind','recip_comp',...
    'name',newtonInfo.networkName,...
    'SLBlockHandle',slbh,...
    'InputSignals',hInSignals,...
    'OutputSignals',hOutSignals,...
    'RoundingMode',newtonInfo.rndMode,...
    'OverflowMode',newtonInfo.satMode,...
    'IterNum',newtonInfo.iterNum,...
    'IntermDT',newtonInfo.intermDT,...
    'InternalRule',newtonInfo.internalRule,...
    'IsMultirate',newtonInfo.isMultirate,...
    'UpFactor',newtonInfo.upFactor,...
    'IsRsqrtBased',newtonInfo.isRsqrtBased,...
    'NFPLatency',nfpOptions.Latency,...
    'NFPDenormals',nfpOptions.Denormals,...
    'NFPRadix',nfpOptions.Radix);
end
