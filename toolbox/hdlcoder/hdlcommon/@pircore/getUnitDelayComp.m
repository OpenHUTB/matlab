function delayComp=getUnitDelayComp(hN,hInSignals,hOutSignals,compName,initVal,resetnone,desc,slHandle)



    [isReset,~,initVal,preserveInitValDimensions]=...
    pircore.processDelayIC(initVal);


    delayComp=hN.addComponent2(...
    'kind','UnitDelay',...
    'SimulinkHandle',slHandle,...
    'name',compName,...
    'InputSignals',hInSignals,...
    'OutputSignals',hOutSignals,...
    'InitialValue',initVal,...
    'ResetNone',resetnone,...
    'ResetInitVal',isReset,...
    'PreserveInitValDimensions',preserveInitValDimensions,...
    'BlockComment',desc);

end
