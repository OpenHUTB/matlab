function trigComp=getTrigonometricComp(hN,hInSignals,hOutSignals,compName,slbh,fname,nfpOptions)


    if nargin<7
        nfpOptions.Latency=int8(0);
        nfpOptions.MantMul=int8(0);
        nfpOptions.Denormals=int8(0);
        nfpOptions.ArgReduction=true;
    end

    trigComp=hN.addComponent2(...
    'kind','trig_comp',...
    'SimulinkHandle',slbh,...
    'name',compName,...
    'InputSignals',hInSignals,...
    'OutputSignals',hOutSignals,...
    'FunctionName',fname,...
    'NFPLatency',nfpOptions.Latency,...
    'NFPDenormals',nfpOptions.Denormals,...
    'NFPMantMul',nfpOptions.MantMul,...
    'NFPArgReduction',nfpOptions.ArgReduction);
    trigComp.setSupportAlteraMegaFunctions(true);
end
