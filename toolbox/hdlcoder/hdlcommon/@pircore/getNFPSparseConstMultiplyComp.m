function coreComp=getNFPSparseConstMultiplyComp(hN,hInSignals,hOutSignals,...
    constMatrixSize,constMatrix,latency,sharingFactor,fpDelays,nfpOptions,name)




    if(nargin<8)
        name='nfpsparseconstmultiply';
    end

    coreComp=hN.addComponent2(...
    'kind','nfpsparseconstmultiply_comp',...
    'Name',name,...
    'InputSignals',hInSignals,...
    'OutputSignals',hOutSignals,...
    'PipelineDelay',latency,...
    'SharingFactor',sharingFactor,...
    'FPDelays',fpDelays,...
    'NFPLatency',nfpOptions.Latency,...
    'NFPCustomLatency',nfpOptions.CustomLatency,...
    'ConstMatrixSize',constMatrixSize,...
    'ConstMatrix',constMatrix);

end