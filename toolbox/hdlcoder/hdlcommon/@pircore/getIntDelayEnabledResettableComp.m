function delayComp=getIntDelayEnabledResettableComp(hN,hInSignals,hOutSignals,delayNumber,compName,ic,resetType,hEnbSignals,hRstSignals,desc,slHandle)



    [isReset,initValScalarExpandable,ic]=...
    pircore.processDelayIC(ic);

    delayComp=hN.addComponent2(...
    'kind','integerdelayenabledresettable',...
    'SimulinkHandle',slHandle,...
    'name',compName,...
    'InputSignals',hInSignals,...
    'OutputSignals',hOutSignals,...
    'ExternalEnableSignal',hEnbSignals,...
    'ExternalSynchronousResetSignal',hRstSignals,...
    'InitialValue',ic,...
    'ResetNone',resetType,...
    'RAMBased',false,...
    'NumDelays',delayNumber,...
    'ResetInitVal',isReset,...
    'InitValScalarExpandable',initValScalarExpandable,...
    'BlockComment',desc);
end
