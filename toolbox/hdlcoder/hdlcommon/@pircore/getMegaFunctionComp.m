function megafunctionComp=getMegaFunctionComp(hN,name,inputSignals,outputSignals,...
    entityName,inportNames,outportNames,clkNames,ceNames,ceclrNames,latency,...
    slbh)



    assert(nargin>=11,'No enough arguments are given to create Dspba comp');

    if nargin<12
        slbh=-1;
    end

    megafunctionComp=hN.addComponent2(...
    'kind','megafunction_comp',...
    'SimulinkHandle',slbh,...
    'name',name,...
    'InputSignals',inputSignals,...
    'OutputSignals',outputSignals,...
    'EntityName',entityName,...
    'InportNames',inportNames,...
    'OutportNames',outportNames,...
    'ClkNames',clkNames,...
    'CeNames',ceNames,...
    'ResetNames',ceclrNames,...
    'Latency',latency...
    );

end


