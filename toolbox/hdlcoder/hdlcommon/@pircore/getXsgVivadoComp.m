function xsgComp=getXsgVivadoComp(hN,name,inputSignals,outputSignals,...
    entityName,inportNames,outportNames,clkNames,ceNames,ceclrNames,...
    rates,baseRate,hasDownSample,blackBoxAttributes,vhdlComponentLibrary,...
    slbh)



    assert(nargin>=12,'No enough arguments are given to create Xsg comp');

    if nargin<15
        slbh=-1;
    end

    if nargin<14
        vhdlComponentLibrary='';
    end

    if nargin<13
        blackBoxAttributes=false;
    end

    xsgComp=hN.addComponent2(...
    'kind','xsg_vivado_comp',...
    'SimulinkHandle',slbh,...
    'name',name,...
    'InputSignals',inputSignals,...
    'OutputSignals',outputSignals,...
    'EntityName',entityName,...
    'InportNames',inportNames,...
    'OutportNames',outportNames,...
    'ClkNames',clkNames,...
    'CeNames',ceNames,...
    'ResetNames',ceclrNames,...
    'Rates',rates,...
    'BaseRate',baseRate,...
    'HasDownSample',hasDownSample,...
    'BlackBoxAttributes',blackBoxAttributes,...
    'VhdlComponentLibrary',vhdlComponentLibrary);

end
