function delayComp=getTapDelayComp(hN,hInSignals,...
    hOutSignals,delayNumber,...
    compName,ic,delayorder,includeCurrent,...
    resettype,desc,slbh)



    [isReset,initValScalarExpandable,ic]=pircore.processDelayIC(ic);


    delayComp=hN.addComponent2(...
    'kind','TappedDelay',...
    'SimulinkHandle',slbh,...
    'name',compName,...
    'InputSignals',hInSignals,...
    'OutputSignals',hOutSignals,...
    'InitialValue',ic,...
    'NumDelays',delayNumber,...
    'DelayOrder',delayorder,...
    'IncludeCurrent',includeCurrent,...
    'ResetNone',resettype,...
    'ResetInitVal',isReset,...
    'InitValScalarExpandable',initValScalarExpandable,...
    'BlockComment',desc);

end
