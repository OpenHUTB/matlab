function cgirComp=getPreLookupComp(hN,hInSignals,hOutSignals,...
    bp_data,bpType,kType,fType,idxOnly,powerof2,compName,slbh,diagnostics)









    if(nargin<10)
        compName='PreLookup';
    end

    cgirComp=hN.addComponent2(...
    'kind','prelookuptable_comp',...
    'Name',compName,...
    'InputSignals',hInSignals,...
    'OutputSignals',hOutSignals,...
    'SLBlockHandle',slbh,...
    'BpData',bp_data,...
    'BpType',bpType,...
    'KType',kType,...
    'PowerOf2',powerof2,...
    'FractionType',fType,...
    'IdxOnly',idxOnly,...
    'Diagnostics',diagnostics);

end
