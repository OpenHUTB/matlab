function delayComp=getUnitDelayEnabledResettableComp(hN,hInSignals,...
    hOutSignals,hEnbSignal,hRstSignal,compName,ic,resettype,softreset,desc,slbh,isSynchronousDelay)



    isReset=pircore.processDelayIC(ic);

    delayComp=hN.addComponent2(...
    'kind','UnitDelayEnabledResettable',...
    'SimulinkHandle',slbh,...
    'name',compName,...
    'InputSignals',hInSignals,...
    'OutputSignals',hOutSignals,...
    'ExternalEnableSignal',hEnbSignal,...
    'ExternalSynchronousResetSignal',hRstSignal,...
    'InitialValue',ic,...
    'ResetNone',resettype,...
    'ResetInitVal',isReset,...
    'Softreset',softreset,...
    'BlockComment',desc,...
    'IsSynchronousDelay',isSynchronousDelay);

end
