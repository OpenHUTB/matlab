function counterComp=getCounterComp(hN,hInSignals,hOutSignals,...
    type,initval,stepval,maxval,resetport,loadport,enbport,dirport,compName,countFrom)




    if(nargin<13)
        countFrom=initval;
    end

    if(nargin<12)
        compName='counter';
    end

    if isempty(initval)
        isReset=true;
    else
        isReset=(all(initval)==0);
    end

    if length(hOutSignals)==1
        isCountHitPortSelected=false;
    else
        isCountHitPortSelected=true;
    end

    counterComp=hN.addComponent2(...
    'kind','hdlcounter_comp',...
    'Name',compName,...
    'InputSignals',hInSignals,...
    'OutputSignals',hOutSignals,...
    'CountType',type,...
    'CountInit',initval,...
    'ICReset',isReset,...
    'CountStep',stepval,...
    'CountMax',maxval,...
    'CountFrom',countFrom,...
    'ResetPort',resetport,...
    'LoadPort',loadport,...
    'EnablePort',enbport,...
    'DirectionPort',dirport,...
    'CountHitOutputPort',isCountHitPortSelected);

end
