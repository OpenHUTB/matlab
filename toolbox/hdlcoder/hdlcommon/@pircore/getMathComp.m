function mathComp=getMathComp(hN,hInSignals,hOutSignals,compName,slbh,fname,nfpOptions)


    narginchk(7,7);


    if~isfield(nfpOptions,'CustomLatency')
        nfpOptions.CustomLatency=int8(0);
    end

    mathComp=hN.addComponent2(...
    'kind','math_comp',...
    'SimulinkHandle',slbh,...
    'name',compName,...
    'InputSignals',hInSignals,...
    'OutputSignals',hOutSignals,...
    'FunctionName',fname,...
    'NFPLatency',nfpOptions.Latency,...
    'NFPCustomLatency',nfpOptions.CustomLatency,...
    'NFPDenormals',nfpOptions.Denormals,...
    'NFPModRemCheckResetToZero',nfpOptions.ModRemCheckResetToZero,...
    'NFPModRemMaxIterations',nfpOptions.ModRemMaxIterations,...
    'NFPRadix',nfpOptions.Radix);
    mathComp.setSupportAlteraMegaFunctions(true);
end
