function dtcComp=getDTCComp(hN,hInSignals,hOutSignals,...
    rndMode,satMode,conversionMode,compName,desc,slbh,nfpOptions)


    if nargin<10
        nfpOptions.Latency=int8(0);
        nfpOptions.MantMul=int8(0);
        nfpOptions.Denormals=int8(0);
    end

    if~isfield(nfpOptions,'CustomLatency')
        nfpOptions.CustomLatency=int8(0);
    end

    dtcComp=hN.addComponent2(...
    'kind','DataTypeConversion',...
    'SimulinkHandle',slbh,...
    'name',compName,...
    'InputSignals',hInSignals,...
    'OutputSignals',hOutSignals,...
    'RoundingMode',rndMode,...
    'OverflowMode',satMode,...
    'ConversionMode',conversionMode,...
    'BlockComment',desc,...
    'NFPLatency',nfpOptions.Latency,...
    'NFPCustomLatency',nfpOptions.CustomLatency);
    dtcComp.setSupportAlteraMegaFunctions(true);
    dtcComp.setSupportXilinxCoreGen(true);
end
