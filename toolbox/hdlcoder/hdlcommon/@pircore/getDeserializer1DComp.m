function hNewC=getDeserializer1DComp(hN,hInSignals,hOutSignals,ratio,idleCycles,initVal,startInPort,validInPort,validOutPort,compName)




    if(nargin<9)
        compName='Deserializer1D';
    end

    isReset=pircore.processDelayIC(initVal);

    hNewC=hN.addComponent2(...
    'kind','deserializer1d_comp',...
    'Name',compName,...
    'InputSignals',hInSignals,...
    'OutputSignals',hOutSignals,...
    'Ratio',ratio,...
    'IdleCycles',idleCycles,...
    'InitialValue',initVal,...
    'StartInPort',startInPort,...
    'ValidInPort',validInPort,...
    'ValidOutPort',validOutPort,...
    'ResetInitVal',isReset);

end
