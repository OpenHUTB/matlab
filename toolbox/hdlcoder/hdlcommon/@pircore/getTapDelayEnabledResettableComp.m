function delayComp=getTapDelayEnabledResettableComp(hN,hInSignals,...
    hOutSignals,delayNumber,...
    compName,ic,delayorder,includeCurrent,...
    resettype,hEnbSignals,hRstSignals,desc,slbh)


    isReset=pircore.processDelayIC(ic);

    delayComp=hN.addComponent2(...
    'kind','TappedDelayEnabledResettable',...
    'SimulinkHandle',slbh,...
    'name',compName,...
    'InputSignals',hInSignals,...
    'OutputSignals',hOutSignals,...
    'ExternalEnableSignal',hEnbSignals,...
    'ExternalSynchronousResetSignal',hRstSignals,...
    'InitialValue',ic,...
    'ResetNone',resettype,...
    'DelayOrder',delayorder,...
    'IncludeCurrent',includeCurrent,...
    'NumDelays',delayNumber,...
    'ResetInitVal',isReset,...
    'BlockComment',desc);
end
