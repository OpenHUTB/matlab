function checkAbs(this,callee,node)




    arg=node.Right;
    type=this.getType(arg);


    if type.isComplex()&&~type.isSingle()
        unsupportedType=type.getMLName();

        this.addMessage(arg,...
        internal.mtree.MessageType.Error,...
        'hdlcommon:matlab2dataflow:UnsupportedFunctionCallWithComplexType',...
        callee,...
        unsupportedType);
    end



    if type.isHalf()
        this.addMessage(arg,...
        internal.mtree.MessageType.Error,...
        'hdlcommon:matlab2dataflow:UnsupportedOperationWithHalfTypes',...
        callee);
    elseif type.isLogical()
        this.addMessage(arg,...
        internal.mtree.MessageType.Error,...
        'hdlcommon:matlab2dataflow:UnsupportedFunctionCallWithType',...
        callee,...
        type.getMLName);
    end
end
