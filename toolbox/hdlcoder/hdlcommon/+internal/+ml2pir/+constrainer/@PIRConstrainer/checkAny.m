function checkAny(this,callee,node)




    arg=node.Right;
    if(arg.List.count>1)
        this.addMessage(arg,...
        internal.mtree.MessageType.Error,...
        'hdlcommon:matlab2dataflow:FunctionCallWithUnsupportedNumArgs',...
        node.tree2str,...
        callee,...
        num2str(arg.List.count),...
        '1');
    end
end
