function hTopN=getSingleFixComp(hN,latency,slRate)
    p=pir(hN.getCtxName);
    hTopN=addNetworks(p);
    pipestage=zeros(1,5);

    switch latency
    case 0
        pipestage=zeros(1,5);
    case 1
        pipestage=[0,0,0,1,0];
    case 2
        pipestage=[1,0,1,0,0];
    case 3
        pipestage=[1,1,0,0,1];
    case 4
        pipestage=[1,1,0,1,1];
    case 5
        pipestage=ones(1,5);
    otherwise
        assert(false,'Illegal latency number for rounding in fix mode.');
    end
    createNetworks(p,pipestage,latency,slRate);
end

function hTopN=addNetworks(p)
    hN=p.addNetwork;
    hN.Name='NFP_Mathworks_Fix_Single';
    hN.FullPath='Single_Fix_Scalar/NFP_Mathworks_Fix_Single';
    hTopN=hN;
    hN=p.addNetwork;
    hN.Name='initCycles';
    hN.FullPath='Single_Fix_Scalar/NFP_Mathworks_Fix_Single/initCycles';
    hN=p.addNetwork;
    hN.Name='InitCycles';
    hN.FullPath='Single_Fix_Scalar/NFP_Mathworks_Fix_Single/initCycles/InitCycles';
end

function createNetworks(p,pipestage,latency,slRate)
    hN_n2=p.findNetwork('fullname','Single_Fix_Scalar/NFP_Mathworks_Fix_Single/initCycles/InitCycles');
    createNetwork_n2(p,hN_n2,pipestage,latency,slRate);
    hN_n1=p.findNetwork('fullname','Single_Fix_Scalar/NFP_Mathworks_Fix_Single/initCycles');
    createNetwork_n1(p,hN_n1,slRate);
    hN_n0=p.findNetwork('fullname','Single_Fix_Scalar/NFP_Mathworks_Fix_Single');
    createNetwork_n0(p,hN_n0,pipestage,slRate);
end

