function createNetworks(p,pipestage,slRate1,denormal)
    if(denormal)
        hN_n20=p.findNetwork('fullname','SQRT_Half/sqrt_Long/sqrt_Normalize_Half/sqrt_Normalize_Denormals');
        createNetwork_n20_denorm(p,hN_n20,slRate1);
        hN_n20.addComment('handling denormalized signals');
    else
        hN_n20=p.findNetwork('fullname','SQRT_Half/sqrt_Long/sqrt_Normalize_Half/sqrt_Normalize_Normals');
        createNetwork_n20(p,hN_n20,slRate1);
    end
    hN_n19=p.findNetwork('fullname','SQRT_Half/sqrt_Long/sqrt_Normalize_Half');
    createNetwork_n19(p,hN_n19,denormal,slRate1);
    hN_n18=p.findNetwork('fullname','SQRT_Half/sqrt_Long/sqrt_Long_get_digit');
    createNetwork_n18(p,hN_n18,slRate1);
    hN_n15=p.findNetwork('fullname','SQRT_Half/sqrt_Long/Subsystem2');
    createNetwork_n15(p,hN_n15,pipestage,slRate1);
    hN_n10=p.findNetwork('fullname','SQRT_Half/sqrt_Long/Subsystem1');
    createNetwork_n10(p,hN_n10,pipestage,slRate1);
    hN_n5=p.findNetwork('fullname','SQRT_Half/sqrt_Long/Subsystem');
    createNetwork_n5(p,hN_n5,pipestage,slRate1);
    hN_n4=p.findNetwork('fullname','SQRT_Half/sqrt_Long/Rounding');
    createNetwork_n4(p,hN_n4,slRate1);
    hN_n3=p.findNetwork('fullname','SQRT_Half/sqrt_Long');
    createNetwork_n3(p,hN_n3,pipestage,slRate1);
    hN_n2=p.findNetwork('fullname','SQRT_Half/sqrt_Inf_or_NaN');
    createNetwork_n2(p,hN_n2,slRate1);
    hN_n1=p.findNetwork('fullname','SQRT_Half/appendMantissaAndCorrectExponent');
    createNetwork_n1(p,hN_n1,slRate1);
    hN_n0=p.findNetwork('fullname','SQRT_Half');
    createNetwork_n0(p,hN_n0,pipestage,slRate1);
end

