function hTopN=getDoubleGainPow2Comp(hN,latency,slRate)
    p=pir(hN.getCtxName);
    denormal=transformnfp.handleDenormal();
    hTopN=addNetworks(p,denormal);

    pipestage=zeros(1,5);
    if~denormal
        switch latency
        case 0
            pipestage=zeros(1,5);
        case 1
            pipestage(5)=1;
        case 2
            pipestage=[1,0,0,0,1];
        otherwise
            assert(false,'Illegal latency number in nfp_gain_comp.');
        end
    else
        switch latency
        case 0
            pipestage=zeros(1,5);
        case 1
            pipestage(3)=1;
        case 2
            pipestage=[0,1,0,0,1];
        case 3
            pipestage=[1,0,1,0,1];
        case 4
            pipestage=[1,0,1,1,1];
        case 5
            pipestage=[1,1,1,1,1];
        otherwise
            assert(false,'Illegal latency number in nfp_gain_comp.');
        end
    end

    createNetworks(p,denormal,pipestage,slRate);
end

function hTopN=addNetworks(p,denormal)
    hN=p.addNetwork;
    hN.Name='GainPow2_Double';
    hN.FullPath='Double_GainPow2/GainPow2_Double';
    hTopN=hN;
    hN=p.addNetwork;
    hN.Name='GainPow2_Main';
    hN.FullPath='Double_GainPow2/GainPow2_Double/GainPow2_Main';
    if(denormal)
        hN=p.addNetwork;
        hN.Name='GainPow2_Main_Denormals';
        hN.FullPath='Double_GainPow2/GainPow2_Double/GainPow2_Main/GainPow2_Main_Denormals';
        hN=p.addNetwork;
        hN.Name='Rounding_Denormals';
        hN.FullPath='Double_GainPow2/GainPow2_Double/GainPow2_Main/GainPow2_Main_Denormals/Rounding_Denormals';
    else
        hN=p.addNetwork;
        hN.Name='GainPow2_Main_Normals';
        hN.FullPath='Double_GainPow2/GainPow2_Double/GainPow2_Main/GainPow2_Main_Normals';
    end
end

function createNetworks(p,denormal,pipestage,slRate)
    if(denormal)
        hN_n13=p.findNetwork('fullname','Double_GainPow2/GainPow2_Double/GainPow2_Main/GainPow2_Main_Denormals/Rounding_Denormals');
        createNetwork_n13(p,hN_n13,pipestage,slRate);
        hN_n2=p.findNetwork('fullname','Double_GainPow2/GainPow2_Double/GainPow2_Main/GainPow2_Main_Denormals');
        createNetwork_n2(p,hN_n2,pipestage,slRate);
    else
        hN_n3=p.findNetwork('fullname','Double_GainPow2/GainPow2_Double/GainPow2_Main/GainPow2_Main_Normals');
        createNetwork_n3(p,hN_n3,pipestage,slRate);
    end
    hN_n1=p.findNetwork('fullname','Double_GainPow2/GainPow2_Double/GainPow2_Main');
    createNetwork_n1(p,hN_n1,denormal,slRate);
    hN_n0=p.findNetwork('fullname','Double_GainPow2/GainPow2_Double');
    createNetwork_n0(p,hN_n0,slRate);
end

