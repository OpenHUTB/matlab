function createNetworks(p,denormal,fname,slRate1)
    if(denormal)
        hN_n28=p.findNetwork('fullname','Pow10_Single/pow10_Main/pow10_Normalize_Answer/pow10_Normalize_Answer_Denormals');
        createNetwork_n28(p,hN_n28,slRate1);
        hN_n27=p.findNetwork('fullname','Pow10_Single/pow10_Main/pow10_Normalize_Answer');
        createNetwork_n27(p,hN_n27,slRate1);
        hN_n27.addComment('handling denormalized signals');
    else
        hN_n28=p.findNetwork('fullname','Pow10_Single/pow10_Main/pow10_Normalize_Answer/pow10_Normalize_Answer_Normals');
        createNetwork_n28_normals(p,hN_n28,slRate1);
        hN_n27=p.findNetwork('fullname','Pow10_Single/pow10_Main/pow10_Normalize_Answer');
        createNetwork_n27_normals(p,hN_n27,slRate1);
    end
    hN_n26=p.findNetwork('fullname','Pow10_Single/pow10_Main/pow10_Multiply_by_Log2_10');
    createNetwork_n26(p,hN_n26,fname,slRate1);
    hN_n15=p.findNetwork('fullname','Pow10_Single/pow10_Main/pow10_Main_Iterations');
    createNetwork_n15(p,hN_n15,slRate1);
    hN_n14=p.findNetwork('fullname','Pow10_Single/pow10_Main/pow10_First_Iteration');
    createNetwork_n14(p,hN_n14,slRate1);
    hN_n13=p.findNetwork('fullname','Pow10_Single/pow10_Main/pow10_Final_Approximation');
    createNetwork_n13(p,hN_n13,slRate1);
    hN_n12=p.findNetwork('fullname','Pow10_Single/pow10_Main/pow10_Extract_Exp_Mant');
    createNetwork_n12(p,hN_n12,slRate1);
    hN_n11=p.findNetwork('fullname','Pow10_Single/pow10_Main/pow10_Check_Exp_Range_and_Shift_Mant');
    createNetwork_n11(p,hN_n11,denormal,slRate1);
    if(denormal)
        hN_n10=p.findNetwork('fullname','Pow10_Single/pow10_Main/Rounding/Rounding_Denormals');
        createNetwork_n10(p,hN_n10,slRate1);
        hN_n9=p.findNetwork('fullname','Pow10_Single/pow10_Main/Rounding');
        createNetwork_n9(p,hN_n9,slRate1);
    else
        hN_n10=p.findNetwork('fullname','Pow10_Single/pow10_Main/Rounding/Rounding_Normals');
        createNetwork_n10_normals(p,hN_n10,slRate1);
        hN_n9=p.findNetwork('fullname','Pow10_Single/pow10_Main/Rounding');
        createNetwork_n9_normals(p,hN_n9,slRate1);
    end
    hN_n8=p.findNetwork('fullname','Pow10_Single/pow10_Main');
    createNetwork_n8(p,hN_n8,slRate1);
    hN_n7=p.findNetwork('fullname','Pow10_Single/pow10_Inf_or_NaN');
    createNetwork_n7(p,hN_n7,slRate1);
    if(denormal)
        hN_n6=p.findNetwork('fullname','Pow10_Single/checkDenormal/checkDenormal_Denormals');
        createNetwork_n6(p,hN_n6,slRate1);
        hN_n5=p.findNetwork('fullname','Pow10_Single/checkDenormal');
        createNetwork_n5(p,hN_n5,slRate1);
    else
        hN_n6=p.findNetwork('fullname','Pow10_Single/checkDenormal/checkDenormal_Normals');
        createNetwork_n6_normals(p,hN_n6,slRate1);
        hN_n5=p.findNetwork('fullname','Pow10_Single/checkDenormal');
        createNetwork_n5_normals(p,hN_n5,slRate1);
    end
    hN_n4=p.findNetwork('fullname','Pow10_Single/appendMantissaAndCorrectExponent');
    createNetwork_n4(p,hN_n4,slRate1);
    hN_n2=p.findNetwork('fullname','Pow10_Single/InitCycles/InitCycles');
    createNetwork_n2(p,hN_n2,slRate1);
    hN_n1=p.findNetwork('fullname','Pow10_Single/InitCycles');
    createNetwork_n1(p,hN_n1,slRate1);
    hN_n0=p.findNetwork('fullname','Pow10_Single');
    createNetwork_n0(p,hN_n0,slRate1);
end

function hN=createNetwork_n28(~,hN,slRate1)
    pirTyp6=pir_ufixpt_t(1,0);
    pirTyp1=pir_sfixpt_t(10,0);
    pirTyp7=pir_sfixpt_t(10,0);
    pirTyp2=pir_sfixpt_t(34,-31);
    pirTyp5=pir_ufixpt_t(1,0);
    pirTyp4=pir_ufixpt_t(24,0);
    pirTyp3=pir_ufixpt_t(8,0);

    fiMath1=fimath('RoundingMethod','Nearest','OverflowAction','Saturate','ProductMode','FullPrecision','SumMode','FullPrecision');

    nt2=numerictype(0,8,0);
    nt1=numerictype(1,10,0);
    nt3=numerictype(1,34,31);




    hN.addInputPort('exp_out');
    exp_out_s0=addSignal(hN,'exp_out',pirTyp1,slRate1);
    exp_out_s0.addDriver(hN,0);

    hN.addInputPort('y');
    y_s1=addSignal(hN,'y',pirTyp2,slRate1);
    y_s1.addDriver(hN,1);

    hN.addOutputPort('exp_norm');
    ifBitget_y_CfType_Pow2Intermedi____out1_s19=addSignal(hN,'if bitget(y, cfType.Pow2Intermedi..._out1',pirTyp3,slRate1);
    ifBitget_y_CfType_Pow2Intermedi____out1_s19.addReceiver(hN,0);

    hN.addOutputPort('mant_out');
    BitSlice2_out1_s5=addSignal(hN,'BitSlice2_out1',pirTyp4,slRate1);
    BitSlice2_out1_s5.addReceiver(hN,1);

    x1_Exp_out_out1_s2=addSignal(hN,'1 - exp_out_out1',pirTyp3,slRate1);
    BitSlice_out1_s3=addSignal(hN,'BitSlice_out1',pirTyp5,slRate1);
    BitSlice1_out1_s4=addSignal(hN,'BitSlice1_out1',pirTyp5,slRate1);
    C_out1_s6=addSignal(hN,'C_out1',pirTyp1,slRate1);
    C1_out1_s7=addSignal(hN,'C1_out1',pirTyp3,slRate1);
    C2_out1_s8=addSignal(hN,'C2_out1',pirTyp3,slRate1);
    C3_out1_s9=addSignal(hN,'C3_out1',pirTyp2,slRate1);
    C4_out1_s10=addSignal(hN,'C4_out1',pirTyp3,slRate1);
    DTC1_out1_s11=addSignal(hN,'DTC1_out1',pirTyp3,slRate1);
    Delay1_out1_s12=addSignal(hN,'Delay1_out1',pirTyp3,slRate1);
    Delay30_out1_s13=addSignal(hN,'Delay30_out1',pirTyp3,slRate1);
    ShiftArithmetic_out1_s14=addSignal(hN,sprintf('Shift\nArithmetic_out1'),pirTyp2,slRate1);
    exp_out__0_out1_s15=addSignal(hN,'exp_out == 0_out1',pirTyp6,slRate1);
    if_exp_out__0__out1_s16=addSignal(hN,'if (exp_out == 0)_out1',pirTyp3,slRate1);
    if_shift_length__Y_WordLength__out1_s17=addSignal(hN,'if (shift_length >= y.WordLength)_out1',pirTyp2,slRate1);
    ifBitget_exp_out_Exp_out_WordLe____out1_s18=addSignal(hN,'if bitget(exp_out, exp_out.WordLe..._out1',pirTyp3,slRate1);
    shift_length__Y_WordLength_out1_s20=addSignal(hN,'shift_length >= y.WordLength_out1',pirTyp6,slRate1);


    pirelab.getConstComp(hN,...
    C_out1_s6,...
    fi(0,nt1,fiMath1,'hex','001'),...
    'C','on',0,'','','');


    pirelab.getConstComp(hN,...
    C1_out1_s7,...
    fi(0,nt2,fiMath1,'hex','01'),...
    'C1','on',0,'','','');


    pirelab.getConstComp(hN,...
    C2_out1_s8,...
    fi(0,nt2,fiMath1,'hex','00'),...
    'C2','on',1,'','','');


    pirelab.getConstComp(hN,...
    C3_out1_s9,...
    fi(0,nt3,fiMath1,'hex','00000000'),...
    'C3','on',1,'','','');


    pirelab.getConstComp(hN,...
    C4_out1_s10,...
    fi(0,nt2,fiMath1,'hex','00'),...
    'C4','on',1,'','','');


    pirelab.getIntDelayComp(hN,...
    DTC1_out1_s11,...
    Delay1_out1_s12,...
    transformnfp.MinLatencyDelay1,'Delay1',...
    uint8(0),...
    0,0,[],0,0);


    pirelab.getIntDelayComp(hN,...
    ifBitget_exp_out_Exp_out_WordLe____out1_s18,...
    Delay30_out1_s13,...
    transformnfp.MinLatencyDelay1,'Delay30',...
    uint8(0),...
    0,0,[],0,0);


    pirelab.getAddComp(hN,...
    [C_out1_s6,exp_out_s0],...
    x1_Exp_out_out1_s2,...
    'Floor','Wrap','1 - exp_out',pirTyp7,'+-');


    pirelab.getBitSliceComp(hN,...
    exp_out_s0,...
    BitSlice_out1_s3,...
    9,9,'BitSlice');


    pirelab.getBitSliceComp(hN,...
    if_shift_length__Y_WordLength__out1_s17,...
    BitSlice1_out1_s4,...
    31,31,'BitSlice1');


    pirelab.getBitSliceComp(hN,...
    if_shift_length__Y_WordLength__out1_s17,...
    BitSlice2_out1_s5,...
    30,7,'BitSlice2');


    pirelab.getDTCComp(hN,...
    exp_out_s0,...
    DTC1_out1_s11,...
    'Floor','Wrap','RWV','DTC1');


    pirelab.getDynamicBitShiftComp(hN,...
    [y_s1,Delay30_out1_s13],...
    ShiftArithmetic_out1_s14,...
    'right','dynamic_shift');


    pirelab.getCompareToValueComp(hN,...
    exp_out_s0,...
    exp_out__0_out1_s15,...
    '==',double(0),...
    'exp_out == 0',0);


    pirelab.getSwitchComp(hN,...
    [C1_out1_s7,C2_out1_s8],...
    if_exp_out__0__out1_s16,...
    exp_out__0_out1_s15,'if (exp_out == 0)',...
    '~=',0,'Floor','Wrap');


    pirelab.getSwitchComp(hN,...
    [C3_out1_s9,ShiftArithmetic_out1_s14],...
    if_shift_length__Y_WordLength__out1_s17,...
    shift_length__Y_WordLength_out1_s20,'if (shift_length >= y.WordLength)',...
    '~=',0,'Floor','Wrap');


    pirelab.getSwitchComp(hN,...
    [x1_Exp_out_out1_s2,if_exp_out__0__out1_s16],...
    ifBitget_exp_out_Exp_out_WordLe____out1_s18,...
    BitSlice_out1_s3,'if bitget(exp_out, exp_out.WordLe...',...
    '~=',0,'Floor','Wrap');


    pirelab.getSwitchComp(hN,...
    [Delay1_out1_s12,C4_out1_s10],...
    ifBitget_y_CfType_Pow2Intermedi____out1_s19,...
    BitSlice1_out1_s4,'if bitget(y, cfType.Pow2Intermedi...',...
    '~=',0,'Floor','Wrap');


    pirelab.getCompareToValueComp(hN,...
    Delay30_out1_s13,...
    shift_length__Y_WordLength_out1_s20,...
    '>=',fi(0,nt2,fiMath1,'hex','22'),...
    'shift_length >= y.WordLength',0);


end

