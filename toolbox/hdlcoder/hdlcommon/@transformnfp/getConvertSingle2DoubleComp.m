function hTopN=getConvertSingle2DoubleComp(hN,latency,slRate)
    p=pir(hN.getCtxName);
    hTopN=addNetworks(p);
    pipestage=zeros(1,5);
    switch latency
    case 0
        pipestage=zeros(1,5);
    case 1
        pipestage=[0,0,1,0,0];
    case 2
        pipestage=[0,1,0,1,0];
    case 3
        pipestage=[1,1,1,0,0];
    case 4
        pipestage=[1,1,1,0,1];
    case 5
        pipestage=[1,1,1,1,1];
    case 6
        pipestage=ones(1,5);
    otherwise
        assert(false,'Illegal latency number in Single2Double');
    end
    createNetworks(p,latency,pipestage,slRate);
end

function hTopN=addNetworks(p)
    hN=p.addNetwork;
    hN.Name='nfp_convert_single2double';
    hN.FullPath='DTC_Single2Double/nfp_convert_single2double';
    hTopN=hN;
    hN=p.addNetwork;
    hN.Name='InitCycles';
    hN.FullPath='DTC_Single2Double/nfp_convert_single2double/InitCycles';
end

function createNetworks(p,latency,pipestage,s1Rate)
    hN_n5=p.findNetwork('fullname','DTC_Single2Double/nfp_convert_single2double/InitCycles');
    createNetwork_n5(p,hN_n5,pipestage,latency,s1Rate);
    hN_n0=p.findNetwork('fullname','DTC_Single2Double/nfp_convert_single2double');
    createNetwork_n0(p,hN_n0,pipestage,s1Rate);
end

