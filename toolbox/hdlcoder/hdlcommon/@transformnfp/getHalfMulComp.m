function hN=createNetwork_n29(~,hN,slRate1)
    pirTyp3=pir_ufixpt_t(1,0);
    pirTyp1=pir_ufixpt_t(13,0);
    pirTyp2=pir_ufixpt_t(2,0);

    hN.addInputPort('In1');
    In1_s0=addSignal(hN,'In1',pirTyp1,slRate1);
    In1_s0.addDriver(hN,0);

    hN.addInputPort('In3');
    In3_s1=addSignal(hN,'In3',pirTyp1,slRate1);
    In3_s1.addDriver(hN,1);

    hN.addInputPort('In4');
    In4_s2=addSignal(hN,'In4',pirTyp1,slRate1);
    In4_s2.addDriver(hN,2);

    hN.addInputPort('In5');
    In5_s3=addSignal(hN,'In5',pirTyp1,slRate1);
    In5_s3.addDriver(hN,3);

    hN.addInputPort('In2');
    In2_s4=addSignal(hN,'In2',pirTyp2,slRate1);
    In2_s4.addDriver(hN,4);

    hN.addOutputPort('Out1');
    Switch3_out1_s9=addSignal(hN,'Switch3_out1',pirTyp1,slRate1);
    Switch3_out1_s9.addReceiver(hN,0);

    BitSlice1_out1_s5=addSignal(hN,'Bit Slice1_out1',pirTyp3,slRate1);
    BitSlice2_out1_s6=addSignal(hN,'Bit Slice2_out1',pirTyp3,slRate1);
    Switch1_out1_s7=addSignal(hN,'Switch1_out1',pirTyp1,slRate1);
    Switch2_out1_s8=addSignal(hN,'Switch2_out1',pirTyp1,slRate1);


    pirelab.getBitSliceComp(hN,...
    In2_s4,...
    BitSlice1_out1_s5,...
    0,0,'Bit Slice1');


    pirelab.getBitSliceComp(hN,...
    In2_s4,...
    BitSlice2_out1_s6,...
    1,1,'Bit Slice2');


    pirelab.getSwitchComp(hN,...
    [In3_s1,In1_s0],...
    Switch1_out1_s7,...
    BitSlice1_out1_s5,'Switch1',...
    '~=',0,'Floor','Wrap');


    pirelab.getSwitchComp(hN,...
    [In5_s3,In4_s2],...
    Switch2_out1_s8,...
    BitSlice1_out1_s5,'Switch2',...
    '~=',0,'Floor','Wrap');


    pirelab.getSwitchComp(hN,...
    [Switch2_out1_s8,Switch1_out1_s7],...
    Switch3_out1_s9,...
    BitSlice2_out1_s6,'Switch3',...
    '~=',0,'Floor','Wrap');


end

