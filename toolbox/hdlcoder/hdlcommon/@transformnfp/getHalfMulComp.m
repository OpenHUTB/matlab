function hTopN=getHalfMulComp(hN,latency,slRate,denormal,mantMulStrategy)


    p=pir(hN.getCtxName);

    hTopN=addNetworks(p,denormal,mantMulStrategy);

    pipestage=zeros(1,6);
    switch latency
    case 0
        pipestage=zeros(1,6);
    case 1
        switch mantMulStrategy
        case 1
            if(denormal)
                pipestage(4)=1;
            else
                pipestage(5)=1;
            end
        case 3
            if(denormal)
                pipestage(4)=1;
            else
                pipestage(3)=1;
            end
        end
    case 2
        switch mantMulStrategy
        case 1
            if(denormal)
                pipestage=[0,0,1,0,1,0];
            else
                pipestage=[0,1,1,0,0,0];
            end
        case 3
            pipestage=[0,1,0,1,0,0];
        end
    case 3
        switch mantMulStrategy
        case 1
            if(denormal)
                pipestage=[0,1,0,1,1,0];
            else
                pipestage=[0,0,1,1,1,0];
            end
        case 3
            if(denormal)
                pipestage=[0,1,0,1,1,0];
            else
                pipestage=[0,0,1,0,1,1];
            end
        end
    case 4
        pipestage=[0,0,1,1,1,1];
    case 5
        switch mantMulStrategy
        case 1
            if(denormal)
                pipestage=[0,1,1,1,1,1];
            else
                pipestage=[1,1,1,1,0,1];
            end
        case 3
            if(denormal)
                pipestage=[0,1,1,1,1,1];
            else
                pipestage=[1,1,0,1,1,1];
            end
        end
    case 6
        switch mantMulStrategy
        case 1
            if(denormal)
                pipestage=[1,0,1,2,1,1];
            else
                pipestage=ones(1,6);
            end
        case 3
            pipestage=ones(1,6);
        end
    case 7
        switch mantMulStrategy
        case 1
            if(denormal)
                pipestage=[1,1,1,2,1,1];
            else
                pipestage=ones(1,6);
            end
        case 3
            if(denormal)
                pipestage=ones(1,7);
            else
                pipestage=ones(1,6);
            end
        end
    otherwise
        assert(false,'Illegal latency number in nfp_mul_comp.');
    end

    createNetworks(p,latency,pipestage,slRate,denormal,mantMulStrategy);
end

