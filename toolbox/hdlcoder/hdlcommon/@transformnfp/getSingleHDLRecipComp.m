function createNetworks(p,denormal,slRate,numIters)
    if(denormal)
        hN_n15=p.findNetwork('fullname','HDLRecipSingle/NormalizedMantissa/denormShift/Denormalized/denormShift1');
        createNetwork_n15(p,hN_n15,slRate);
        hN_n14=p.findNetwork('fullname','HDLRecipSingle/NormalizedMantissa/denormShift/Denormalized');
        createNetwork_n14(p,hN_n14,slRate);
        hN_n14.addComment('handling denormalized signals');
    else
        hN_n14=p.findNetwork('fullname','HDLRecipSingle/NormalizedMantissa/denormShift/Normalized');
        createNetwork_n14_norm(p,hN_n14,slRate);
    end
    hN_n13=p.findNetwork('fullname','HDLRecipSingle/NormalizedMantissa/denormShift');
    createNetwork_n13(p,hN_n13,denormal,slRate);
    hN_n12=p.findNetwork('fullname','HDLRecipSingle/NormalizedMantissa');
    createNetwork_n12(p,hN_n12,slRate);
    hN_n11=p.findNetwork('fullname','HDLRecipSingle/Normalised_FP/ShiftLength_Cal');
    createNetwork_n11(p,hN_n11,slRate);
    hN_n10=p.findNetwork('fullname','HDLRecipSingle/Normalised_FP');
    createNetwork_n10(p,hN_n10,slRate);
    hN_n9=p.findNetwork('fullname','HDLRecipSingle/NRDelays');
    createNetwork_n9(p,hN_n9,numIters,slRate);
    hN_n5=p.findNetwork('fullname','HDLRecipSingle/NR');
    createNetwork_n5(p,hN_n5,numIters,slRate);
    hN_n4=p.findNetwork('fullname','HDLRecipSingle/InitCycles/InitCycles');
    createNetwork_n4(p,hN_n4,numIters,slRate);
    hN_n3=p.findNetwork('fullname','HDLRecipSingle/InitCycles');
    createNetwork_n3(p,hN_n3,slRate);
    hN_n2=p.findNetwork('fullname','HDLRecipSingle/E=0_judgement');
    createNetwork_n2(p,hN_n2,slRate);
    hN_n1=p.findNetwork('fullname','HDLRecipSingle/0.M');
    createNetwork_n1(p,hN_n1,slRate);
    hN_n0=p.findNetwork('fullname','HDLRecipSingle');
    createNetwork_n0(p,hN_n0,slRate);
end

