    BitSlice1_out1_s4=addSignal(hN,'BitSlice1_out1',pirTyp5,slRate1);
    C_out1_s6=addSignal(hN,'C_out1',pirTyp1,slRate1);
    C1_out1_s7=addSignal(hN,'C1_out1',pirTyp3,slRate1);
    C2_out1_s8=addSignal(hN,'C2_out1',pirTyp3,slRate1);
    C3_out1_s9=addSignal(hN,'C3_out1',pirTyp2,slRate1);
    C4_out1_s10=addSignal(hN,'C4_out1',pirTyp3,slRate1);
    DTC1_out1_s11=addSignal(hN,'DTC1_out1',pirTyp3,slRate1);
    ShiftArithmetic_out1_s12=addSignal(hN,sprintf('Shift\nArithmetic_out1'),pirTyp2,slRate1);
    exp_out__0_out1_s13=addSignal(hN,'exp_out == 0_out1',pirTyp6,slRate1);
    if_exp_out__0__out1_s14=addSignal(hN,'if (exp_out == 0)_out1',pirTyp3,slRate1);
    if_shift_length__Y_WordLength__out1_s15=addSignal(hN,'if (shift_length >= y.WordLength)_out1',pirTyp2,slRate1);
    ifBitget_exp_out_Exp_out_WordLe____out1_s16=addSignal(hN,'if bitget(exp_out, exp_out.WordLe..._out1',pirTyp3,slRate1);
    shift_length__Y_WordLength_out1_s18=addSignal(hN,'shift_length >= y.WordLength_out1',pirTyp6,slRate1);


    pirelab.getConstComp(hN,...
    C_out1_s6,...
    fi(0,nt1,fiMath1,'hex','01'),...
    'C','on',0,'','','');


    pirelab.getConstComp(hN,...
    C1_out1_s7,...
    fi(0,nt2,fiMath1,'hex','01'),...
    'C1','on',0,'','','');


    pirelab.getConstComp(hN,...
    C2_out1_s8,...
    fi(0,nt2,fiMath1,'hex','00'),...
    'C2','on',1,'','','');


    pirelab.getConstComp(hN,...
    C3_out1_s9,...
    fi(0,nt3,fiMath1,'hex','00000'),...
    'C3','on',1,'','','');


    pirelab.getConstComp(hN,...
    C4_out1_s10,...
    fi(0,nt2,fiMath1,'hex','00'),...
    'C4','on',1,'','','');


    pirelab.getAddComp(hN,...
    [C_out1_s6,exp_out_s0],...
    x1_Exp_out_out1_s2,...
    'Floor','Wrap','1 - exp_out',pirTyp7,'+-');


    pirelab.getBitSliceComp(hN,...
    exp_out_s0,...
    BitSlice_out1_s3,...
    6,6,'BitSlice');


    pirelab.getBitSliceComp(hN,...
    if_shift_length__Y_WordLength__out1_s15,...
    BitSlice1_out1_s4,...
    17,17,'BitSlice1');


    pirelab.getBitSliceComp(hN,...
    if_shift_length__Y_WordLength__out1_s15,...
    BitSlice2_out1_s5,...
    16,6,'BitSlice2');


    pirelab.getDTCComp(hN,...
    exp_out_s0,...
    DTC1_out1_s11,...
    'Floor','Wrap','RWV','DTC1');


    pirelab.getDynamicBitShiftComp(hN,...
    [y_s1,ifBitget_exp_out_Exp_out_WordLe____out1_s16],...
    ShiftArithmetic_out1_s12,...
    'right','dynamic_shift');


    pirelab.getCompareToValueComp(hN,...
    exp_out_s0,...
    exp_out__0_out1_s13,...
    '==',double(0),...
    'exp_out == 0',0);


    pirelab.getSwitchComp(hN,...
    [C1_out1_s7,C2_out1_s8],...
    if_exp_out__0__out1_s14,...
    exp_out__0_out1_s13,'if (exp_out == 0)',...
    '~=',0,'Floor','Wrap');


    pirelab.getSwitchComp(hN,...
    [C3_out1_s9,ShiftArithmetic_out1_s12],...
    if_shift_length__Y_WordLength__out1_s15,...
    shift_length__Y_WordLength_out1_s18,'if (shift_length >= y.WordLength)',...
    '~=',0,'Floor','Wrap');


    pirelab.getSwitchComp(hN,...
    [x1_Exp_out_out1_s2,if_exp_out__0__out1_s14],...
    ifBitget_exp_out_Exp_out_WordLe____out1_s16,...
    BitSlice_out1_s3,'if bitget(exp_out, exp_out.WordLe...',...
    '~=',0,'Floor','Wrap');


    pirelab.getSwitchComp(hN,...
    [DTC1_out1_s11,C4_out1_s10],...
    ifBitget_y_CfType_Pow2Intermedi____out1_s17,...
    BitSlice1_out1_s4,'if bitget(y, cfType.Pow2Intermedi...',...
    '~=',0,'Floor','Wrap');


    pirelab.getCompareToValueComp(hN,...
    ifBitget_exp_out_Exp_out_WordLe____out1_s16,...
    shift_length__Y_WordLength_out1_s18,...
    '>=',fi(0,nt2,fiMath1,'hex','14'),...
    'shift_length >= y.WordLength',0);


end

