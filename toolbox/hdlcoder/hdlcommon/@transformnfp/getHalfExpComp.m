    y_s1.addDriver(hN,1);

    hN.addOutputPort('exp_norm');
    ifBitget_y_CfType_Pow2Intermedi____out1_s17=addSignal(hN,'if bitget(y, cfType.Pow2Intermedi..._out1',pirTyp3,slRate1);
    ifBitget_y_CfType_Pow2Intermedi____out1_s17.addReceiver(hN,0);

    hN.addOutputPort('mant_out');
    BitSlice2_out1_s5=addSignal(hN,'BitSlice2_out1',pirTyp4,slRate1);
    BitSlice2_out1_s5.addReceiver(hN,1);

    x1_Exp_out_out1_s2=addSignal(hN,'1 - exp_out_out1',pirTyp3,slRate1);
