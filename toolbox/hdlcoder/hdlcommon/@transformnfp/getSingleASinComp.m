function hN=createNetwork_n18(~,hN,slRate1)
    pirTyp3=pir_boolean_t;
    pirTyp4=pir_signed_t(8);
    pirTyp5=pir_sfixpt_t(32,0);
    pirTyp2=pir_sfixpt_t(9,0);
    pirTyp1=pir_unsigned_t(8);

    fiMath1=fimath('RoundingMethod','Nearest','OverflowAction','Saturate','ProductMode','FullPrecision','SumMode','FullPrecision');

    nt2=numerictype(0,8,0);
    nt1=numerictype(1,8,0);
    nt3=numerictype(1,9,0);


    hN.addInputPort('biased_e');
    biased_e_s0=addSignal(hN,'biased_e',pirTyp1,slRate1);
    biased_e_s0.addDriver(hN,0);

    hN.addOutputPort('unbiased_e');
    Delay14_out1_s3=addSignal(hN,'Delay14_out1',pirTyp2,slRate1);
    Delay14_out1_s3.addReceiver(hN,0);

    hN.addOutputPort('small_exp');
    RelationalOperator_out1_s4=addSignal(hN,sprintf('Relational\nOperator_out1'),pirTyp3,slRate1);
    RelationalOperator_out1_s4.addReceiver(hN,1);

    x1_out1_s1=addSignal(hN,'1_out1',pirTyp4,slRate1);
    x127_out1_s2=addSignal(hN,'127_out1',pirTyp1,slRate1);
    Subtract_out1_s5=addSignal(hN,'Subtract_out1',pirTyp2,slRate1);


    pirelab.getConstComp(hN,...
    x1_out1_s1,...
    fi(0,nt1,fiMath1,'hex','f4'),...
    '1','on',0,'','','');


    pirelab.getConstComp(hN,...
    x127_out1_s2,...
    fi(0,nt2,fiMath1,'hex','7f'),...
    '127','on',0,'','','');


    pirelab.getIntDelayComp(hN,...
    Subtract_out1_s5,...
    Delay14_out1_s3,...
    transformnfp.Delay1,'Delay14',...
    fi(0,nt3,fiMath1,'hex','000'),...
    0,0,[],0,0);

    pirelab.getAnnotationComp(hN,...
    'Display');


    pirelab.getRelOpComp(hN,...
    [Delay14_out1_s3,x1_out1_s1],...
    RelationalOperator_out1_s4,...
    '<',0,sprintf('Relational\nOperator'));


    pirelab.getAddComp(hN,...
    [biased_e_s0,x127_out1_s2],...
    Subtract_out1_s5,...
    'Floor','Wrap','Subtract',pirTyp5,'+-');


end

