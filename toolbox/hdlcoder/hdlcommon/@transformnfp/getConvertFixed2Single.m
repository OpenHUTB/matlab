function hTopN=getConvertFixed2Single(hN,latency,slRate,WL,UWL,FL)



    if WL==UWL
        topNwkName='nfp_convert_fix_';
    else
        topNwkName='nfp_convert_sfix_';
    end
    topNwkName=[topNwkName,int2str(WL),'_En',int2str(FL),'_to_single'];
    p=pir(hN.getCtxName);
    hTopN=addNetworks(p,topNwkName);

    pipestage=zeros(1,6);
    switch latency
    case 0
        pipestage=zeros(1,6);
    case 1
        pipestage(3)=1;
    case 2
        pipestage=[0,1,0,0,1,0];
    case 3
        pipestage=[0,1,0,1,0,1];
    case 4
        pipestage=[1,0,1,1,0,1];
    case 5
        pipestage=[1,0,1,1,1,1];
    case 6
        pipestage=[1,1,1,1,1,1];
    otherwise
        assert(false,'Illegal latency number in nfp_dtc_comp.');
    end
    createNetworks(p,topNwkName,pipestage,slRate,WL,UWL,FL);
end

