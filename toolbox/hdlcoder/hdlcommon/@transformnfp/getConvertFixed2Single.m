    nt3=numerictype(0,128,FL);
    nt4=numerictype(0,60,0);
    nt1=numerictype(0,8,0);
    nt2=numerictype(1,9,0);


    hN.addInputPort('RE');
    RE_s0=addSignal(hN,'RE',pirTyp1,slRate1);
    RE_s0.addDriver(hN,0);

    hN.addInputPort('Integer');
    Integer_s1=addSignal(hN,'Integer',pirTyp2,slRate1);
    Integer_s1.addDriver(hN,1);

    hN.addOutputPort('Shifted_integer_D');
    Delay3_out1_s6=addSignal(hN,'Delay3_out1',pirTyp3,slRate1);
    Delay3_out1_s6.addReceiver(hN,0);

    BitSlice1_out1_s2=addSignal(hN,'Bit Slice1_out1',pirTyp3,slRate1);
    Constant_out1_s3=addSignal(hN,'Constant_out1',pirTyp1,slRate1);
    Delay1_out1_s4=addSignal(hN,'Delay1_out1',pirTyp4,slRate1);
    Delay2_out1_s5=addSignal(hN,'Delay2_out1',pirTyp2,slRate1);
    ShiftArithmetic_out1_s7=addSignal(hN,sprintf('Shift\nArithmetic_out1'),pirTyp2,slRate1);
    Subtract_out1_s8=addSignal(hN,'Subtract_out1',pirTyp4,slRate1);
    shiftArithmetic_selsig_s9=addSignal(hN,sprintf('shift\narithmetic_selsig'),pirTyp4,slRate1);
    shiftArithmetic_zerosig_s10=addSignal(hN,sprintf('shift\narithmetic_zerosig'),pirTyp4,slRate1);


    pirelab.getConstComp(hN,...
    Constant_out1_s3,...
    fi(0,nt1,fiMath1,'hex','80'),...
    'Constant','on',0,'','','');


    pirelab.getIntDelayComp(hN,...
    Subtract_out1_s8,...
    Delay1_out1_s4,...
    pipestage(4),'Delay1',...
    fi(0,nt2,fiMath1,'hex','000'),...
    0,0,[],0,0);


    pirelab.getIntDelayComp(hN,...
    Integer_s1,...
    Delay2_out1_s5,...
    pipestage(4),'Delay2',...
    fi(0,nt3,fiMath1,'hex','00000000000000000000000000000000'),...
    0,0,[],0,0);


    pirelab.getIntDelayComp(hN,...
    BitSlice1_out1_s2,...
    Delay3_out1_s6,...
    pipestage(5),'Delay3',...
    fi(0,nt4,fiMath1,'hex','000000000000000'),...
    0,0,[],0,0);


    pirelab.getBitSliceComp(hN,...
    ShiftArithmetic_out1_s7,...
    BitSlice1_out1_s2,...
    126,67,'Bit Slice1');


    pirelab.getConstComp(hN,...
    shiftArithmetic_zerosig_s10,...
    fi(0,nt2,fiMath1,'hex','000'),...
    'const','on',0,'','','');

    pirelab.getMinMaxComp(hN,...
    [Delay1_out1_s4,shiftArithmetic_zerosig_s10],...
    shiftArithmetic_selsig_s9,...
    sprintf('shift\narithmetic_nonneg'),'max',0,'Value',1);

    pirelab.getDynamicBitShiftComp(hN,...
    [Delay2_out1_s5,shiftArithmetic_selsig_s9],...
    ShiftArithmetic_out1_s7,...
    'left','dynamic_shift');


    pirelab.getAddComp(hN,...
    [RE_s0,Constant_out1_s3],...
    Subtract_out1_s8,...
    'Floor','Wrap','Subtract',pirTyp4,'-+');


end

