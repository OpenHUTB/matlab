        ConstantS=addSignal(hN,'ConstantS',pirTyp7,slRate1);
        pirelab.getConstComp(hN,...
        ConstantS,...
        0,...
        'Constant','on',0,'','','');

        pirelab.getAddComp(hN,...
        [ConstantS,DataTypeConversion1_out1_s8],...
        UnaryMinus_out1_s21,...
        'Floor','Wrap','Subtract',pirTyp7,'+-');
    else

        pirelab.getUnaryMinusComp(hN,...
        DataTypeConversion1_out1_s8,...
        UnaryMinus_out1_s21,...
        'Wrap','Unary Minus');
    end

end

function hN=createNetwork_n0(p,hN,topNwkName,slRate1,WL,UWL,FL)
    pirTyp2=pir_ufixpt_t(1,0);
    if(UWL==WL)
        pirTyp1=pir_ufixpt_t(WL,-FL);
    else
        pirTyp1=pir_sfixpt_t(WL,-FL);
    end
    pirTyp4=pir_ufixpt_t(23,0);
    pirTyp3=pir_ufixpt_t(8,0);




    hN.addInputPort('In1');
    In1_s0=addSignal(hN,'In1',pirTyp1,slRate1);
    In1_s0.addDriver(hN,0);

    hN.addOutputPort('Out1');
    Convert_Fixed2Single_out1_s1=addSignal(hN,'Convert_Fixed2Single_out1',pirTyp2,slRate1);
    Convert_Fixed2Single_out1_s1.addReceiver(hN,0);

    hN.addOutputPort('Out2');
    Convert_Fixed2Single_out2_s2=addSignal(hN,'Convert_Fixed2Single_out2',pirTyp3,slRate1);
    Convert_Fixed2Single_out2_s2.addReceiver(hN,1);

    hN.addOutputPort('Out3');
    Convert_Fixed2Single_out3_s3=addSignal(hN,'Convert_Fixed2Single_out3',pirTyp4,slRate1);
    Convert_Fixed2Single_out3_s3.addReceiver(hN,2);


    hRefN=p.findNetwork('fullname',getSubNwkName(topNwkName,'Convert_Fixed2Single'));
    Convert_Fixed2Single=hN.addComponent('ntwk_instance_comp',hRefN);
    Convert_Fixed2Single.Name='Convert_Fixed2Single';
    pirelab.connectNtwkInstComp(Convert_Fixed2Single,...
    In1_s0,...
    [Convert_Fixed2Single_out1_s1,Convert_Fixed2Single_out2_s2,Convert_Fixed2Single_out3_s3]);


end

