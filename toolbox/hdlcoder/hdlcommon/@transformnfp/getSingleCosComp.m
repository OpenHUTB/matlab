function hN=createNetwork_n24(~,hN,slRate1)
    pirTyp2=pir_boolean_t;
    pirTyp3=pir_signed_t(8);
    pirTyp4=pir_sfixpt_t(32,0);
    pirTyp1=pir_unsigned_t(8);

    fiMath1=fimath('RoundingMethod','Nearest','OverflowAction','Saturate','ProductMode','FullPrecision','SumMode','FullPrecision');

    nt1=numerictype(0,8,0);
    nt2=numerictype(1,8,0);


    hN.addInputPort('biased_e');
    biased_e_s0=addSignal(hN,'biased_e',pirTyp1,slRate1);
    biased_e_s0.addDriver(hN,0);

    hN.addOutputPort('small_exp');
    RelOp_out1_s4=addSignal(hN,'RelOp_out1',pirTyp2,slRate1);
    RelOp_out1_s4.addReceiver(hN,0);

    hN.addOutputPort('unbiased_e');
    Delay14_out1_s3=addSignal(hN,'Delay14_out1',pirTyp3,slRate1);
    Delay14_out1_s3.addReceiver(hN,1);

    x127_out1_s1=addSignal(hN,'127_out1',pirTyp1,slRate1);
    Constant_out1_s2=addSignal(hN,'Constant_out1',pirTyp3,slRate1);
    Subtract_out1_s5=addSignal(hN,'Subtract_out1',pirTyp3,slRate1);


    pirelab.getConstComp(hN,...
    x127_out1_s1,...
    fi(0,nt1,fiMath1,'hex','7f'),...
    '127','on',0,'','','');


    pirelab.getConstComp(hN,...
    Constant_out1_s2,...
    fi(0,nt2,fiMath1,'hex','f5'),...
    'Constant','on',0,'','','');


    pirelab.getIntDelayComp(hN,...
    Subtract_out1_s5,...
    Delay14_out1_s3,...
    transformnfp.Delay1,'Delay14',...
    int8(0),...
    0,0,[],0,0);


    pirelab.getRelOpComp(hN,...
    [Delay14_out1_s3,Constant_out1_s2],...
    RelOp_out1_s4,...
    '<',0,'RelOp');


    pirelab.getAddComp(hN,...
    [biased_e_s0,x127_out1_s1],...
    Subtract_out1_s5,...
    'Floor','Wrap','Subtract',pirTyp4,'+-');


end

