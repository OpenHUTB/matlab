function hTopN=getConvertDouble2SingleComp(hN,latency,slRate)



    p=pir(hN.getCtxName);
    hTopN=addNetworks(p);
    pipestage=zeros(1,6);
    switch latency
    case 0
        pipestage=zeros(1,6);
    case 1
        pipestage=[0,1,0,0,0,0];
    case 2
        pipestage=[0,1,0,0,1,0];
    case 3
        pipestage=[1,1,0,0,1,0];
    case 4
        pipestage=[1,1,1,0,0,1];
    case 5
        pipestage=[1,1,1,1,0,1];
    case 6
        pipestage=ones(1,6);
    otherwise
        assert(false,'Illegal latency number in Double2Single');
    end
    createNetworks(p,latency,pipestage,slRate);
end

function hTopN=addNetworks(p)
    hN=p.addNetwork;
    hN.Name='nfp_convert_double2single';
    hN.FullPath='DTC_Double2Single/nfp_convert_double2single';
    hTopN=hN;
    hN=p.addNetwork;
    hN.Name='InitCycles';
    hN.FullPath='DTC_Double2Single/nfp_convert_double2single/InitCycles';
end

function createNetworks(p,latency,pipestage,s1Rate)
    hN_n9=p.findNetwork('fullname','DTC_Double2Single/nfp_convert_double2single/InitCycles');
    createNetwork_n9(p,hN_n9,latency,pipestage,s1Rate);
    hN_n0=p.findNetwork('fullname','DTC_Double2Single/nfp_convert_double2single');
    createNetwork_n0(p,hN_n0,pipestage,s1Rate);
end

