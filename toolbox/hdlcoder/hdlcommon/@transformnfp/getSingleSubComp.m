function hTopN = getSingleSubComp( hN, latency, slRate )
p = pir( hN.getCtxName );
hTopN = addNetworks( p );
pipestage = zeros( 1, 11 );
switch latency
case 0
pipestage = zeros( 1, 11 );
case 1
pipestage( 6 ) = 1;
case 2
pipestage( 4 ) = 1;pipestage( 8 ) = 1;
case 3
pipestage( 3 ) = 1;pipestage( 6 ) = 1;pipestage( 9 ) = 1;
case 4
pipestage( 3 ) = 1;pipestage( 5 ) = 1;
pipestage( 7 ) = 1;pipestage( 9 ) = 1;
