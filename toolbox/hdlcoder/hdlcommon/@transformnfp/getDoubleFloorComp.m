        pirTyp2=pir_ufixpt_t(11,0);
        pirTyp3=pir_ufixpt_t(52,0);


        hN.addInputPort('inSign');
        inSign_s0=addSignal(hN,'inSign',pirTyp1,slRate1);
        inSign_s0.addDriver(hN,0);

        hN.addInputPort('inExponent');
        inExponent_s1=addSignal(hN,'inExponent',pirTyp2,slRate1);
        inExponent_s1.addDriver(hN,1);

        hN.addInputPort('inMantissa');
        inMantissa_s2=addSignal(hN,'inMantissa',pirTyp3,slRate1);
