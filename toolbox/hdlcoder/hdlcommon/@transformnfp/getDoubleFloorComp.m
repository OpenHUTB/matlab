        Constant1_out1_s3,...
        fi(0,nt2,fiMath1,'hex','0'),...
        'Constant1','on',1,'','','');


        pirelab.getConstComp(hN,...
        Constant2_out1_s4,...
        fi(0,nt3,fiMath1,'hex','0000000000000'),...
        'Constant2','on',1,'','','');


        pirelab.getConstComp(hN,...
        Constant3_out1_s5,...
        fi(0,nt4,fiMath1,'hex','000'),...
        'Constant3','on',1,'','','');


        pirelab.getIntDelayComp(hN,...
        Switch1_out1_s10,...
        Delay3_PS_5_out1_s6,...
        pipestage(5),'Delay3_PS_5',...
