        Switch5_out1_s11,...
        InitCycles1_out1_s9,'Switch5',...
        '~=',0,'Floor','Wrap');


        pirelab.getSwitchComp(hN,...
        [Constant3_out1_s5,inExponent_s1],...
        Switch6_out1_s12,...
        InitCycles1_out1_s9,'Switch6',...
        '~=',0,'Floor','Wrap');

    else
