function hTopN=getDoubleFloorComp(hN,latency,slRate)




    p=pir(hN.getCtxName);
    hTopN=addNetworks(p);
    pipestage=zeros(1,5);
    switch latency
    case 0
        pipestage=zeros(1,5);

    case 1
        pipestage=[0,1,0,0,0];

    case 2
        pipestage=[1,0,0,1,0];

    case 3
        pipestage=[1,0,1,0,1];

    case 4
        pipestage=[1,1,0,1,1];

    case 5
        pipestage=ones(1,5);
    otherwise
        assert(false,'Illegal latency number for rounding in floor mode.');
    end
    createNetworks(p,pipestage,latency,slRate);
end

function hTopN=addNetworks(p)
    hN=p.addNetwork;
    hN.Name='NFP_Mathworks_Floor_Double';
    hN.FullPath='Double_Floor_Scalar/NFP_Mathworks_Floor_Double';
    hTopN=hN;
    hN=p.addNetwork;
    hN.Name='initCycles';
    hN.FullPath='Double_Floor_Scalar/NFP_Mathworks_Floor_Double/initCycles';
    hN=p.addNetwork;
    hN.Name='InitCycles';
    hN.FullPath='Double_Floor_Scalar/NFP_Mathworks_Floor_Double/initCycles/InitCycles';
end

function createNetworks(p,pipestage,latency,slRate)
    hN_n10=p.findNetwork('fullname','Double_Floor_Scalar/NFP_Mathworks_Floor_Double/initCycles/InitCycles');
    createNetwork_n10(p,hN_n10,pipestage,latency,slRate);
    hN_n9=p.findNetwork('fullname','Double_Floor_Scalar/NFP_Mathworks_Floor_Double/initCycles');
    createNetwork_n9(p,hN_n9,slRate);
    hN_n0=p.findNetwork('fullname','Double_Floor_Scalar/NFP_Mathworks_Floor_Double');
    createNetwork_n0(p,hN_n0,pipestage,slRate);
end

