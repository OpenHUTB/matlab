function hTopN=getConvertSingle2HalfComp(hN,slRate,denormal)




    p=pir(hN.getCtxName);
    hTopN=addNetworks(p,denormal);
    createNetworks(p,denormal,slRate);
end

function hTopN=addNetworks(p,denormal)
    hN=p.addNetwork;
    hN.Name='Convert_Single2Half';
    hN.FullPath='Convert_Single2Half';
    hTopN=hN;
    hN=p.addNetwork;
    hN.Name='NFP_Convert_Single2Half';
    hN.FullPath='Convert_Single2Half/NFP_Convert_Single2Half';
    if(denormal)
        hN=p.addNetwork;
        hN.Name='NFP_Convert_Single2Half_Denormals';
        hN.FullPath='Convert_Single2Half/NFP_Convert_Single2Half/NFP_Convert_Single2Half_Denormals';
        hN=p.addNetwork;
        hN.Name='Subsystem';
        hN.FullPath='Convert_Single2Half/NFP_Convert_Single2Half/NFP_Convert_Single2Half_Denormals/Subsystem';
        hN=p.addNetwork;
        hN.Name='Subsystem1';
        hN.FullPath='Convert_Single2Half/NFP_Convert_Single2Half/NFP_Convert_Single2Half_Denormals/Subsystem1';
    else
        hN=p.addNetwork;
        hN.Name='NFP_Convert_Single2Half_Normals';
        hN.FullPath='Convert_Single2Half/NFP_Convert_Single2Half/NFP_Convert_Single2Half_Normals';
    end
end

function createNetworks(p,denormal,slRate)
    if(denormal)
        hN_n4=p.findNetwork('fullname','Convert_Single2Half/NFP_Convert_Single2Half/NFP_Convert_Single2Half_Denormals/Subsystem1');
        createNetwork_n4(p,hN_n4,slRate);
        hN_n3=p.findNetwork('fullname','Convert_Single2Half/NFP_Convert_Single2Half/NFP_Convert_Single2Half_Denormals/Subsystem');
        createNetwork_n3(p,hN_n3,slRate);
        hN_n2=p.findNetwork('fullname','Convert_Single2Half/NFP_Convert_Single2Half/NFP_Convert_Single2Half_Denormals');
        createNetwork_n2(p,hN_n2,slRate);
    else
        hN_n2=p.findNetwork('fullname','Convert_Single2Half/NFP_Convert_Single2Half/NFP_Convert_Single2Half_Normals');
        createNetwork_n2_normals(p,hN_n2,slRate);
    end
    hN_n1=p.findNetwork('fullname','Convert_Single2Half/NFP_Convert_Single2Half');
    createNetwork_n1(p,hN_n1,denormal,slRate);
    hN_n0=p.findNetwork('fullname','Convert_Single2Half');
    createNetwork_n0(p,hN_n0,slRate);
end

