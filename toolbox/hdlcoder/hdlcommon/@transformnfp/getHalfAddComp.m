function hTopN=getHalfAddComp(hN,latency,slRate)


    p=pir(hN.getCtxName);

    hTopN=addNetworks(p);

    pipestage=zeros(1,8);
    switch latency
    case 0
        pipestage=zeros(1,8);
    case 1
        pipestage=[0,0,0,1,0,0,0,0];
    case 2
        pipestage=[0,0,0,1,0,0,1,0];
    case 3
        pipestage=[0,1,0,0,1,0,1,0];
    case 4
        pipestage=[0,1,0,1,0,1,0,1];
    case 5
        pipestage=[1,1,0,1,0,1,0,1];
    case 6
        pipestage=[1,1,0,1,1,1,0,1];
    case 7
        pipestage=[1,1,1,0,1,1,1,1];
    case 8
        pipestage=ones(1,8);
    otherwise
        assert(false,'Illegal latency number in nfp_adder_comp.');
    end
    createNetworks(p,pipestage,slRate);
end

