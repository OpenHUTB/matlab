function hTopN=getSingleGainPow2Comp(hN,latency,slRate)
    p=pir(hN.getCtxName);
    denormal=transformnfp.handleDenormal();
    hTopN=addNetworks(p,denormal);

    pipestage=zeros(1,5);
    if~denormal
        switch latency
        case 0
            pipestage=zeros(1,5);
        case 1
            pipestage(5)=1;
        case 2
            pipestage=[1,0,0,0,1];
        otherwise
            assert(false,'Illegal latency number in nfp_gain_comp.');
        end
    else
        switch latency
        case 0
            pipestage=zeros(1,5);
        case 1
            pipestage(3)=1;
        case 2
            pipestage=[1,0,0,0,1];
        case 3
            pipestage=[1,0,1,0,1];
        case 4
            pipestage=[1,0,1,1,1];
        case 5
            pipestage=[1,1,1,1,1];
        otherwise
            assert(false,'Illegal latency number in nfp_gain_comp.');
        end
    end

    createNetworks(p,denormal,pipestage,slRate);
end

function hTopN=addNetworks(p,denormal)
    hN=p.addNetwork;
    hN.Name='GainPow2_Single';
    hN.FullPath='GainPow2_Single';
    hTopN=hN;
    hN=p.addNetwork;
    hN.Name='GainPow2_Main';
    hN.FullPath='GainPow2_Single/GainPow2_Main';
    if(denormal)
        hN=p.addNetwork;
        hN.Name='GainPow2_Main_Denormals';
        hN.FullPath='GainPow2_Single/GainPow2_Main/GainPow2_Main_Denormals';
        hN=p.addNetwork;
        hN.Name='Rounding_Denormals';
        hN.FullPath='GainPow2_Single/GainPow2_Main/GainPow2_Main_Denormals/Rounding_Denormals';
    else
        hN=p.addNetwork;
        hN.Name='GainPow2_Main_Normals';
        hN.FullPath='GainPow2_Single/GainPow2_Main/GainPow2_Main_Normals';
    end
end

function createNetworks(p,denormal,pipestage,slRate1)
    if(denormal)
        hN_n19=p.findNetwork('fullname','GainPow2_Single/GainPow2_Main/GainPow2_Main_Denormals/Rounding_Denormals');
        createNetwork_n19(p,hN_n19,slRate1);
        hN_n2=p.findNetwork('fullname','GainPow2_Single/GainPow2_Main/GainPow2_Main_Denormals');
        createNetwork_n2(p,hN_n2,pipestage,slRate1);
        hN_n2.addComment('handling denormalized signals');
    else
        hN_n2=p.findNetwork('fullname','GainPow2_Single/GainPow2_Main/GainPow2_Main_Normals');
        createNetwork_n2_normals(p,hN_n2,pipestage,slRate1);
    end
    hN_n1=p.findNetwork('fullname','GainPow2_Single/GainPow2_Main');
    createNetwork_n1(p,hN_n1,denormal,slRate1);
    hN_n0=p.findNetwork('fullname','GainPow2_Single');
    createNetwork_n0(p,hN_n0,pipestage,slRate1);
end

