function hNewC=lowerGain(hN,hC)



    hOutSignals=hC.PirOutputSignals;
    outTpEx=pirelab.getTypeInfoAsFi(hOutSignals(1).Type,hC.getRoundingMode,hC.getOverflowMode);

    hNewC=pireml.getGainComp(hN,...
    hC.PirInputSignals,...
    hOutSignals,...
    hC.getGainValue,...
    outTpEx,...
    hC.getGainMode,...
    hC.getConstMultiplierMode,...
    hC.Name);

    hNewC.setMultStyle(int8(hC.getDSPStyle));

end
