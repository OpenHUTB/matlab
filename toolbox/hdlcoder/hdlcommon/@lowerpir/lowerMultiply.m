function hNewC=lowerMultiply(hN,hC)



    inputSigns=hC.getInputSigns;

    if strcmp(inputSigns,'/')
        hNewC=pirelab.getReciprocalDivComp(hN,...
        hC.PirInputSignals(1),hC.PirOutputSignals,...
        hC.getRoundingMode,hC.getOverflowMode,...
        hC.Name);
    else

        hNewC=pireml.getMulComp(...
        hN,...
        hC.PirInputSignals,...
        hC.PirOutputSignals,...
        hC.getRoundingMode,...
        hC.getOverflowMode,...
        hC.Name,...
        inputSigns);

        hNewC.setMultStyle(int8(hC.getDSPStyle));

    end


    hNewC.runLoopUnrolling(false);