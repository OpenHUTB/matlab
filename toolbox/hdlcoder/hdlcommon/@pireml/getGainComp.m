function gainComp=getGainComp(hN,hInSignals,hOutSignals,gainFactorEx,outTpEx,gainMode,constMultiplierOptimMode,compName)




    gainComp=hN.addComponent2(...
    'kind','cgireml',...
    'Name',compName,...
    'InputSignals',hInSignals,...
    'OutputSignals',hOutSignals,...
    'EMLFileName','hdleml_gain',...
    'EMLParams',{gainFactorEx,outTpEx,gainMode},...
    'EMLFlag_RunLoopUnrolling',true,...
    'EMLFlag_ConstMultiplierOptimization',constMultiplierOptimMode,...
    'EMLFlag_ParamsFollowInputs',true);

end
