function IterComp=getDivPreCorrectionComp(hN,hInSignals,hOutSignals)




    in1type=pirelab.getTypeInfoAsFi(hInSignals(1).Type);
    in2type=pirelab.getTypeInfoAsFi(hInSignals(2).Type);
    out1type=pirelab.getTypeInfoAsFi(hOutSignals(1).Type);
    out2type=pirelab.getTypeInfoAsFi(hOutSignals(2).Type);

    IterComp=hN.addComponent2(...
    'kind','cgireml',...
    'Name','precorrection',...
    'InputSignals',hInSignals,...
    'OutputSignals',hOutSignals,...
    'EMLFileName','hdleml_divide_preCorrection',...
    'EMLParams',{in1type,in2type,out1type,out2type},...
    'EMLFlag_ParamsFollowInputs',true,...
    'EMLFlag_TreatInputBoolsAsUfix1','true');

end


