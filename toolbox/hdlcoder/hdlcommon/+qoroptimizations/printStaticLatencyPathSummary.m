

function printStaticLatencyPathSummary(model,staticLatReportFile,res,outModelName,topLevelModel,codegenDir)


    tempCodegenDir=split(codegenDir,filesep);

    codegenDir='fullfile(';
    for ii=1:length(tempCodegenDir)
        codegenDir=[codegenDir,'''',tempCodegenDir{ii},'''',','];
    end
    codegenDir(end)=')';



    longestLatPaths=res{1};
    numLongestLatPaths=length(longestLatPaths);

    loopRegions=res{2};
    numLoopRegions=length(loopRegions);

    uncharacterized=res{3};
    numUncharacterized=length(uncharacterized);

    baseRate=res{4};

    [~,fname,~]=fileparts(res{end});

    clearHighlightlink=sprintf(['<a href="matlab:',...
    'try ',...
    'run(''%s'');',...
    'catch;',...
    'staticLatHdlSrcDir_001xME = [];',...
    'staticLatHdlSrcDir_001x86_999=dir(''**/*.m'');',...
    'staticLatHdlSrcDir_001x86_998={staticLatHdlSrcDir_001x86_999.name};',...
    'staticLatHdlSrcDir_001x86_997=staticLatHdlSrcDir_001x86_999(find(strcmp(staticLatHdlSrcDir_001x86_998,''%s'')));',...
    'staticLatHdlSrcDir_001x86_997 = staticLatHdlSrcDir_001x86_997(find(endsWith({staticLatHdlSrcDir_001x86_997.folder},%s)));',...
    'try ',...
    'staticLatHdlSrcDir_001x86_996=cd(staticLatHdlSrcDir_001x86_997.folder);',...
    'try ',...
    'run(''%s'');',...
    'catch staticLatHdlSrcDir_001xME;',...
    'end;',...
    'cd(staticLatHdlSrcDir_001x86_996);',...
    'clear(''staticLatHdlSrcDir_001x86_996'');',...
    'catch;',...
    'end;',...
    'clear(''staticLatHdlSrcDir_001x86_997'',''staticLatHdlSrcDir_001x86_998'',''staticLatHdlSrcDir_001x86_999'');',...
    'if ~isempty(staticLatHdlSrcDir_001xME) rethrow(staticLatHdlSrcDir_001xME); end;',...
    'clear(''staticLatHdlSrcDir_001xME'');',...
    'end;',...
    '">%s</a>'],...
    res{end},[fname,'.m'],codegenDir,fname,message('hdlcoder:report:ClearHighlighting').getString);


    w=hdlhtml.reportingWizard(staticLatReportFile,'Static Latency Path Analysis Report');
    w.addCollapsibleJS;
    w.setHeader(message('hdlcoder:report:StaticLatencyPathReportTitle',model).getString);
    w.addBreak(3);


    section=w.createSectionTitle(message('hdlcoder:optimization:SectionSummary').getString);
    w.commitSection(section);
    w.addText('<hr>');

    if numUncharacterized~=0
        w.addText(message('hdlcoder:report:static_lat_uncharacterized_blocks_detected_message',message('hdlcoder:report:static_lat_uncharacterized_section_title').getString).getString);
        w.addBreak(2);
    end


    if(numLongestLatPaths==0)
        w.addText(message('hdlcoder:report:static_lat_no_path_found_message').getString);
        w.addBreak(2);
    end


    if numLoopRegions~=0
        w.addText(message('hdlcoder:report:static_lat_loop_detected_message',message('hdlcoder:report:static_lat_loop_section_title').getString).getString);
        w.addBreak(2);
    end

    if numLongestLatPaths~=0
        latValues=zeros(1,numLongestLatPaths);

        for ii=1:numLongestLatPaths
            latValues(ii)=abs(longestLatPaths(ii).Latency);
        end


        [~,longestLatPathSortedIdx]=sort(latValues,'descend');

        if~isempty(longestLatPaths(longestLatPathSortedIdx(1)).firstComp)
            w.addText(message('hdlcoder:report:static_lat_base_rate_message',num2str(baseRate)).getString);
            w.addBreak(2);


            w.addText(message('hdlcoder:report:static_lat_longest_lat_path_delay_message',num2str(latValues(longestLatPathSortedIdx(1)))).getString);
            w.addBreak(1);
            w.addText(message('hdlcoder:report:static_lat_longest_lat_path_begin_message',hdlhtml.reportingWizard.generateSystemLink(longestLatPaths(longestLatPathSortedIdx(1)).firstComp)).getString);
            w.addBreak(1);
            w.addText(message('hdlcoder:report:static_lat_longest_lat_path_end_message',hdlhtml.reportingWizard.generateSystemLink(longestLatPaths(longestLatPathSortedIdx(1)).lastComp)).getString);
            w.addBreak(1);

            [~,fname,~]=fileparts(longestLatPaths(longestLatPathSortedIdx(1)).fname);

            cpelink=sprintf(['<a href="matlab:',...
            'try ',...
            'run(''%s'');',...
            'catch;',...
            'staticLatHdlSrcDir_001xME = [];',...
            'staticLatHdlSrcDir_001x86_999=dir(''**/*.m'');',...
            'staticLatHdlSrcDir_001x86_998={staticLatHdlSrcDir_001x86_999.name};',...
            'staticLatHdlSrcDir_001x86_997=staticLatHdlSrcDir_001x86_999(find(strcmp(staticLatHdlSrcDir_001x86_998,''%s'')));',...
            'staticLatHdlSrcDir_001x86_997 = staticLatHdlSrcDir_001x86_997(find(endsWith({staticLatHdlSrcDir_001x86_997.folder},%s)));',...
            'try ',...
            'staticLatHdlSrcDir_001x86_996=cd(staticLatHdlSrcDir_001x86_997.folder);',...
            'try ',...
            'run(''%s'');',...
            'catch staticLatHdlSrcDir_001xME;',...
            'end;',...
            'cd(staticLatHdlSrcDir_001x86_996);',...
            'clear(''staticLatHdlSrcDir_001x86_996'');',...
            'catch;',...
            'end;',...
            'clear(''staticLatHdlSrcDir_001x86_997'',''staticLatHdlSrcDir_001x86_998'',''staticLatHdlSrcDir_001x86_999'');',...
            'if ~isempty(staticLatHdlSrcDir_001xME) rethrow(staticLatHdlSrcDir_001xME); end;',...
            'clear(''staticLatHdlSrcDir_001xME'');',...
            'end;',...
            '">%s.m</a>'],...
            longestLatPaths(longestLatPathSortedIdx(1)).fname,[fname,'.m'],codegenDir,...
            fname,longestLatPaths(longestLatPathSortedIdx(1)).fname);


            w.addText(message('hdlcoder:report:static_lat_longest_lat_path_highlight_message',cpelink).getString);
            w.addBreak(2);


            if topLevelModel
                w.addText(clearHighlightlink);
            end
            w.addBreak(3);
        end
    end


    if numLongestLatPaths>1
        section=w.createSectionTitle(message('hdlcoder:report:static_lat_latency_section_title').getString);
        w.commitSection(section);
        w.addText('<hr>');


        smtable=w.createTable(numLongestLatPaths,4);
        smtable.getData.setColWidth(1,1);
        smtable.setColHeading(1,message('hdlcoder:report:static_lat_latency_path_table_heading').getString);
        smtable.setColHeading(2,message('hdlcoder:report:static_lat_latency_path_begin_table_heading').getString);
        smtable.setColHeading(3,message('hdlcoder:report:static_lat_latency_path_end_table_heading').getString);
        smtable.setColHeading(4,message('hdlcoder:report:static_lat_latency_cycles_table_heading').getString);


        for ii=1:numLongestLatPaths

            cpelink=sprintf('<a href="matlab:run(''%s'')">%s %d</a>',longestLatPaths(longestLatPathSortedIdx(ii)).fname,message('hdlcoder:report:static_lat_latency_path_table_heading').getString,ii);
            smtable.createEntry(ii,1,cpelink);

            smtable.createEntry(ii,2,hdlhtml.reportingWizard.generateSystemLink(longestLatPaths(longestLatPathSortedIdx(ii)).firstComp));

            smtable.createEntry(ii,3,hdlhtml.reportingWizard.generateSystemLink(longestLatPaths(longestLatPathSortedIdx(ii)).lastComp));

            smtable.createEntry(ii,4,num2str(latValues(longestLatPathSortedIdx(ii))));
        end
    end


    if numLoopRegions~=0
        section=w.createSectionTitle(message('hdlcoder:report:static_lat_loop_section_title').getString);
        w.commitSection(section);
        w.addText('<hr>');


        smtable=w.createTable(numLoopRegions,2);
        smtable.getData.setColWidth(1,1);
        smtable.setColHeading(1,message('hdlcoder:report:static_lat_loop_region_table_heading').getString);
        smtable.setColHeading(2,message('hdlcoder:report:static_lat_loop_feedback_signal_table_heading').getString);


        for ii=1:numLoopRegions

            [~,fname,~]=fileparts(loopRegions(ii).fname);

            cpelink=sprintf(['<a href="matlab:',...
            'try ',...
            'run(''%s'');',...
            'catch;',...
            'staticLatHdlSrcDir_001xME = [];',...
            'staticLatHdlSrcDir_001x86_999=dir(''**/*.m'');',...
            'staticLatHdlSrcDir_001x86_998={staticLatHdlSrcDir_001x86_999.name};',...
            'staticLatHdlSrcDir_001x86_997=staticLatHdlSrcDir_001x86_999(find(strcmp(staticLatHdlSrcDir_001x86_998,''%s'')));',...
            'staticLatHdlSrcDir_001x86_997 = staticLatHdlSrcDir_001x86_997(find(endsWith({staticLatHdlSrcDir_001x86_997.folder},%s)));',...
            'try ',...
            'staticLatHdlSrcDir_001x86_996=cd(staticLatHdlSrcDir_001x86_997.folder);',...
            'try ',...
            'run(''%s'');',...
            'catch staticLatHdlSrcDir_001xME;',...
            'end;',...
            'cd(staticLatHdlSrcDir_001x86_996);',...
            'clear(''staticLatHdlSrcDir_001x86_996'');',...
            'catch;',...
            'end;',...
            'clear(''staticLatHdlSrcDir_001x86_997'',''staticLatHdlSrcDir_001x86_998'',''staticLatHdlSrcDir_001x86_999'');',...
            'if ~isempty(staticLatHdlSrcDir_001xME) rethrow(staticLatHdlSrcDir_001xME); end;',...
            'clear(''staticLatHdlSrcDir_001xME'');',...
            'end;',...
            '">%s %d</a>'],...
            loopRegions(ii).fname,[fname,'.m'],codegenDir,fname,message('hdlcoder:report:static_lat_loop_region_table_heading').getString,ii);
            smtable.createEntry(ii,1,cpelink);

            backEdgesStr='';

            for bidx=1:length(loopRegions(ii).BackEdges)

                if~isempty(loopRegions(ii).BackEdges{bidx})
                    cpelink=sprintf(['<a href="matlab:',...
                    'staticLatHdlSrcDir_001xME = [];',...
                    'open_system([%s, filesep, ''%s'']);',...
                    'staticLatHdlSrcDir_001x86_999=dir(''**/*.m'');',...
                    'staticLatHdlSrcDir_001x86_998={staticLatHdlSrcDir_001x86_999.name};',...
                    'staticLatHdlSrcDir_001x86_997=staticLatHdlSrcDir_001x86_999(find(strcmp(staticLatHdlSrcDir_001x86_998,''%s'')));',...
                    'staticLatHdlSrcDir_001x86_997 = staticLatHdlSrcDir_001x86_997(find(endsWith({staticLatHdlSrcDir_001x86_997.folder},%s)));',...
                    'try ',...
                    'staticLatHdlSrcDir_001x86_996=cd(staticLatHdlSrcDir_001x86_997.folder);',...
                    'try ',...
                    '%s(''%s'',''%s'');',...
                    'catch staticLatHdlSrcDir_001xME;',...
                    'end;',...
                    'cd(staticLatHdlSrcDir_001x86_996);',...
                    'clear(''staticLatHdlSrcDir_001x86_996'');',...
                    'catch;',...
                    'end;',...
                    'clear(''staticLatHdlSrcDir_001x86_997'',''staticLatHdlSrcDir_001x86_998'',''staticLatHdlSrcDir_001x86_999'');',...
                    'if ~isempty(staticLatHdlSrcDir_001xME) rethrow(staticLatHdlSrcDir_001xME); end;',...
                    'clear(''staticLatHdlSrcDir_001xME'');',...
                    '">%s %d</a>'],...
                    codegenDir,outModelName,[loopRegions(1).backEdgeFileName,'.m'],codegenDir,...
                    loopRegions(1).backEdgeFileName,loopRegions(ii).BackEdges{bidx}{1},loopRegions(ii).BackEdges{bidx}{2},...
                    message('hdlcoder:report:static_lat_loop_feedback_signal_table_heading').getString,bidx);

                    if isempty(backEdgesStr)
                        backEdgesStr=cpelink;
                    else
                        backEdgesStr=[backEdgesStr,'<br>',cpelink];
                    end
                end
            end

            smtable.createEntry(ii,2,backEdgesStr);
        end

        w.commitTable(smtable);
        w.addBreak(1);


        if topLevelModel
            w.addText(clearHighlightlink);
        end
        w.addBreak(3);
    end


    if numUncharacterized~=0
        section=w.createSectionTitle(message('hdlcoder:report:static_lat_uncharacterized_section_title').getString);
        w.commitSection(section);
        w.addText('<hr>');


        smtable=w.createTable(numUncharacterized,1);
        smtable.getData.setColWidth(1,1);
        smtable.setColHeading(1,message('hdlcoder:report:static_lat_uncharacterized_table_heading').getString);


        for ii=1:numUncharacterized
            smtable.createEntry(ii,1,hdlhtml.reportingWizard.generateSystemLink(uncharacterized{ii}));
        end
        w.commitTable(smtable);
        w.addBreak(1);


        if topLevelModel
            w.addText(clearHighlightlink);
        end
        w.addBreak(3);
    end


    w.dumpHTML;
end



