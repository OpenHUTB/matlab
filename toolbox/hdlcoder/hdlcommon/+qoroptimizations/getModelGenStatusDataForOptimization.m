function newHDLCodeGenStatus=getModelGenStatusDataForOptimization(codeGenDriver,p)




    newHDLCodeGenStatus=incrementalcodegen.IncrementalCodeGenDriver.getModelGenerationStatusData(codeGenDriver,p);




    newHDLCodeGenStatus.TopNetworkPortInfo=[];


    fieldsToIgnore={'TargetDirectory',...
    'Backannotation',...
    'GenerateValidationModel',...
    'OptimizationData',...
    'CPGuidanceFile',...
    'CPAnnotationFile',...
    'LatencyConstraint',...
    'GuidedRetiming',...
    'SignalNamesMangling',...
    'GenerateModel',...
    'GenerateHDLCode',...
    'CriticalPathEstimation',...
    'IncrementalCodeGenForTopModel'};
    for i=1:length(fieldsToIgnore)
        newHDLCodeGenStatus.CLI=regexprep(newHDLCodeGenStatus.CLI,['\n',fieldsToIgnore{i},'[^\n]*\n'],'\n');
    end
end


