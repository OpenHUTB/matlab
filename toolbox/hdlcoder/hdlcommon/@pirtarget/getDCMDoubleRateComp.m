function hC=getDCMDoubleRateComp(hN,hInSignals,hOutSignals,dcmName,dcmFXMul,dcmFXDiv,dcmClkInPeriod)




    if nargin<7
        dcmClkInPeriod=0;
    end

    if nargin<6
        dcmFXDiv=0;
    end

    if nargin<5
        dcmFXMul=0;
    end

    if~strcmp(dcmName,'DCM_SP')

        error(message('hdlcommon:workflow:UnsupportedCMFamily',dcmName));
    else

        genericListStr=sprintf(...
        ['{{''CLKFX_MULTIPLY'', ''%d''}, ',...
        '{''CLKFX_DIVIDE'', ''%d''}, ',...
        '{''CLKIN_PERIOD'', ''%0.3f'', ''real''}}'],...
        dcmFXMul,dcmFXDiv,dcmClkInPeriod);

        hC=pirelab.getInstantiationComp(...
        'Network',hN,...
        'Name',dcmName,...
        'EntityName',dcmName,...
        'InportNames',{'CLKIN','CLKFB','RST','PSEN'},...
        'OutportNames',{'CLK0','CLKFX','CLKFX180','LOCKED'},...
        'InportSignals',hInSignals,...
        'OutportSignals',hOutSignals,...
        'AddClockPort','off',...
        'AddClockEnablePort','off',...
        'AddResetPort','off',...
        'InlineConfigurations','off',...
        'GenericList',genericListStr...
        );


    end


