function fillFromSignalStruct(this,signalStruct)





    this.blockPath=signalStruct.blockPath;
    this.portIndex=signalStruct.portIndex;
    this.stateName=signalStruct.stateName;
    this.signalName=signalStruct.signalName;
    this.SimulationDataBlockPath=signalStruct.SimulationDataBlockPath;
    this.signalType=signalStruct.signalType;

end
