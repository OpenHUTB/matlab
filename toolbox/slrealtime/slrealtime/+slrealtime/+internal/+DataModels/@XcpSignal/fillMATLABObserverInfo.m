function fillMATLABObserverInfo(this,mlodata)






    this.attachMatlabObs=true;
    this.matlabObsFcn=mlodata.matlabObsFcn;
    this.matlabObsParam=mlodata.matlabObsParam;
    this.matlabObsCallbackGroup=mlodata.matlabObsCallbackGroup;
    this.matlabObsFuncHandle=mlodata.matlabObsFuncHandle;
    this.matlabObsDropIfBusy=mlodata.matlabObsDropIfBusy;

end
