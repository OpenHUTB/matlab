function addSignal(this,displayText,blockPath,portIndex,signalLabel)







    controlName=this.getUniqueControlName();



    controlType=this.SignalControlTypes{2};



    this.createComponentForPropsMap(controlName,controlType);

    signal=createSignal(...
    blockPath,...
    portIndex,...
    signalLabel,...
    '',...
    controlName,...
    controlType,...
    '',...
    '',...
    '',...
    '',...
    num2str(slrealtime.instrument.LineStyle.WidthDefault),...
    slrealtime.instrument.LineStyle.StyleDefault,...
    num2str(slrealtime.instrument.LineStyle.ColorDefault),...
    slrealtime.instrument.LineStyle.MarkerDefault,...
    num2str(slrealtime.instrument.LineStyle.MarkerSizeDefault),...
    true,...
true...
    );



    if isempty(this.BindingData)
        this.BindingData={signal};
    else
        this.BindingData{end+1}=signal;
    end



    rowdata{this.BindingTableTypeColIdx}='';
    rowdata{this.BindingTableAppDataColIdx}=displayText;
    rowdata{this.BindingTableControlNameColIdx}=this.BindingData{end}.ControlName;
    rowdata{this.BindingTableControlTypeColIdx}=this.BindingData{end}.ControlType;
    this.BindingTable.Data(end+1,:)=rowdata;



    this.refreshStyles();
end

