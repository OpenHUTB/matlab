    parsedData.UniqueIdentifier=data.ClientProperties.BlockIdentifier;
    parsedData.Path=data.ClientProperties.Path;
    parsedData.RefMdlPath=data.ClientProperties.ModelReferencePath;
    parsedData.DataTypeName=data.ClientProperties.DataTypeName;
    parsedData.ElementName=data.ClientProperties.ElementName;
    parsedData.MinValue=data.InstrumentationData.Minimum;
    parsedData.MaxValue=data.InstrumentationData.Maximum;
    parsedData.numZeros=data.InstrumentationData.NumberOfZeroes;
    parsedData.isAlwaysInteger=data.InstrumentationData.IsAlwaysInteger;

    tempHistogramData=reshape(data.InstrumentationData.HistogramData,3,[])';
    parsedData.histogramValues=int32([tempHistogramData(:,1),tempHistogramData(:,3),tempHistogramData(:,2)]);

    if slfeature('ObservedPrecision')
        tempPrecisionHistogramData=reshape(data.InstrumentationData.PrecisionHistogramData,3,[])';
        parsedData.precisionHistogramValues=int32([tempPrecisionHistogramData(:,1),tempPrecisionHistogramData(:,2),tempPrecisionHistogramData(:,3)]);
    end

    if~isempty(data.ClientProperties.OverflowLogs)
        parsedData.OverflowOccurred=data.ClientProperties.OverflowLogs.OverflowOccurred;
        parsedData.SaturationOccurred=data.ClientProperties.OverflowLogs.SaturationOccurred;
        parsedData.DivisionByZeroOccurred=data.ClientProperties.OverflowLogs.DivisionByZeroOccurred;
    end
    SimulinkFixedPoint.ApplicationData.addDataFromSrc(parsedData);

end

