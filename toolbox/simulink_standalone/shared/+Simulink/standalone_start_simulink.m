function start_simulink









    persistent isSimulinkStarted;
    if isempty(isSimulinkStarted)
        isSimulinkStarted=true;
        Simulink.SimulationInput();
        Simulink.RapidAccelerator.getStandaloneModelInterface('simulink');
        rapid_accel_target_utils('get_opt','verbose',0);
        Simulink.SimulationData.SerializeInput.RtInpNonDatasetSerializer(...
        [],...
        'simulink',...
        [],...
        [],...
        [],...
        [],...
        [],...
        [],...
        []...
        );
        Simulink.SimulationData.SerializeInput.RtInpDatasetSerializer(...
        [],...
        'simulink',...
        [],...
        [],...
        [],...
        [],...
        [],...
        [],...
        []...
        );
    end
end

