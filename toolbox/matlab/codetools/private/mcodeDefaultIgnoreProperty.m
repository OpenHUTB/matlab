function bool=mcodeDefaultIgnoreProperty(hObj,hProp)





    bool=false;

    prop_name=hProp.Name;
    if ishghandle(hObj,'figure')
        bool=localHGFigure_mcodeIgnoreProperty(hObj,prop_name)...
        ||localGenericHG_mcodeIgnoreProperty(hObj,prop_name);
    elseif ishghandle(hObj,'axes')
        bool=localHGAxes_mcodeIgnoreProperty(hObj,prop_name)...
        ||localGenericHG_mcodeIgnoreProperty(hObj,prop_name);
    elseif isa(hObj,'matlab.graphics.Graphics')
        bool=localGenericHG_mcodeIgnoreProperty(hObj,prop_name);
    end



    function bool=localGenericHG_mcodeIgnoreProperty(hObj,prop_name)


        ignore_props={...
        'BeingDeleted',...
        'ButtonDownFcn',...
        'Children',...
        'CreateFcn',...
        'DeleteFcn',...
        'BusyAction',...
        'HandleVisibility',...
        'Interactions',...
        'Interruptible',...
        'Selected',...
        'SelectionHighlight',...
        'Type',...
        'UIContextMenu',...
        'UserData'};

        bool=localShouldIgnore(hObj,ignore_props,prop_name);


        function bool=localHGFigure_mcodeIgnoreProperty(hObj,prop_name)


            ignore_props={...
            'Alphamap',...
            'BackingStore',...
            'CloseRequestFcn',...
            'CurrentAxes',...
            'CurrentCharacter',...
            'CurrentObject',...
            'CurrentPoint',...
            'DoubleBuffer',...
            'DockControls',...
            'FileName',...
            'InnerPosition',...
            'KeyPressFcn',...
            'MenuBar',...
            'NextPlot',...
            'Parent',...
            'Pointer',...
            'PointerShapeCData',...
            'PointerShapeHotSpot',...
            'PaperPosition',...
            'Position',...
            'RendererMode',...
            'Resize',...
            'ResizeFcn',...
            'SelectionType',...
            'ShareColors',...
            'ToolBar',...
            'Units',...
            'WindowButtonDownFcn',...
            'WindowButtonMotionFcn',...
            'WindowButtonUpFcn',...
            'WindowStyle',...
            'WVisual',...
            'WVisualMode'};

            bool=localShouldIgnore(hObj,ignore_props,prop_name);


            function bool=localHGAxes_mcodeIgnoreProperty(hObj,prop_name)


                ignore_props={...
                'Title',...
                'XLabel',...
                'YLabel',...
                'ZLabel',...
                'XLim',...
                'YLim',...
                'ZLim'};


                bool=~strcmp(prop_name,'View')...
                &&localShouldIgnore(hObj,ignore_props,prop_name);



                function ret=localShouldIgnore(hObj,ignore_props,prop_name)




                    ret=any(strcmp(prop_name,ignore_props))...
                    ||local_is_prop_mode_auto(hObj,prop_name);



                    function ignore=local_is_prop_mode_auto(h,prop_name)





                        ignore=false;


                        if numel(prop_name)>3&&strcmp(prop_name(end-3:end),'Mode')
                            ignore=true;
                            return
                        end



                        if~ignore
                            mode_property_name=[prop_name,'Mode'];
                            p=findprop(h,mode_property_name);


                            if~isempty(p)&&ischar(p.GetAccess)&&strcmp(p.GetAccess,'public')&&strcmp(h.(mode_property_name),'auto')
                                ignore=true;
                            end
                        end
